MODULE SUBROUTINE ACDC_WIPE_TSPNG (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSPNG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LRSPONGF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LRSPONGF = ALLOCATED (SELF%RSPONGF)
IF (LRSPONGF) THEN
  !$ACC EXIT DATA DETACH (SELF%RSPONGF)
  !$ACC EXIT DATA DELETE (SELF%RSPONGF)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
