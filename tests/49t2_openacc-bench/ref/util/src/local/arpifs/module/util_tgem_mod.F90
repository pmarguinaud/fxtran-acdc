  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NLOENG (IL1(1):IU1(1)))
  READ (KLUN) SELF%NLOENG
ENDIF
READ (KLUN) LNMEN
IF (LNMEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NMEN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NMEN
ENDIF
READ (KLUN) LNMENG
IF (LNMENG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NMENG (IL1(1):IU1(1)))
  READ (KLUN) SELF%NMENG
ENDIF
READ (KLUN) LNDGLU
IF (LNDGLU) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NDGLU (IL1(1):IU1(1)))
  READ (KLUN) SELF%NDGLU
ENDIF
READ (KLUN) LNSTAGP
