SUBMODULE (CPG_GMV_TYPE_MOD) CPG_GMV_TYPE_MOD_CPG_GMV_TYPE_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_HOST_CPG_GMV_TYPE (SELF)

IMPLICIT NONE
CLASS (CPG_GMV_TYPE), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LGMV

CALL SELF%U%ACDC_HOST ()

CALL SELF%V%ACDC_HOST ()

CALL SELF%T%ACDC_HOST ()

CALL SELF%SPD%ACDC_HOST ()

CALL SELF%SVD%ACDC_HOST ()

CALL SELF%NHX%ACDC_HOST ()

LGMV = ALLOCATED (SELF%GMV)
IF (LGMV) THEN
  DO J1 = LBOUND (SELF%GMV, 1), UBOUND (SELF%GMV, 1)
    CALL SELF%GMV (J1)%ACDC_HOST ()
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
