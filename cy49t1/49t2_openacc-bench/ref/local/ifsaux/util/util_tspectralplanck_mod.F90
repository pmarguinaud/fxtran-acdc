SUBROUTINE LOAD_TSPECTRALPLANCK (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSPECTRALPLANCK), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LINTERVAL_MAP, LPLANCK_LUT, LWAVLEN_BOUND
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%NINTERVALS
READ (KLUN) YD%NTEMPS
READ (KLUN) ZTMP0
YD%TEMP1 = ZTMP0
READ (KLUN) ZTMP0
YD%DTEMP = ZTMP0
READ (KLUN) LPLANCK_LUT
IF (LPLANCK_LUT) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%PLANCK_LUT (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%PLANCK_LUT,1):UBOUND(YD%PLANCK_LUT,1), LBOUND(YD%PLANCK_LUT,2):UBOUND(YD%PLANCK_LUT,2)))
  READ (KLUN) ZTMP2
  YD%PLANCK_LUT = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LWAVLEN_BOUND
IF (LWAVLEN_BOUND) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%WAVLEN_BOUND (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%WAVLEN_BOUND,1):UBOUND(YD%WAVLEN_BOUND,1)))
  READ (KLUN) ZTMP1
  YD%WAVLEN_BOUND = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LINTERVAL_MAP
IF (LINTERVAL_MAP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%INTERVAL_MAP (IL1(1):IU1(1)))
  READ (KLUN) YD%INTERVAL_MAP
ENDIF
END SUBROUTINE


