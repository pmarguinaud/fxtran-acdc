        &, Z_YDMF_PHYS_BASE_STATE_YGSP_RR_T(:, JBLK), ZAESEA(:, JBLK), ZAELAN(:, JBLK), ZAESOO(:, JBLK), ZAEDES&
        &(:, JBLK), ZAESUL(:, JBLK), ZAEVOL(:, JBLK), PAER(:,:,:, JBLK), PAERINDS(:,:, JBLK))
