CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TEDIM (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEDIM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LNISMAX, LNISNAX
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LNISMAX = ASSOCIATED (SELF%NISMAX)
IF (LNISMAX) THEN
  !$ACC EXIT DATA DETACH (SELF%NISMAX)
  !$ACC EXIT DATA DELETE (SELF%NISMAX)
ENDIF

LNISNAX = ASSOCIATED (SELF%NISNAX)
IF (LNISNAX) THEN
  !$ACC EXIT DATA DETACH (SELF%NISNAX)
  !$ACC EXIT DATA DELETE (SELF%NISNAX)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
