    STOP 1
ENDIF


IF (KIND (ZWU2H) == 8) THEN
    fxtran_acdc_alloc8 (ZWU2H)
ELSEIF (KIND (ZWU2H) == 4) THEN
    fxtran_acdc_alloc4 (ZWU2H)
ELSE
    STOP 1
ENDIF


IF (KIND (ZBUOH) == 8) THEN
    fxtran_acdc_alloc8 (ZBUOH)
ELSEIF (KIND (ZBUOH) == 4) THEN
    fxtran_acdc_alloc4 (ZBUOH)
ELSE
    STOP 1
