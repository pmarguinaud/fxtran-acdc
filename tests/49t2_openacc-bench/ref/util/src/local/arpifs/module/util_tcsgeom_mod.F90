FUNCTION ACDC_SIZE_TCSGEOM (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCSGEOM),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRCOLON = ASSOCIATED (SELF%RCOLON)
IF (LRCOLON) THEN
  ISIZE = KIND (SELF%RCOLON) * SIZE (SELF%RCOLON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RCOLON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSILON = ASSOCIATED (SELF%RSILON)
IF (LRSILON) THEN
  ISIZE = KIND (SELF%RSILON) * SIZE (SELF%RSILON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSILON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRINDX = ASSOCIATED (SELF%RINDX)
IF (LRINDX) THEN
  ISIZE = KIND (SELF%RINDX) * SIZE (SELF%RINDX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RINDX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRINDY = ASSOCIATED (SELF%RINDY)
IF (LRINDY) THEN
  ISIZE = KIND (SELF%RINDY) * SIZE (SELF%RINDY)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RINDY'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRATATH = ASSOCIATED (SELF%RATATH)
IF (LRATATH) THEN
  ISIZE = KIND (SELF%RATATH) * SIZE (SELF%RATATH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RATATH'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRATATX = ASSOCIATED (SELF%RATATX)
IF (LRATATX) THEN
