MODULE SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_VDIAGO2 (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VDIAGO2), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_OCDEP, LF_USTRC, LF_VSTRC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_OCDEP = ASSOCIATED (SELF%F_OCDEP)
IF (LF_OCDEP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_OCDEP)
  !$ACC ENTER DATA ATTACH (SELF%F_OCDEP)
ENDIF

LF_USTRC = ASSOCIATED (SELF%F_USTRC)
IF (LF_USTRC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_USTRC)
  !$ACC ENTER DATA ATTACH (SELF%F_USTRC)
ENDIF

LF_VSTRC = ASSOCIATED (SELF%F_VSTRC)
IF (LF_VSTRC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_VSTRC)
  !$ACC ENTER DATA ATTACH (SELF%F_VSTRC)
ENDIF

END SUBROUTINE

END SUBMODULE
