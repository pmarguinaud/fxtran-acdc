SUBROUTINE HL2FL_OPENACC (KIDIA, KFDIA, KLON, KTDIAT&
&, KLEV, PAPRS, PAPRSF, PXHL, KINI, PXFL, YDSTACK)
!$ACC ROUTINE (HL2FL_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KTDIAT
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
REAL (KIND=JPRB), INTENT (IN)::PAPRS (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPRSF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PXHL (KLON, KINI:KLEV)
INTEGER (KIND=JPIM), INTENT (IN)::KINI
REAL (KIND=JPRB), INTENT (OUT)::PXFL (KLON, KLEV)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JLEV


JLON = KIDIA



IF (KINI==1) THEN

  DO JLEV=KTDIAT+1, KLEV
    
    PXFL (JLON, JLEV)=((PXHL (JLON, JLEV)-PXHL (JLON, JLEV-1))*PAPRSF (JLON, JLEV)+PXHL (JLON, JLEV-1)*PAPRS&
    & (JLON, JLEV)-PXHL (JLON, JLEV)*PAPRS (JLON, JLEV-1))/(PAPRS (JLON, JLEV)-PAPRS (JLON, JLEV-1))
  
  ENDDO

  
  PXFL (JLON, KTDIAT)=PXFL (JLON, KTDIAT+1)
  
ELSE

  DO JLEV=KTDIAT, KLEV
    
    PXFL (JLON, JLEV)=((PXHL (JLON, JLEV)-PXHL (JLON, JLEV-1))*PAPRSF (JLON, JLEV)+PXHL (JLON, JLEV-1)*PAPRS&
    & (JLON, JLEV)-PXHL (JLON, JLEV)*PAPRS (JLON, JLEV-1))/(PAPRS (JLON, JLEV)-PAPRS (JLON, JLEV-1))
  
  ENDDO

ENDIF


END SUBROUTINE HL2FL_OPENACC
