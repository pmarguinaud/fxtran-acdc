SUBROUTINE ACDC_SAVE_TELBC_GEO (SELF, KLUN)

IMPLICIT NONE
TYPE (TELBC_GEO), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LMPTRCPLBLK, LNIND_LEN, LNIND_LIST
LNIND_LIST = ALLOCATED (SELF%NIND_LIST)
WRITE (KLUN) LNIND_LIST
IF (LNIND_LIST) THEN
  WRITE (KLUN) LBOUND (SELF%NIND_LIST)
  WRITE (KLUN) UBOUND (SELF%NIND_LIST)
  WRITE (KLUN) SELF%NIND_LIST
ENDIF
LNIND_LEN = ALLOCATED (SELF%NIND_LEN)
WRITE (KLUN) LNIND_LEN
IF (LNIND_LEN) THEN
  WRITE (KLUN) LBOUND (SELF%NIND_LEN)
  WRITE (KLUN) UBOUND (SELF%NIND_LEN)
  WRITE (KLUN) SELF%NIND_LEN
ENDIF
WRITE (KLUN) SELF%NCPLBLKS
LMPTRCPLBLK = ALLOCATED (SELF%MPTRCPLBLK)
WRITE (KLUN) LMPTRCPLBLK
IF (LMPTRCPLBLK) THEN
  WRITE (KLUN) LBOUND (SELF%MPTRCPLBLK)
  WRITE (KLUN) UBOUND (SELF%MPTRCPLBLK)
  WRITE (KLUN) SELF%MPTRCPLBLK
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TELBC_GEO (SELF, KLUN)

IMPLICIT NONE
TYPE (TELBC_GEO), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LMPTRCPLBLK, LNIND_LEN, LNIND_LIST
READ (KLUN) LNIND_LIST
IF (LNIND_LIST) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NIND_LIST (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NIND_LIST
ENDIF
READ (KLUN) LNIND_LEN
IF (LNIND_LEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NIND_LEN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NIND_LEN
ENDIF
READ (KLUN) SELF%NCPLBLKS
READ (KLUN) LMPTRCPLBLK
IF (LMPTRCPLBLK) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MPTRCPLBLK (IL1(1):IU1(1)))
  READ (KLUN) SELF%MPTRCPLBLK
ENDIF
END SUBROUTINE

