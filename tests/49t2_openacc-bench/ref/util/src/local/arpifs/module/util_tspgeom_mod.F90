FUNCTION ACDC_SIZE_TSPGEOM (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TSPGEOM),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGMR, LSCGMAP
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LGMR = ALLOCATED (SELF%GMR)
IF (LGMR) THEN
  ISIZE = KIND (SELF%GMR) * SIZE (SELF%GMR)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GMR'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSCGMAP = ALLOCATED (SELF%SCGMAP)
IF (LSCGMAP) THEN
  ISIZE = KIND (SELF%SCGMAP) * SIZE (SELF%SCGMAP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SCGMAP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%ESCGMAP) * SIZE (SELF%ESCGMAP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ESCGMAP'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

