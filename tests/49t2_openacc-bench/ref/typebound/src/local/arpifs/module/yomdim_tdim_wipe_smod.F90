CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TDIM (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TDIM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LNDLUNL, LNDLUXL
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LNDLUNL = ALLOCATED (SELF%NDLUNL)
IF (LNDLUNL) THEN
  !$ACC EXIT DATA DETACH (SELF%NDLUNL)
  !$ACC EXIT DATA DELETE (SELF%NDLUNL)
ENDIF

LNDLUXL = ALLOCATED (SELF%NDLUXL)
IF (LNDLUXL) THEN
  !$ACC EXIT DATA DETACH (SELF%NDLUXL)
  !$ACC EXIT DATA DELETE (SELF%NDLUXL)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
