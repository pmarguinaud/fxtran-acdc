MODULE SUBROUTINE ACDC_WIPE_TMODERR (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TMODERR), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LMSTEPERR = ALLOCATED (SELF%MSTEPERR)
IF (LMSTEPERR) THEN
  !$ACC EXIT DATA DETACH (SELF%MSTEPERR)
  !$ACC EXIT DATA DELETE (SELF%MSTEPERR)
ENDIF

LTSTEPERR = ALLOCATED (SELF%TSTEPERR)
IF (LTSTEPERR) THEN
  !$ACC EXIT DATA DETACH (SELF%TSTEPERR)
  !$ACC EXIT DATA DELETE (SELF%TSTEPERR)
ENDIF

LFSTEPERR = ALLOCATED (SELF%FSTEPERR)
IF (LFSTEPERR) THEN
  !$ACC EXIT DATA DETACH (SELF%FSTEPERR)
  !$ACC EXIT DATA DELETE (SELF%FSTEPERR)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
