MODULE SUBROUTINE ACDC_WIPE_TOROG (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TOROG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LOROG = ALLOCATED (SELF%OROG)
IF (LOROG) THEN
  !$ACC EXIT DATA DETACH (SELF%OROG)
  !$ACC EXIT DATA DELETE (SELF%OROG)
ENDIF

LOROGL = ALLOCATED (SELF%OROGL)
IF (LOROGL) THEN
  !$ACC EXIT DATA DETACH (SELF%OROGL)
  !$ACC EXIT DATA DELETE (SELF%OROGL)
ENDIF

LOROGM = ALLOCATED (SELF%OROGM)
IF (LOROGM) THEN
  !$ACC EXIT DATA DETACH (SELF%OROGM)
  !$ACC EXIT DATA DELETE (SELF%OROGM)
ENDIF

LOROGLL = ALLOCATED (SELF%OROGLL)
IF (LOROGLL) THEN
  !$ACC EXIT DATA DETACH (SELF%OROGLL)
  !$ACC EXIT DATA DELETE (SELF%OROGLL)
ENDIF

LOROGMM = ALLOCATED (SELF%OROGMM)
IF (LOROGMM) THEN
  !$ACC EXIT DATA DETACH (SELF%OROGMM)
  !$ACC EXIT DATA DELETE (SELF%OROGMM)
ENDIF

LOROGLM = ALLOCATED (SELF%OROGLM)
IF (LOROGLM) THEN
  !$ACC EXIT DATA DETACH (SELF%OROGLM)
  !$ACC EXIT DATA DELETE (SELF%OROGLM)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
