SUBROUTINE ACDC_LOAD_TEAERD (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TEAERD), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCVDAED, LCVDAEL, LCVDAES, LCVDAEU
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LCVDAES
IF (LCVDAES) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CVDAES (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%CVDAES,1):UBOUND(SELF%CVDAES,1)))
  READ (KLUN) ZTMP1
  SELF%CVDAES = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCVDAEL
IF (LCVDAEL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CVDAEL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%CVDAEL,1):UBOUND(SELF%CVDAEL,1)))
  READ (KLUN) ZTMP1
  SELF%CVDAEL = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCVDAEU
IF (LCVDAEU) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CVDAEU (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%CVDAEU,1):UBOUND(SELF%CVDAEU,1)))
  READ (KLUN) ZTMP1
  SELF%CVDAEU = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCVDAED
IF (LCVDAED) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CVDAED (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%CVDAED,1):UBOUND(SELF%CVDAED,1)))
  READ (KLUN) ZTMP1
  SELF%CVDAED = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
ALLOCATE (ZTMP1(LBOUND(SELF%RAESC,1):UBOUND(SELF%RAESC,1)))
READ (KLUN) ZTMP1
SELF%RAESC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAESS,1):UBOUND(SELF%RAESS,1)))
READ (KLUN) ZTMP1
SELF%RAESS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAELC,1):UBOUND(SELF%RAELC,1)))
READ (KLUN) ZTMP1
SELF%RAELC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAELS,1):UBOUND(SELF%RAELS,1)))
READ (KLUN) ZTMP1
SELF%RAELS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAEUC,1):UBOUND(SELF%RAEUC,1)))
READ (KLUN) ZTMP1
SELF%RAEUC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAEUS,1):UBOUND(SELF%RAEUS,1)))
READ (KLUN) ZTMP1
SELF%RAEUS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAEDC,1):UBOUND(SELF%RAEDC,1)))
READ (KLUN) ZTMP1
SELF%RAEDC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(SELF%RAEDS,1):UBOUND(SELF%RAEDS,1)))
READ (KLUN) ZTMP1
SELF%RAEDS = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%RCAEOPS = ZTMP0
READ (KLUN) ZTMP0
SELF%RCAEOPL = ZTMP0
READ (KLUN) ZTMP0
SELF%RCAEOPU = ZTMP0
READ (KLUN) ZTMP0
SELF%RCAEOPD = ZTMP0
READ (KLUN) ZTMP0
SELF%RCTRBGA = ZTMP0
READ (KLUN) ZTMP0
SELF%RCVOBGA = ZTMP0
READ (KLUN) ZTMP0
SELF%RCSTBGA = ZTMP0
READ (KLUN) ZTMP0
SELF%RCTRPT = ZTMP0
ALLOCATE (ZTMP1(LBOUND(SELF%RCAEADK,1):UBOUND(SELF%RCAEADK,1)))
READ (KLUN) ZTMP1
SELF%RCAEADK = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
SELF%RCAEADM = ZTMP0
READ (KLUN) ZTMP0
SELF%RCAEROS = ZTMP0
END SUBROUTINE

