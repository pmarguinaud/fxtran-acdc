LOGICAL::LLFLAG 

#include "cuadjtq.func.h"
INTEGER (KIND=JPIM) :: JLON

YLSTACK = YDSTACK



IF (KIND (ILAB) == 8) THEN
    alloc8 (ILAB)
ELSEIF (KIND (ILAB) == 4) THEN
    alloc4 (ILAB)
ELSE
    STOP 1
ENDIF


IF (KIND (ZWU2H) == 8) THEN
    alloc8 (ZWU2H)
ELSEIF (KIND (ZWU2H) == 4) THEN
    alloc4 (ZWU2H)
ELSE
    STOP 1
ENDIF


IF (KIND (ZBUOH) == 8) THEN
    alloc8 (ZBUOH)
ELSEIF (KIND (ZBUOH) == 4) THEN
    alloc4 (ZBUOH)
ELSE
    STOP 1
ENDIF


IF (KIND (ZSUH) == 8) THEN
    alloc8 (ZSUH)
ELSEIF (KIND (ZSUH) == 4) THEN
    alloc4 (ZSUH)
ELSE
    STOP 1
ENDIF


IF (KIND (ZQENH) == 8) THEN
    alloc8 (ZQENH)
ELSEIF (KIND (ZQENH) == 4) THEN
    alloc4 (ZQENH)
ELSE
    STOP 1
ENDIF


IF (KIND (ZSENH) == 8) THEN
    alloc8 (ZSENH)
ELSEIF (KIND (ZSENH) == 4) THEN
    alloc4 (ZSENH)
