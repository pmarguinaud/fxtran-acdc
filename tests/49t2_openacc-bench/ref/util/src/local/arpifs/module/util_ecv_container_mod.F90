    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RGPECV3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLSM = ALLOCATED (SELF%RLSM)
