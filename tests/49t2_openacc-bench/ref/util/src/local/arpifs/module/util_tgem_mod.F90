READ (KLUN) ZTMP0
SELF%RNLGINC = ZTMP0
READ (KLUN) ZTMP0
SELF%R4JP = ZTMP0
READ (KLUN) ZTMP0
SELF%RC2P1 = ZTMP0
READ (KLUN) ZTMP0
SELF%RC2M1 = ZTMP0
READ (KLUN) ZTMP0
SELF%RCOR0 = ZTMP0
READ (KLUN) ZTMP0
SELF%RCOR1 = ZTMP0
READ (KLUN) LNLOEN
IF (LNLOEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NLOEN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NLOEN
ENDIF
