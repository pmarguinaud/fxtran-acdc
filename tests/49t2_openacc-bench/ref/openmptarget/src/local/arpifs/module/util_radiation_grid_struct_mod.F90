ENDIF
READ (KLUN) LNLOENG
IF (LNLOENG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NLOENG (IL1(1):IU1(1)))
  READ (KLUN) SELF%NLOENG
ENDIF
READ (KLUN) LNPTRFRSTLAT
