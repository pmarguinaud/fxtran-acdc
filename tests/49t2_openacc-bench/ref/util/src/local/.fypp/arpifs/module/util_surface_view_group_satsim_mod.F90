MODULE UTIL_SURFACE_VIEW_GROUP_SATSIM_MOD

USE SURFACE_VIEWS_DIAGNOSTIC_MODULE, ONLY : SURFACE_VIEW_GROUP_SATSIM



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_SURFACE_VIEW_GROUP_SATSIM
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_SURFACE_VIEW_GROUP_SATSIM
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_SURFACE_VIEW_GROUP_SATSIM
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_SATSIM (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_SATSIM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CLBT, LF_CSBT
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_CLBT = ASSOCIATED (SELF%F_CLBT)
IF (LF_CLBT .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_CLBT)
  !$ACC ENTER DATA ATTACH (SELF%F_CLBT)
ENDIF

LF_CSBT = ASSOCIATED (SELF%F_CSBT)
IF (LF_CSBT .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_CSBT)
  !$ACC ENTER DATA ATTACH (SELF%F_CSBT)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_SATSIM (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_SATSIM), TARGET :: SELF
LOGICAL :: LF_CLBT, LF_CSBT
LF_CLBT = ASSOCIATED (SELF%F_CLBT)
IF (LF_CLBT) THEN
  CALL HOST_FIELD_3RB (SELF%F_CLBT)
ENDIF

LF_CSBT = ASSOCIATED (SELF%F_CSBT)
IF (LF_CSBT) THEN
  CALL HOST_FIELD_3RB (SELF%F_CSBT)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_SURFACE_VIEW_GROUP_SATSIM (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_SATSIM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CLBT, LF_CSBT
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_CLBT = ASSOCIATED (SELF%F_CLBT)
IF (LF_CLBT .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_CLBT)
ENDIF

LF_CSBT = ASSOCIATED (SELF%F_CSBT)
IF (LF_CSBT .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_CSBT)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
