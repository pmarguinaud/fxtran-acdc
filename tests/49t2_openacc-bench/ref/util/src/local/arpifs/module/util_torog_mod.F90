SUBROUTINE ACDC_LOAD_TOROG (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TOROG), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LOROG
IF (LOROG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OROG (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OROG,1):UBOUND(SELF%OROG,1)))
  READ (KLUN) ZTMP1
  SELF%OROG = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGL
IF (LOROGL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OROGL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OROGL,1):UBOUND(SELF%OROGL,1)))
  READ (KLUN) ZTMP1
  SELF%OROGL = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGM
IF (LOROGM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OROGM (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OROGM,1):UBOUND(SELF%OROGM,1)))
  READ (KLUN) ZTMP1
  SELF%OROGM = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGLL
IF (LOROGLL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%OROGLL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%OROGLL,1):UBOUND(SELF%OROGLL,1)))
