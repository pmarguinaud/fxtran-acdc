READ (KLUN) ZTMP2
SELF%RTAESS = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP2(LBOUND(SELF%RTAESU,1):UBOUND(SELF%RTAESU,1), LBOUND(SELF%RTAESU,2):UBOUND(SELF%RTAESU,2)))
READ (KLUN) ZTMP2
SELF%RTAESU = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP1(LBOUND(SELF%RTAEVO,1):UBOUND(SELF%RTAEVO,1)))
READ (KLUN) ZTMP1
SELF%RTAEVO = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) SELF%ISYEAR
READ (KLUN) SELF%IEYEAR
READ (KLUN) SELF%IS_INITIALISED
END SUBROUTINE

