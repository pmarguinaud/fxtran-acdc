SUBROUTINE SAVE_TSRFTLAD (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSRFTLAD), INTENT (IN), TARGET :: YD
LOGICAL :: LGPTSKIN0, LGPTSKIN9
LGPTSKIN0 = ALLOCATED (YD%GPTSKIN0)
WRITE (KLUN) LGPTSKIN0
IF (LGPTSKIN0) THEN
  WRITE (KLUN) LBOUND (YD%GPTSKIN0)
  WRITE (KLUN) UBOUND (YD%GPTSKIN0)
  WRITE (KLUN) YD%GPTSKIN0
ENDIF
LGPTSKIN9 = ALLOCATED (YD%GPTSKIN9)
WRITE (KLUN) LGPTSKIN9
IF (LGPTSKIN9) THEN
  WRITE (KLUN) LBOUND (YD%GPTSKIN9)
  WRITE (KLUN) UBOUND (YD%GPTSKIN9)
  WRITE (KLUN) YD%GPTSKIN9
ENDIF
WRITE (KLUN) YD%NGSKIN
WRITE (KLUN) YD%LREGSF
END SUBROUTINE

SUBROUTINE LOAD_TSRFTLAD (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSRFTLAD), INTENT (OUT), TARGET :: YD
INTEGER :: IL3(3), IU3(3)
LOGICAL :: LGPTSKIN0, LGPTSKIN9
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LGPTSKIN0
IF (LGPTSKIN0) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%GPTSKIN0 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%GPTSKIN0,1):UBOUND(YD%GPTSKIN0,1), LBOUND(YD%GPTSKIN0,2):UBOUND(YD%GPTSKIN0,2), LBOUND(YD%GPTSKIN0,3):UBOUND(YD%GPTSKIN0,3)))
  READ (KLUN) ZTMP3
  YD%GPTSKIN0 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LGPTSKIN9
IF (LGPTSKIN9) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%GPTSKIN9 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%GPTSKIN9,1):UBOUND(YD%GPTSKIN9,1), LBOUND(YD%GPTSKIN9,2):UBOUND(YD%GPTSKIN9,2), LBOUND(YD%GPTSKIN9,3):UBOUND(YD%GPTSKIN9,3)))
  READ (KLUN) ZTMP3
  YD%GPTSKIN9 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) YD%NGSKIN
READ (KLUN) YD%LREGSF
END SUBROUTINE


