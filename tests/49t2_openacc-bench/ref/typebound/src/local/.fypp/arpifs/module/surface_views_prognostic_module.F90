  SUBROUTINE ASSIGN2_HOST (FLHS, FRHS, YDCPG_OPTS, YDCPG_BNDS)
  USE FIELD_ACCESS_MODULE
  CLASS (FIELD_2RB), POINTER :: FLHS, FRHS
  INTEGER (KIND=JPIM) :: JLON, JBLK
