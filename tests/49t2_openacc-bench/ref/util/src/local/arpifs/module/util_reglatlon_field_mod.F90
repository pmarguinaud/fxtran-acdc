FUNCTION ACDC_SIZE_REGLATLON_FIELD (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (REGLATLON_FIELD),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LPFLD, LPLAT, LPLON, LPSIN
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NLAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NLON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLON'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%DLAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%DLAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%DLON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%DLON'
ENDIF
KSIZE = KSIZE + ISIZE
LPFLD = ASSOCIATED (SELF%PFLD)
IF (LPFLD) THEN
  ISIZE = KIND (SELF%PFLD) * SIZE (SELF%PFLD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PFLD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPLAT = ASSOCIATED (SELF%PLAT)
IF (LPLAT) THEN
  ISIZE = KIND (SELF%PLAT) * SIZE (SELF%PLAT)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PLAT'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPSIN = ASSOCIATED (SELF%PSIN)
IF (LPSIN) THEN
  ISIZE = KIND (SELF%PSIN) * SIZE (SELF%PSIN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PSIN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPLON = ASSOCIATED (SELF%PLON)
IF (LPLON) THEN
  ISIZE = KIND (SELF%PLON) * SIZE (SELF%PLON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PLON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

