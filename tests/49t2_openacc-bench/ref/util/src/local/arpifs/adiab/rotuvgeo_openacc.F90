SUBROUTINE ROTUVGEO_OPENACC (KFLEV, KPROMA, KST, KEND, PGNORDM&
&, PGNORDL, PU, PV, PRU, PRV, LDIR, LDADD, LDSUB, YDSTACK)
!$ACC ROUTINE (ROTUVGEO_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


INTEGER (KIND=JPIM), INTENT (IN)::KEND
INTEGER (KIND=JPIM), INTENT (IN)::KST
INTEGER (KIND=JPIM), INTENT (IN)::KPROMA
INTEGER (KIND=JPIM), INTENT (IN)::KFLEV
LOGICAL, INTENT (IN)::LDIR
LOGICAL, INTENT (IN), OPTIONAL::LDSUB
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
LOGICAL, INTENT (IN), OPTIONAL::LDADD
REAL (KIND=JPRB), INTENT (IN)::PGNORDL (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGNORDM (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PU (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (IN)::PV (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (INOUT)::PRU (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (INOUT)::PRV (KPROMA, KFLEV)
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JROF
LOGICAL::LLSUB
LOGICAL::LLADD
REAL (KIND=JPRB)::ZGNORDL 
REAL (KIND=JPRB)::ZGNORDM 
REAL (KIND=JPRB)::ZV
REAL (KIND=JPRB)::ZU

#include "rotate.func.h"

JROF = KST


LLADD=.FALSE.

IF (PRESENT (LDADD))  THEN
  LLADD=LDADD
ENDIF

LLSUB=.FALSE.

IF (PRESENT (LDSUB))  THEN
  LLSUB=LDSUB
ENDIF


IF (LLSUB) THEN
  ZGNORDM =-PGNORDM (JROF)
  ZGNORDL =-PGNORDL (JROF)
ELSE
  ZGNORDM =PGNORDM (JROF)
  ZGNORDL =PGNORDL (JROF)
ENDIF

LLADD=LLADD.OR.LLSUB

IF (LDIR) THEN

  IF (LLADD) THEN

    DO JLEV=1, KFLEV
      
      ZU=PU (JROF, JLEV)
      ZV=PV (JROF, JLEV)
      PRU (JROF, JLEV)=FROTUADD (PRU (JROF, JLEV), ZU, ZV, ZGNORDM , ZGNORDL )
      PRV (JROF, JLEV)=FROTVADD (PRV (JROF, JLEV), ZU, ZV, ZGNORDM , ZGNORDL )
    
    ENDDO

  ELSE

    DO JLEV=1, KFLEV
      
      ZU=PU (JROF, JLEV)
      ZV=PV (JROF, JLEV)
      PRU (JROF, JLEV)=FROTU (ZU, ZV, ZGNORDM , ZGNORDL )
      PRV (JROF, JLEV)=FROTV (ZU, ZV, ZGNORDM , ZGNORDL )
    
    ENDDO

  ENDIF

ELSE

  IF (LLADD) THEN

    DO JLEV=1, KFLEV
      
      ZU=PU (JROF, JLEV)
      ZV=PV (JROF, JLEV)
      PRU (JROF, JLEV)=FUNROTUADD (PRU (JROF, JLEV), ZU, ZV, ZGNORDM , ZGNORDL )
      PRV (JROF, JLEV)=FUNROTVADD (PRV (JROF, JLEV), ZU, ZV, ZGNORDM , ZGNORDL )
    
    ENDDO

  ELSE

    DO JLEV=1, KFLEV
      
      ZU=PU (JROF, JLEV)
      ZV=PV (JROF, JLEV)
      PRU (JROF, JLEV)=FUNROTU (ZU, ZV, ZGNORDM , ZGNORDL )
      PRV (JROF, JLEV)=FUNROTV (ZU, ZV, ZGNORDM , ZGNORDL )
    
    ENDDO

  ENDIF

ENDIF


END SUBROUTINE ROTUVGEO_OPENACC
