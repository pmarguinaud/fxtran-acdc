INTEGER*8 FUNCTION SIZE_TSRFTLAD (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TSRFTLAD),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGPTSKIN0, LGPTSKIN9

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LGPTSKIN0 = ALLOCATED (YD%GPTSKIN0)
IF (LGPTSKIN0) THEN
  ISIZE = KIND (YD%GPTSKIN0) * SIZE (YD%GPTSKIN0)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GPTSKIN0'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LGPTSKIN9 = ALLOCATED (YD%GPTSKIN9)
IF (LGPTSKIN9) THEN
  ISIZE = KIND (YD%GPTSKIN9) * SIZE (YD%GPTSKIN9)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GPTSKIN9'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%NGSKIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NGSKIN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LREGSF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LREGSF'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

