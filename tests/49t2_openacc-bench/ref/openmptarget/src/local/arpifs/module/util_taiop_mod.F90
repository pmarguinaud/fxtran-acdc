FUNCTION ACDC_SIZE_TAIOP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TAIOP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRLWAAS, LRLWAME, LRLWASS, LRLWWEI, LRLWWL, LRRH, LRSUNFR, LRSWAAS, LRSWAME, LRSWASS
LOGICAL :: LRSWWL
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NAEROMR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAEROMR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NWAVSW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWAVSW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NWAVLW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWAVLW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NRELHU)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NRELHU'
ENDIF
KSIZE = KSIZE + ISIZE
LRSWAME = ALLOCATED (SELF%RSWAME)
IF (LRSWAME) THEN
  ISIZE = KIND (SELF%RSWAME) * SIZE (SELF%RSWAME)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSWAME'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLWAME = ALLOCATED (SELF%RLWAME)
IF (LRLWAME) THEN
  ISIZE = KIND (SELF%RLWAME) * SIZE (SELF%RLWAME)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RLWAME'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSWAAS = ALLOCATED (SELF%RSWAAS)
IF (LRSWAAS) THEN
  ISIZE = KIND (SELF%RSWAAS) * SIZE (SELF%RSWAAS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSWAAS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRLWAAS = ALLOCATED (SELF%RLWAAS)
IF (LRLWAAS) THEN
  ISIZE = KIND (SELF%RLWAAS) * SIZE (SELF%RLWAAS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RLWAAS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
