IMPLICIT NONE

PRIVATE

PUBLIC :: FXTRAN_ACDC_GEMV_MANYBLOCKS

CONTAINS

SUBROUTINE FXTRAN_ACDC_GEMV_MANYBLOCKS (KIDIA, KFDIA, TRANSA, TRANSB, M, N, K, ALPHA, A, &
                                       & LDA, B, LDB, BETA, C, LDC, LDDONE, LDACC,KGPBLKS,YDOFFSET)

#ifdef _FXTRAN_USE_CUBLAS
USE CUBLAS_V2
USE CUDAFOR
#endif


USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD
USE FXTRAN_ACDC_CHECKCONTIGUOUS_MOD

                                ! VERINTS
INTEGER     :: KIDIA
INTEGER     :: KFDIA
CHARACTER*1 :: TRANSA           ! 'N'
CHARACTER*1 :: TRANSB           ! 'T'
INTEGER     :: M                ! KPROMA
INTEGER     :: N                ! 1
INTEGER     :: K                ! KLEVIN
REAL*8      :: ALPHA            ! 1.0_JPRD
REAL*8      :: A (:, :,:)         ! ZIN
INTEGER     :: LDA              ! KPROMA
REAL*8      :: B (:, :)         ! PINTE
INTEGER     :: LDB              ! KLEVOUT
REAL*8      :: BETA             ! 0.0_JPRB
REAL*8      :: C (:,:)            ! POUTS
INTEGER     :: LDC              ! KPROMA
LOGICAL     :: LDDONE
LOGICAL     :: LDACC
INTEGER                  :: KGPBLKS
TYPE (FXTRAN_ACDC_STACK), OPTIONAL :: YDOFFSET

#ifdef _FXTRAN_USE_CUBLAS
TYPE (CUBLASHANDLE), SAVE :: YLCBH
LOGICAL, SAVE :: LLCBH_INITIALIZED = .FALSE.
INTEGER (KIND=CUDA_STREAM_KIND), SAVE :: STREAM = 0
#endif

INTEGER :: STRIDEA
INTEGER :: STRIDEB
INTEGER :: STRIDEC
INTEGER :: BATCHCOUNT


INTEGER :: JM, JN, JK, JBLK
CHARACTER*1, SAVE :: CLENV = ''
LOGICAL, SAVE :: LLSIMPLE_DGEMM = .FALSE.
CHARACTER (LEN=*), PARAMETER :: CLNAME = 'FXTRAN_ACDC_GEMV'

IF (CLENV == '') THEN
  CALL GETENV ('FXTRAN_ACDC_SIMPLE_DGEMM', CLENV)
  LLSIMPLE_DGEMM = CLENV (1:1) == '1' 
  CLENV = '0'
ENDIF

IF (TRANSA /= 'N') CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (TRANSB /= 'T') CALL FXTRAN_ACDC_ABORT (CLNAME)

CALL FXTRAN_ACDC_CHECKCONTIGUOUS (A(:,:,1), CLNAME)
CALL FXTRAN_ACDC_CHECKCONTIGUOUS (B, CLNAME)
CALL FXTRAN_ACDC_CHECKCONTIGUOUS (C(:,1), CLNAME)

IF (LDA /= SIZE (A, 1)) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (LDB /= SIZE (B, 1)) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (LDC /= SIZE (C, 1)) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (KIDIA /= 1) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (ALPHA /= 1._8) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (BETA /= 0._8) CALL FXTRAN_ACDC_ABORT (CLNAME)

IF (LLSIMPLE_DGEMM) THEN

#ifdef _FXTRAN_USE_OPENACC
  !$ACC PARALLEL LOOP GANG &
  !$ACC&PRESENT (A, B, C) &
  !$ACC&PRIVATE(JBLK) &
  !$ACC&VECTOR_LENGTH (LDA) IF(LDACC) 
#endif
  DO JBLK=1,KGPBLKS
#ifdef _FXTRAN_USE_OPENACC
  !$ACC LOOP VECTOR&
  !$ACC&PRIVATE(JM,JK)
#endif
    DO JM = KIDIA, MERGE(M,KFDIA,JBLK<KGPBLKS)
      C (JM,JBLK) = 0.
      DO JK = 1, K
        C (JM,JBLK) = C (JM,JBLK) + B (LDB , JK) * A (JM, JK, JBLK)
      ENDDO
    ENDDO
  ENDDO

ELSE

  IF (LDACC) THEN
#ifdef _FXTRAN_USE_CUBLAS

    IF (.NOT. LLCBH_INITIALIZED) THEN
      CALL CHECKCUBLAS (CUBLASCREATE (YLCBH))
      CALL CHECKCUBLAS (CUBLASGETSTREAM (YLCBH, STREAM))
      LLCBH_INITIALIZED = .TRUE.
    ENDIF

    STRIDEA= FXTRAN_ACDC_STRIDE (A)
    STRIDEB= 0
    STRIDEC= FXTRAN_ACDC_STRIDE (C)
    BATCHCOUNT=KGPBLKS

    !$ACC DATA PRESENT(A,B,C)
    !$ACC HOST_DATA USE_DEVICE(A,B,C)
     CALL CHECKCUBLAS (&
      & CUBLASDGEMMSTRIDEDBATCHED_V2 (YLCBH, CUBLAS_OP_N, CUBLAS_OP_T, M, 1, K, &
      &                        ALPHA, A (1, 1, 1), LDA, STRIDEA, &
      &                               B (LDB, 1),  LDB, STRIDEB, &
      &                        BETA,  C (1, 1),    LDC, STRIDEC, &
      &                               BATCHCOUNT))   
    !$ACC END HOST_DATA
    !$ACC END DATA
    CALL CHECKCUDA (CUDASTREAMSYNCHRONIZE (STREAM))

#endif

  ELSE
    DO JBLK=1,KGPBLKS
      CALL DGEMM ('N','T', M, N, K, ALPHA, A(1,1,JBLK), LDA, B(LDB,1), LDB, BETA, C(1,JBLK), LDC)
    ENDDO
  ENDIF

ENDIF

LDDONE = .TRUE.

END SUBROUTINE

#ifdef _FXTRAN_USE_CUBLAS
SUBROUTINE CHECKCUBLAS (STATUS)

USE CUBLAS_V2

USE FXTRAN_ACDC_ABORT_MOD

INTEGER :: STATUS

IF (STATUS /= CUBLAS_STATUS_SUCCESS) THEN
  PRINT *, 'CUBLAS ERROR: STATUS =', STATUS
  CALL FXTRAN_ACDC_ABORT ('FXTRAN_ACDC_GEMM')
END IF

END SUBROUTINE CHECKCUBLAS

SUBROUTINE CHECKCUDA (STATUS)

USE CUDAFOR

USE FXTRAN_ACDC_ABORT_MOD

INTEGER :: STATUS

IF (STATUS /= CUDASUCCESS) THEN
  PRINT *, 'CUDA ERROR:', CUDAGETERRORSTRING (STATUS)
  CALL FXTRAN_ACDC_ABORT ('FXTRAN_ACDC_GEMM')
END IF

END SUBROUTINE CHECKCUDA
#endif

END MODULE
