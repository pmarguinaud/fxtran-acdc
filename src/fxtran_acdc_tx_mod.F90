MODULE FXTRAN_ACDC_TX_MOD

USE ISO_C_BINDING

#include "fxtran_acdc_config.h"

IMPLICIT NONE

#ifdef _FXTRAN_ACDC_USE_ROCTX
CHARACTER (LEN=*), PARAMETER :: CLMARK = "roctxMarkA"
CHARACTER (LEN=*), PARAMETER :: CLPUSH = "roctxRangePushA"
CHARACTER (LEN=*), PARAMETER :: CLPOP  = "roctxRangePop"
#endif

#ifdef _FXTRAN_ACDC_USE_NVTX
CHARACTER (LEN=*), PARAMETER :: CLMARK = "nvtxMark"
CHARACTER (LEN=*), PARAMETER :: CLPUSH = "nvtxRangePush"
CHARACTER (LEN=*), PARAMETER :: CLPOP  = "nvtxRangePop"
#endif

INTERFACE
  SUBROUTINE MARKA (CDNAME) BIND (C, NAME=CLMARK)
    IMPORT 
    IMPLICIT NONE
    CHARACTER (C_CHAR) :: CDNAME(*)
  END SUBROUTINE 
  SUBROUTINE RANGEPUSHA (CDNAME) BIND(C, NAME=CLPUSH)
    IMPORT 
    IMPLICIT NONE
    CHARACTER (C_CHAR) :: CDNAME (*)
  END SUBROUTINE 
  SUBROUTINE RANGEPOP () BIND (C, NAME=CLPOP)
    IMPORT 
    IMPLICIT NONE
  END SUBROUTINE 
END INTERFACE

PRIVATE

PUBLIC :: FXTRAN_ACDC_TX_MARK
PUBLIC :: FXTRAN_ACDC_TX_START
PUBLIC :: FXTRAN_ACDC_TX_END

CONTAINS

SUBROUTINE FXTRAN_ACDC_TX_MARK (CDNAME)
CHARACTER (LEN=*) :: CDNAME
CALL MARKA (TRIM (CDNAME)//CHAR (0))
END SUBROUTINE

SUBROUTINE FXTRAN_ACDC_TX_START (CDNAME)
CHARACTER (LEN=*) :: CDNAME
CALL RANGEPUSHA (TRIM (CDNAME)//CHAR (0))
END SUBROUTINE

SUBROUTINE FXTRAN_ACDC_TX_END ()
CALL RANGEPOP ()
END SUBROUTINE

END MODULE
