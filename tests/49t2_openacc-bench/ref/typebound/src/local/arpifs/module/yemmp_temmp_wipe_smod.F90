IF (LNUEMPP) THEN
  !$ACC EXIT DATA DETACH (SELF%NUEMPP)
  !$ACC EXIT DATA DELETE (SELF%NUEMPP)
ENDIF

LNEALLNS = ASSOCIATED (SELF%NEALLNS)
IF (LNEALLNS) THEN
  !$ACC EXIT DATA DETACH (SELF%NEALLNS)
  !$ACC EXIT DATA DELETE (SELF%NEALLNS)
ENDIF

LNEPTRNS = ASSOCIATED (SELF%NEPTRNS)
IF (LNEPTRNS) THEN
  !$ACC EXIT DATA DETACH (SELF%NEPTRNS)
  !$ACC EXIT DATA DELETE (SELF%NEPTRNS)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

