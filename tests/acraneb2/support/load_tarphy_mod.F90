MODULE LOAD_TARPHY_MOD

USE YOMARPHY, ONLY : TARPHY
USE PARKIND1, ONLY : JPRB, JPRD

INTERFACE LOAD
MODULE PROCEDURE LOAD_TARPHY
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TARPHY (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TARPHY), INTENT (OUT) :: YD

READ (KLUN) YD%LMPA
READ (KLUN) YD%LMICRO
READ (KLUN) YD%LTURB
READ (KLUN) YD%LMSE
READ (KLUN) YD%LKFBCONV
READ (KLUN) YD%LKFBD
READ (KLUN) YD%LKFBS
READ (KLUN) YD%LMFSHAL
READ (KLUN) YD%LUSECHEM
READ (KLUN) YD%LORILAM
READ (KLUN) YD%LRDUST
READ (KLUN) YD%LRCO2
READ (KLUN) YD%LINITCHEM
READ (KLUN) YD%LINITORILAM
READ (KLUN) YD%LINITDUST
READ (KLUN) YD%LRDEPOS
READ (KLUN) YD%LBUFLUX
READ (KLUN) YD%CCOUPLING
READ (KLUN) YD%LMDUST
READ (KLUN) YD%LSURFEX_CRITICAL
READ (KLUN) YD%LSURFEX_KFROM
READ (KLUN) YD%NSURFEX_ITER
END SUBROUTINE


END MODULE
