FUNCTION ACDC_SIZE_THSLMER (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (THSLMER),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LR3DTW, LRIPI, LRSLD, LRSLDW
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRIPI = ALLOCATED (SELF%RIPI)
IF (LRIPI) THEN
  ISIZE = KIND (SELF%RIPI) * SIZE (SELF%RIPI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RIPI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSLD = ALLOCATED (SELF%RSLD)
IF (LRSLD) THEN
  ISIZE = KIND (SELF%RSLD) * SIZE (SELF%RSLD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSLD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSLDW = ALLOCATED (SELF%RSLDW)
IF (LRSLDW) THEN
  ISIZE = KIND (SELF%RSLDW) * SIZE (SELF%RSLDW)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSLDW'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LR3DTW = ALLOCATED (SELF%R3DTW)
IF (LR3DTW) THEN
  ISIZE = KIND (SELF%R3DTW) * SIZE (SELF%R3DTW)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%R3DTW'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

