MODULE UTIL_SURFACE_VIEW_GROUP_VCLIP_MOD

USE SURFACE_VIEWS_DIAGNOSTIC_MODULE, ONLY : SURFACE_VIEW_GROUP_VCLIP



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_SURFACE_VIEW_GROUP_VCLIP
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_SURFACE_VIEW_GROUP_VCLIP
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_SURFACE_VIEW_GROUP_VCLIP
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_VCLIP (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_TPC, LF_WPC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_TPC = ASSOCIATED (SELF%F_TPC)
IF (LF_TPC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_TPC)
  !$ACC ENTER DATA ATTACH (SELF%F_TPC)
ENDIF

LF_WPC = ASSOCIATED (SELF%F_WPC)
IF (LF_WPC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_WPC)
  !$ACC ENTER DATA ATTACH (SELF%F_WPC)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_VCLIP (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIP), TARGET :: SELF
LOGICAL :: LF_TPC, LF_WPC
LF_TPC = ASSOCIATED (SELF%F_TPC)
IF (LF_TPC) THEN
  CALL HOST_FIELD_2RB (SELF%F_TPC)
ENDIF

LF_WPC = ASSOCIATED (SELF%F_WPC)
IF (LF_WPC) THEN
  CALL HOST_FIELD_2RB (SELF%F_WPC)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_SURFACE_VIEW_GROUP_VCLIP (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_TPC, LF_WPC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_TPC = ASSOCIATED (SELF%F_TPC)
IF (LF_TPC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_TPC)
ENDIF

LF_WPC = ASSOCIATED (SELF%F_WPC)
IF (LF_WPC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_WPC)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
