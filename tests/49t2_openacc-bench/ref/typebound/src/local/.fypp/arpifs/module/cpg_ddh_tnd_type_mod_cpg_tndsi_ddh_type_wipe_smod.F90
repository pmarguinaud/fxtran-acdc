SUBMODULE (CPG_DDH_TND_TYPE_MOD) CPG_DDH_TND_TYPE_MOD_CPG_TNDSI_DDH_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_TNDSI_DDH_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (CPG_TNDSI_DDH_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LZDATA_GMV, LZVIEW_GMV
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LZDATA_GMV = ASSOCIATED (SELF%ZDATA_GMV)
IF (LZDATA_GMV) THEN
  !$ACC EXIT DATA DETACH (SELF%ZDATA_GMV)
  !$ACC EXIT DATA DELETE (SELF%ZDATA_GMV)
ENDIF

LZVIEW_GMV = ASSOCIATED (SELF%ZVIEW_GMV)
IF (LZVIEW_GMV) THEN
  !$ACC EXIT DATA DETACH (SELF%ZVIEW_GMV)
  !$ACC EXIT DATA DELETE (SELF%ZVIEW_GMV)
ENDIF

CALL SELF%U%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%V%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%T%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%PD%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%VD%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
