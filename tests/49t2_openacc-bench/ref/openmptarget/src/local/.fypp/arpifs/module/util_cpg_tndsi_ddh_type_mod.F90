SUBROUTINE ACDC_COPY_CPG_TNDSI_DDH_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_VARIABLE_3RB_MOD
IMPLICIT NONE
TYPE (CPG_TNDSI_DDH_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LZDATA_GMV, LZVIEW_GMV
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$OMP TARGET ENTER DATA MAP (ALLOC: SELF)
  !$OMP TARGET UPDATE TO (SELF)
ENDIF
LZDATA_GMV = ASSOCIATED (SELF%ZDATA_GMV)
IF (LZDATA_GMV) THEN
  !$OMP TARGET ENTER DATA MAP (ALLOC: SELF%ZDATA_GMV)
  !$OMP TARGET UPDATE TO (SELF%ZDATA_GMV)
  !$OMP TARGET ENTER DATA MAP (TO: SELF%ZDATA_GMV)
ENDIF

LZVIEW_GMV = ASSOCIATED (SELF%ZVIEW_GMV)
IF (LZVIEW_GMV) THEN
  !$OMP TARGET ENTER DATA MAP (ALLOC: SELF%ZVIEW_GMV)
  !$OMP TARGET UPDATE TO (SELF%ZVIEW_GMV)
  !$OMP TARGET ENTER DATA MAP (TO: SELF%ZVIEW_GMV)
ENDIF

CALL ACDC_COPY_VARIABLE_3RB (SELF%U, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_3RB (SELF%V, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_3RB (SELF%T, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_3RB (SELF%PD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_VARIABLE_3RB (SELF%VD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

