SUBROUTINE ACDC_SAVE_TEDIM (SELF, KLUN)

IMPLICIT NONE
TYPE (TEDIM), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LNISMAX, LNISNAX
WRITE (KLUN) SELF%NSECPLG
WRITE (KLUN) SELF%NBZONG
WRITE (KLUN) SELF%NBZONL
WRITE (KLUN) SELF%NBZONU
WRITE (KLUN) SELF%NNOEXTZG
WRITE (KLUN) SELF%NNOEXTZL
LNISMAX = ASSOCIATED (SELF%NISMAX)
WRITE (KLUN) LNISMAX
IF (LNISMAX) THEN
  WRITE (KLUN) LBOUND (SELF%NISMAX)
  WRITE (KLUN) UBOUND (SELF%NISMAX)
  WRITE (KLUN) SELF%NISMAX
ENDIF
LNISNAX = ASSOCIATED (SELF%NISNAX)
WRITE (KLUN) LNISNAX
IF (LNISNAX) THEN
  WRITE (KLUN) LBOUND (SELF%NISNAX)
  WRITE (KLUN) UBOUND (SELF%NISNAX)
  WRITE (KLUN) SELF%NISNAX
ENDIF
WRITE (KLUN) SELF%LBIPINCI
WRITE (KLUN) SELF%NBIPINCIX
WRITE (KLUN) SELF%NBIPINCIY
WRITE (KLUN) SELF%NEDOM
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TEDIM (SELF, KLUN)

IMPLICIT NONE
TYPE (TEDIM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LNISMAX, LNISNAX
READ (KLUN) SELF%NSECPLG
READ (KLUN) SELF%NBZONG
READ (KLUN) SELF%NBZONL
READ (KLUN) SELF%NBZONU
READ (KLUN) SELF%NNOEXTZG
READ (KLUN) SELF%NNOEXTZL
READ (KLUN) LNISMAX
IF (LNISMAX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NISMAX (IL1(1):IU1(1)))
  READ (KLUN) SELF%NISMAX
ELSE
  NULLIFY (SELF%NISMAX)
ENDIF
READ (KLUN) LNISNAX
IF (LNISNAX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NISNAX (IL1(1):IU1(1)))
  READ (KLUN) SELF%NISNAX
ELSE
  NULLIFY (SELF%NISNAX)
ENDIF
READ (KLUN) SELF%LBIPINCI
READ (KLUN) SELF%NBIPINCIX
READ (KLUN) SELF%NBIPINCIY
READ (KLUN) SELF%NEDOM
END SUBROUTINE

