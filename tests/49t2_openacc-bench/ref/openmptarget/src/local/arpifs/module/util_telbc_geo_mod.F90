FUNCTION ACDC_SIZE_TELBC_GEO (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TELBC_GEO),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMPTRCPLBLK, LNIND_LEN, LNIND_LIST
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LNIND_LIST = ALLOCATED (SELF%NIND_LIST)
IF (LNIND_LIST) THEN
  ISIZE = KIND (SELF%NIND_LIST) * SIZE (SELF%NIND_LIST)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NIND_LIST'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNIND_LEN = ALLOCATED (SELF%NIND_LEN)
IF (LNIND_LEN) THEN
  ISIZE = KIND (SELF%NIND_LEN) * SIZE (SELF%NIND_LEN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NIND_LEN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%NCPLBLKS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCPLBLKS'
ENDIF
KSIZE = KSIZE + ISIZE
LMPTRCPLBLK = ALLOCATED (SELF%MPTRCPLBLK)
IF (LMPTRCPLBLK) THEN
  ISIZE = KIND (SELF%MPTRCPLBLK) * SIZE (SELF%MPTRCPLBLK)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MPTRCPLBLK'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

