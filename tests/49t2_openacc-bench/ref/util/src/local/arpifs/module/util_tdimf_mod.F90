FUNCTION ACDC_SIZE_TDIMF (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TDIMF),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LNGRBSP2, LNGRBSP3
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NFTHER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFTHER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NF3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NF3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFD2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFD2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NFC2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFC2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NS3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NS3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NS2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NS2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NS1D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NS1D'
ENDIF
KSIZE = KSIZE + ISIZE
LNGRBSP3 = ALLOCATED (SELF%NGRBSP3)
IF (LNGRBSP3) THEN
  ISIZE = KIND (SELF%NGRBSP3) * SIZE (SELF%NGRBSP3)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NGRBSP3'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNGRBSP2 = ALLOCATED (SELF%NGRBSP2)
IF (LNGRBSP2) THEN
  ISIZE = KIND (SELF%NGRBSP2) * SIZE (SELF%NGRBSP2)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NGRBSP2'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%LVOR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LVOR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LADER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LADER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LUVDER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LUVDER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%LSPT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSPT'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

