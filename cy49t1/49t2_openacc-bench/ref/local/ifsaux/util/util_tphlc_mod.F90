ISIZE = KIND (YD%USTARS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%USTARS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%ALANDZ0)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ALANDZ0'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%ASEAZ0)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ASEAZ0'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSPHLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSPHLC'
ENDIF
