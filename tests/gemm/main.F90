PROGRAM GEMM

#include "fxtran_acdc_config.h"

USE FXTRAN_ACDC_GEMM_MOD            , ONLY : FXTRAN_ACDC_GEMM
USE FXTRAN_ACDC_GEMM_MOD_OPENACC    , ONLY : FXTRAN_ACDC_GEMM_OPENACC
USE FXTRAN_ACDC_GEMM_MOD_MANYBLOCKS , ONLY : FXTRAN_ACDC_GEMM_MANYBLOCKS
USE FXTRAN_ACDC_GEMM_MOD_SINGLEBLOCK, ONLY : FXTRAN_ACDC_GEMM_SINGLEBLOCK
USE FXTRAN_ACDC_BLAS_MOD            , ONLY : FXTRAN_ACDC_SET_SIMPLE_DGEMM

USE FXTRAN_ACDC_STACK_MOD           , ONLY : FXTRAN_ACDC_STACK

USE, INTRINSIC :: ISO_FORTRAN_ENV
USE, INTRINSIC :: IEEE_ARITHMETIC

IMPLICIT NONE

INTEGER, PARAMETER :: JPIM = 4, JPRB = 8

REAL (KIND=JPRB) :: ZNAN 

INTEGER (KIND=JPIM) :: NPROMA, ILEVIN, ILEVOUT, NGPBLKS, IST, IEND

REAL (KIND=JPRB), PARAMETER :: ZEPS = 1E-17_JPRB
LOGICAL, PARAMETER :: LLVERBOSE = .FALSE.

REAL (KIND=JPRB), ALLOCATABLE :: ZIN (:,:,:), ZOUT (:,:,:), ZINTE (:,:), ZREF (:,:,:)

LOGICAL :: LLDONE, LLACC
INTEGER (KIND=JPIM) :: JLON, JLEV, JBLK, IFDIA
TYPE (FXTRAN_ACDC_STACK) :: YLSTACK
INTEGER :: JSIMPLE, JACC
CHARACTER (LEN=128) :: CLFILE

CALL GETARG (1, CLFILE)

ZNAN = IEEE_VALUE (1._JPRB, IEEE_QUIET_NAN)

OPEN (77, FILE=TRIM (CLFILE), FORM='UNFORMATTED', STATUS='OLD')

READ (77) NPROMA, ILEVIN, ILEVOUT, NGPBLKS, IST, IEND

ALLOCATE (ZOUT (NPROMA, ILEVOUT-1, NGPBLKS),  ZREF (NPROMA, ILEVOUT-1, NGPBLKS), &
        & ZIN (NPROMA, ILEVIN, NGPBLKS), ZINTE (ILEVOUT,ILEVIN))

READ (77) ZIN
READ (77) ZREF
READ (77) ZINTE

CLOSE (77)

DO JACC = 0, 1
DO JSIMPLE = 0, 1

LLACC = JACC == 1
CALL FXTRAN_ACDC_SET_SIMPLE_DGEMM (JSIMPLE == 1)

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMM',             47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUT = ZNAN
CALL RUN_GEMM
CALL CHECK

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMM_OPENACC',     47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUT = ZNAN
CALL RUN_GEMM_OPENACC
CALL CHECK

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMM_MANYBLOCKS',  47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUT = ZNAN
CALL RUN_GEMM_MANYBLOCKS
CALL CHECK

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMM_SINGLEBLOCK', 47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUT = ZNAN
CALL RUN_GEMM_SINGLEBLOCK
CALL CHECK

ENDDO
ENDDO

CONTAINS

SUBROUTINE RUN_GEMM 

!$OMP PARALLEL DO PRIVATE (IFDIA)
DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
  CALL FXTRAN_ACDC_GEMM (1, IFDIA, 'N', 'T', NPROMA, ILEVOUT-1, ILEVIN, 1.0_JPRB, ZIN (:,:,JBLK), &
                       & NPROMA, ZINTE, ILEVOUT, 0.0_JPRB, ZOUT (:,:,JBLK), NPROMA, LLDONE)
ENDDO
!$OMP END PARALLEL DO

END SUBROUTINE

SUBROUTINE RUN_GEMM_OPENACC

#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC DATA COPYIN (ZIN, ZINTE) COPYOUT (ZOUT) IF (LLACC)
!$ACC PARALLEL LOOP GANG VECTOR PRIVATE (JBLK, IFDIA, JLON) IF (LLACC)
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TO: ZIN, ZINTE) MAP (FROM: ZOUT) IF (LLACC)
!$OMP TARGET TEAMS DISTRIBUTE PARALLEL DO SIMD PRIVATE (JBLK, IFDIA, JLON) IF (LLACC)
#endif
DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
  DO JLON = 1, IFDIA
    CALL FXTRAN_ACDC_GEMM_OPENACC (JLON, JLON, 'N', 'T', NPROMA, ILEVOUT-1, ILEVIN, 1.0_JPRB, ZIN (:,:,JBLK), &
                                 & NPROMA, ZINTE, ILEVOUT, 0.0_JPRB, ZOUT (:,:,JBLK), NPROMA, LLDONE, YLSTACK)
  ENDDO
ENDDO
#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

END SUBROUTINE

SUBROUTINE RUN_GEMM_MANYBLOCKS 

REAL (KIND=JPRB), ALLOCATABLE :: ZIN1 (:, :, :), ZOUT1 (:, :, :)

ALLOCATE (ZIN1 (NPROMA, ILEVIN+2, NGPBLKS), ZOUT1 (NPROMA, ILEVOUT-1+2, NGPBLKS))

ZIN1 (:, 1:ILEVIN, :) = ZIN
ZIN1 (:, ILEVIN+1:, :) = ZNAN
ZOUT1 (:, 1:ILEVOUT-1, :) = ZOUT
ZOUT1 (:, ILEVOUT:, :) = ZNAN

#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC DATA COPYIN (ZIN1, ZINTE) COPYOUT (ZOUT1) IF (LLACC)
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TO: ZIN1, ZINTE) MAP (FROM: ZOUT1) IF (LLACC)
#endif
CALL FXTRAN_ACDC_GEMM_MANYBLOCKS (1, IEND, 'N', 'T', NPROMA, ILEVOUT-1, ILEVIN, 1.0_JPRB, &
                                & ZIN1 (:, 1:ILEVIN, :), NPROMA, ZINTE, ILEVOUT, 0.0_JPRB, &
                                & ZOUT1 (:,1:ILEVOUT-1,:), NPROMA, LLDONE, LLACC, NGPBLKS, YLSTACK)
#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

ZOUT = ZOUT1 (:, 1:ILEVOUT-1, :)

END SUBROUTINE

SUBROUTINE RUN_GEMM_SINGLEBLOCK

REAL (KIND=JPRB), ALLOCATABLE :: ZIN1 (:,:), ZOUT1 (:,:)

INTEGER (KIND=JPIM) :: NPROMA1

NPROMA1 = NPROMA * NGPBLKS

ALLOCATE (ZIN1 (NPROMA1, ILEVIN), ZOUT1 (NPROMA1, ILEVOUT-1))

DO JBLK = 1, NGPBLKS
  DO JLON = 1, NPROMA
    ZIN1 (NPROMA * (JBLK-1) + JLON, :) = ZIN (JLON, :, JBLK)
  ENDDO
ENDDO

#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC DATA COPYIN (ZIN1, ZINTE) COPYOUT (ZOUT1) IF (LLACC)
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TO: ZIN1, ZINTE) MAP (FROM: ZOUT1) IF (LLACC)
#endif
CALL FXTRAN_ACDC_GEMM_SINGLEBLOCK (1, NPROMA1, 'N', 'T', NPROMA1, ILEVOUT-1, ILEVIN, 1.0_JPRB, ZIN1, &
                                 & NPROMA1, ZINTE, ILEVOUT, 0.0_JPRB, ZOUT1, NPROMA1, LLDONE, LLACC)
#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

DO JBLK = 1, NGPBLKS
  DO JLON = 1, NPROMA
    ZOUT (JLON, :, JBLK) = ZOUT1 (NPROMA * (JBLK-1) + JLON, :)
  ENDDO
ENDDO

DEALLOCATE (ZIN1, ZOUT1)

END SUBROUTINE

SUBROUTINE CHECK

REAL (KIND=JPRB) :: ZRMS (ILEVOUT-1), ZAVG (ILEVOUT-1)
INTEGER (KIND=JPIM) :: ICNT (ILEVOUT-1)

ZRMS = 0
ZAVG = 0
ICNT = 0

DO JLEV = 1, ILEVOUT-1
  DO JBLK = 1, NGPBLKS
    IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
    DO JLON = 1, IFDIA
      ZRMS (JLEV) = ZRMS (JLEV) + (ZOUT (JLON, JLEV, JBLK) - ZREF (JLON, JLEV, JBLK)) ** 2
      ZAVG (JLEV) = ZAVG (JLEV) + ABS (ZREF (JLON, JLEV, JBLK))
      ICNT (JLEV) = ICNT (JLEV) + 1
    ENDDO
  ENDDO
ENDDO

ZRMS = SQRT (ZRMS / ICNT)
ZAVG = ZAVG / ICNT

IF (LLVERBOSE) THEN
DO JLEV = 1, ILEVOUT-1
  WRITE (*, '(I6,E30.20,E30.20)') JLEV, ZRMS (JLEV), ZAVG (JLEV)
ENDDO
WRITE (*, *)
WRITE (*, '(6X,E30.20,E30.20)') SUM (ZRMS) / SIZE (ZRMS), SUM (ZAVG) / SIZE (ZAVG)
WRITE (*, *)
ENDIF

IF (SUM (ZRMS) / SIZE (ZRMS) < ZEPS) THEN
WRITE (*, *) "  OK  "
ELSE
WRITE (*, *) " FAIL "
ENDIF
WRITE (*, *)

END SUBROUTINE

FUNCTION LEFT (CDSTRING, KLENGTH, CDPAD) RESULT (CLLEFT)

CHARACTER (LEN=*) :: CDSTRING
INTEGER :: KLENGTH
CHARACTER*1, OPTIONAL :: CDPAD

CHARACTER (LEN=KLENGTH) :: CLLEFT
INTEGER :: ILEN, I
CHARACTER*1 :: CLPAD

CLPAD = ' '

IF (PRESENT (CDPAD)) CLPAD = CDPAD

DO I = 1, KLENGTH
  CLLEFT (I:I) = CLPAD
ENDDO

ILEN = LEN_TRIM (CDSTRING)

CLLEFT (1:ILEN) = CDSTRING

END FUNCTION

END PROGRAM
