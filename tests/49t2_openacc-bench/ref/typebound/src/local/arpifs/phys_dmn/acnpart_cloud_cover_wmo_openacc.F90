SUBROUTINE ACNPART_CLOUD_COVER_WMO_OPENACC (YDCST, YDML_PHY_MF, KIDIA, KFDIA, KLON, KTDIA&
&, KLEV, PDECRDRED, PWMXOV, PDECRD, PNEB, PAPHI, PAPRSF, PCLCH, PCLCM, PCLCL, YDSTACK)
!$ACC ROUTINE (ACNPART_CLOUD_COVER_WMO_OPENACC) SEQ
USE MODEL_PHYSICS_MF_MOD,ONLY:MODEL_PHYSICS_MF_TYPE
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


TYPE (TCST), INTENT (IN)::YDCST
TYPE (MODEL_PHYSICS_MF_TYPE), INTENT (IN)::YDML_PHY_MF
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KTDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
REAL (KIND=JPRB), INTENT (IN)::PDECRDRED
REAL (KIND=JPRB), INTENT (IN)::PWMXOV
REAL (KIND=JPRB), INTENT (IN)::PDECRD (KLON)
REAL (KIND=JPRB), INTENT (IN)::PNEB (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPHI (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPRSF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PCLCH (KLON)
REAL (KIND=JPRB), INTENT (OUT)::PCLCM (KLON)
REAL (KIND=JPRB), INTENT (OUT)::PCLCL (KLON)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPRB)::ZL
REAL (KIND=JPRB)::ZH
REAL (KIND=JPRB)::ZCLOV

REAL (KIND=JPRB)::ZOVLP 

JLON = KIDIA





IF (YDML_PHY_MF%YRPHY%LRNUMX.AND.YDML_PHY_MF%YRPHY%LACPANMX) THEN
  
  PCLCH (JLON)=1._JPRB-PNEB (JLON, KTDIA)
  
ELSE
  
  PCLCH (JLON)=PNEB (JLON, KTDIA)
  
ENDIF


DO JLEV=KTDIA+1, KLEV-1

  IF (YDML_PHY_MF%YRPHY%LRNUMX.AND.YDML_PHY_MF%YRPHY%LACPANMX) THEN
    
    ZOVLP =(MIN (1.0_JPRB-PNEB (JLON, JLEV), 1.0_JPRB-PWMXOV*PNEB&
    & (JLON, JLEV-1)))/(1.0_JPRB-PWMXOV*PNEB (JLON, JLEV-1))
  
  ELSEIF (YDML_PHY_MF%YRPHY%LRNUMX.AND.YDML_PHY_MF%YRPHY%LRNUEXP) THEN
    
    ZCLOV=EXP ((PAPRSF (JLON, JLEV-1)-PAPRSF (JLON, JLEV))/(PDECRDRED*PDECRD (JLON)))
    ZOVLP =(1._JPRB-ZCLOV)*PNEB (JLON, JLEV)*PNEB (JLON, JLEV&
    &-1)+ZCLOV*MIN (PNEB (JLON, JLEV), PNEB (JLON, JLEV-1))
  
  ELSEIF (YDML_PHY_MF%YRPHY%LRNUMX) THEN
    
    ZOVLP =MIN (PNEB (JLON, JLEV), PNEB (JLON, JLEV-1))
  
  ELSE
    
    ZOVLP =PNEB (JLON, JLEV)*PNEB (JLON, JLEV-1)
  
  ENDIF


  IF (YDML_PHY_MF%YRPHY%LRNUMX.AND.YDML_PHY_MF%YRPHY%LACPANMX) THEN
    
    ZL=(PAPHI (JLON, JLEV-1)-PAPHI (JLON, KLEV))/YDCST%RG
    ZH=(PAPHI (JLON, JLEV-2)-PAPHI (JLON, KLEV))/YDCST%RG

    IF (ZL>=YDML_PHY_MF%YRPHY2%HWMOHIGH) THEN
      PCLCH (JLON)=PCLCH (JLON)*ZOVLP 
    ELSEIF (ZH>=YDML_PHY_MF%YRPHY2%HWMOHIGH) THEN
      PCLCM (JLON)=1._JPRB-PNEB (JLON, JLEV)
    ELSEIF (ZL>=YDML_PHY_MF%YRPHY2%HWMOLOW) THEN
      PCLCM (JLON)=PCLCM (JLON)*ZOVLP 
    ELSEIF (ZH>=YDML_PHY_MF%YRPHY2%HWMOLOW) THEN
      PCLCL (JLON)=1._JPRB-PNEB (JLON, JLEV)
    ELSE
      PCLCL (JLON)=PCLCL (JLON)*ZOVLP 
    ENDIF

  
  ELSE
    
    ZL=(PAPHI (JLON, JLEV-1)-PAPHI (JLON, KLEV))/YDCST%RG
    ZH=(PAPHI (JLON, JLEV-2)-PAPHI (JLON, KLEV))/YDCST%RG

    IF (ZL>=YDML_PHY_MF%YRPHY2%HWMOHIGH) THEN
      PCLCH (JLON)=PCLCH (JLON)+(PNEB (JLON, JLEV)-ZOVLP )*(1&
      &._JPRB-PCLCH (JLON))/(1._JPRB-PNEB (JLON, JLEV-1))
    ELSEIF (ZH>=YDML_PHY_MF%YRPHY2%HWMOHIGH) THEN
      PCLCM (JLON)=PNEB (JLON, JLEV)
    ELSEIF (ZL>=YDML_PHY_MF%YRPHY2%HWMOLOW) THEN
      PCLCM (JLON)=PCLCM (JLON)+(PNEB (JLON, JLEV)-ZOVLP )*(1&
      &._JPRB-PCLCM (JLON))/(1._JPRB-PNEB (JLON, JLEV-1))
    ELSEIF (ZH>=YDML_PHY_MF%YRPHY2%HWMOLOW) THEN
      PCLCL (JLON)=PNEB (JLON, JLEV)
    ELSE
      PCLCL (JLON)=PCLCL (JLON)+(PNEB (JLON, JLEV)-ZOVLP )*(1&
      &._JPRB-PCLCL (JLON))/(1._JPRB-PNEB (JLON, JLEV-1))
    ENDIF

  
  ENDIF

ENDDO


IF (YDML_PHY_MF%YRPHY%LRNUMX.AND.YDML_PHY_MF%YRPHY%LACPANMX) THEN
  
  PCLCH (JLON)=1._JPRB-PCLCH (JLON)
  PCLCM (JLON)=1._JPRB-PCLCM (JLON)
  PCLCL (JLON)=1._JPRB-PCLCL (JLON)
  
ENDIF



END SUBROUTINE ACNPART_CLOUD_COVER_WMO_OPENACC
