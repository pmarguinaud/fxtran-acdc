&.YDMODEL%YRML_PHY_MF%YRPHY%LAEROSOO.AND.YDMODEL%YRML_PHY_MF%YRPHY%LAERODES

IF ((MOD (YDCPG_OPTS%NSTEP, YDMODEL%YRML_PHY_RAD%YRERAD%NRADFR)==0).OR.(YDMODEL&
  &%YRML_PHY_MF%YRPHY%LRAY.AND.(.NOT.YDMODEL%YRML_PHY_MF%YRPHY%LRSTAER))) THEN
  IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
  &:RADAER',0,ZHOOK_HANDLE_PARALLEL)

  IF (FXTRAN_ACDC_LPARALLELMETHOD ('OPENMP','APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL:RADAER')) THEN
    
    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:GET_DATA',0,ZHOOK_HANDLE_FIELD_API)
    PAER => GET_HOST_DATA_RDWR (YD_PAER)
    PAERINDS => GET_HOST_DATA_RDWR (YD_PAERINDS)
    ZAEDES => GET_HOST_DATA_RDWR (YL_ZAEDES)
    ZAELAN => GET_HOST_DATA_RDWR (YL_ZAELAN)
    ZAESEA => GET_HOST_DATA_RDWR (YL_ZAESEA)
    ZAESOO => GET_HOST_DATA_RDWR (YL_ZAESOO)
