SUBROUTINE ACDC_LOAD_TECLD (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TECLD), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCETA
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LCETA
IF (LCETA) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CETA (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%CETA,1):UBOUND(SELF%CETA,1)))
  READ (KLUN) ZTMP1
  SELF%CETA = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) ZTMP0
SELF%RANVA = ZTMP0
READ (KLUN) ZTMP0
