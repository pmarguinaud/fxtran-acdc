MODULE LOAD_YOMPHY0_MOD
USE PARKIND1, ONLY : JPRB, JPRD

USE YOMPHY0

CONTAINS

SUBROUTINE LOAD_YOMPHY0 (KLUN)
USE LOAD_TPHY0_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRPHY0
READ (KLUN) LYRPHY0
IF (LYRPHY0) THEN
  ALLOCATE (YRPHY0)
  CALL LOAD (KLUN, YRPHY0)
ELSE
  NULLIFY (YRPHY0)
ENDIF
END SUBROUTINE


END MODULE
