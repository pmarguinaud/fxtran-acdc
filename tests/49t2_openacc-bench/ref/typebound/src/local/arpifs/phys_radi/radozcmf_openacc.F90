SUBROUTINE RADOZCMF_OPENACC (YDCST, YDEOZOC, KIDIA, KFDIA, KLON, KLEV, PAPRS, PGEMU, POZON, YDSTACK)
!$ACC ROUTINE (RADOZCMF_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
USE YOEOZOC,ONLY:TEOZOC
#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


TYPE (TCST), INTENT (IN)::YDCST
TYPE (TEOZOC), INTENT (IN)::YDEOZOC
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
REAL (KIND=JPRB), INTENT (IN)::PAPRS (KLON, KLEV+1)
REAL (KIND=JPRB), INTENT (IN)::PGEMU (KLON)
REAL (KIND=JPRB), INTENT (OUT)::POZON (KLON, KLEV)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
TYPE(FXTRAN_ACDC_STACK) :: YLSTACK
fxtran_acdc_temp (REAL (KIND=JPRB), ZOZON, (KLON, KLEV+1))
REAL (KIND=JPRB)::ZOZLT (0:35)
REAL (KIND=JPRB)::ZSILAT 
REAL (KIND=JPRB)::ZRRR (0:34)
INTEGER (KIND=JPIM)::INLA 
INTEGER (KIND=JPIM)::I1
INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JC
REAL (KIND=JPRB)::Z2
REAL (KIND=JPRB)::Z1


YLSTACK = YDSTACK



IF (KIND (ZOZON) == 8) THEN
    fxtran_acdc_alloc8 (ZOZON)
ELSEIF (KIND (ZOZON) == 4) THEN
    fxtran_acdc_alloc4 (ZOZON)
ELSE
    STOP 1
ENDIF


JLON = KIDIA



INLA =1+INT (18.0_JPRB/YDCST%RPI*ASIN (PGEMU (JLON))+9.0_JPRB)
ZSILAT =(PGEMU (JLON)-YDEOZOC%RSINC (INLA ))/(YDEOZOC%RSINC (INLA +1)-YDEOZOC%RSINC (INLA ))


DO JC=0, 35
  
  Z1=REAL (INLA )
  Z2=MIN (ABS ((Z1-1._JPRB)*(Z1-18._JPRB)), 1.0_JPRB)
  I1=INLA 
  ZOZLT (JC)=(1.0_JPRB-Z2)*YDEOZOC%ROZT (I1, JC)+Z2*(YDEOZOC%ROZT (I1&
  &, JC)+ZSILAT *(YDEOZOC%ROZT (I1+1, JC)-YDEOZOC%ROZT (I1, JC)))
  
ENDDO


DO JC=0, 34
  
  ZRRR (JC)=(1.0_JPRB/(YDEOZOC%RPROC (JC)-YDEOZOC%RPROC (JC+1)))*(ZOZLT (JC)-ZOZLT (JC+1))
  
ENDDO


DO JLEV=1, KLEV+1
  
  ZOZON (JLON, JLEV)=0.0_JPRB
  
ENDDO


DO JC=0, 34

  DO JLEV=1, KLEV+1
    
    ZOZON (JLON, JLEV)=ZOZON (JLON, JLEV)+(ZOZLT (JC+1)+(PAPRS (JLON, JLEV)-YDEOZOC%RPROC&
    & (JC+1))*ZRRR (JC))*MAX (0.0_JPRB, SIGN (1.0_JPRB, PAPRS (JLON, JLEV)-YDEOZOC%RPROC&
    & (JC)))*MAX (0.0_JPRB, SIGN (1.0_JPRB, YDEOZOC%RPROC (JC+1)-PAPRS (JLON, JLEV)))
  
  ENDDO

ENDDO


DO JLEV=1, KLEV+1
  
  ZOZON (JLON, JLEV)=ZOZON (JLON, JLEV)+ZOZLT (35)*MAX (0.0_JPRB&
  &, SIGN (1.0_JPRB, PAPRS (JLON, JLEV)-YDEOZOC%RPROC (35)))
  
ENDDO


DO JLEV=1, KLEV
  
  POZON (JLON, JLEV)=(PAPRS (JLON, JLEV+1)-PAPRS (JLON, JLEV&
  &))*(ZOZON (JLON, JLEV)+ZOZON (JLON, JLEV+1))*0.5_JPRB
  
ENDDO


END SUBROUTINE RADOZCMF_OPENACC
