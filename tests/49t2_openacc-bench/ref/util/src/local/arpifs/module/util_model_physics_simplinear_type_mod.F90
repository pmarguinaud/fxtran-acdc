SUBROUTINE ACDC_SAVE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LGPHIST
CALL ACDC_SAVE_TEPHLI (SELF%YREPHLI, KLUN)
CALL ACDC_SAVE_TCUMFS (SELF%YRCUMFS, KLUN)
CALL ACDC_SAVE_TEGWDWMS (SELF%YREGWDWMS, KLUN)
CALL ACDC_SAVE_TECUMF2 (SELF%YRECUMF2, KLUN)
CALL ACDC_SAVE_TPHLC (SELF%YRPHLC, KLUN)
CALL ACDC_SAVE_TPHNC (SELF%YRPHNC, KLUN)
CALL ACDC_SAVE_TNCL (SELF%YRNCL, KLUN)
CALL ACDC_SAVE_TSRFTLAD (SELF%YRSRFTLAD, KLUN)
LGPHIST = ASSOCIATED (SELF%GPHIST)
WRITE (KLUN) LGPHIST
IF (LGPHIST) THEN
  WRITE (KLUN) LBOUND (SELF%GPHIST)
  WRITE (KLUN) UBOUND (SELF%GPHIST)
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL ACDC_SAVE_SPHYS_HIST_TYPE (SELF%GPHIST (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGPHIST
CALL ACDC_LOAD_TEPHLI (SELF%YREPHLI, KLUN)
CALL ACDC_LOAD_TCUMFS (SELF%YRCUMFS, KLUN)
CALL ACDC_LOAD_TEGWDWMS (SELF%YREGWDWMS, KLUN)
CALL ACDC_LOAD_TECUMF2 (SELF%YRECUMF2, KLUN)
CALL ACDC_LOAD_TPHLC (SELF%YRPHLC, KLUN)
CALL ACDC_LOAD_TPHNC (SELF%YRPHNC, KLUN)
CALL ACDC_LOAD_TNCL (SELF%YRNCL, KLUN)
CALL ACDC_LOAD_TSRFTLAD (SELF%YRSRFTLAD, KLUN)
READ (KLUN) LGPHIST
IF (LGPHIST) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%GPHIST (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL ACDC_LOAD_SPHYS_HIST_TYPE (SELF%GPHIST (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%GPHIST)
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_COPY_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LGPHIST
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL ACDC_COPY_TEPHLI (SELF%YREPHLI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TCUMFS (SELF%YRCUMFS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TEGWDWMS (SELF%YREGWDWMS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TECUMF2 (SELF%YRECUMF2, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TPHLC (SELF%YRPHLC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TPHNC (SELF%YRPHNC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TNCL (SELF%YRNCL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_COPY_TSRFTLAD (SELF%YRSRFTLAD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  !$ACC ENTER DATA CREATE (SELF%GPHIST)
  !$ACC UPDATE DEVICE (SELF%GPHIST)
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL ACDC_COPY_SPHYS_HIST_TYPE (SELF%GPHIST (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%GPHIST)
ENDIF

END SUBROUTINE

