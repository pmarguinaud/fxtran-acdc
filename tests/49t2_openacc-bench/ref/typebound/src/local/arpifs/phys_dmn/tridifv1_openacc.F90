
SUBROUTINE TRIDIFV1_OPENACC (YDPHY, YDPHY0, KIDIA, KFDIA, KLON, KTDIA&
&, KLEV, PA, PB, PC, PF, PDFDT, PIPOI, PY, PCFA, PCFB, YDSTACK)
!$ACC ROUTINE (TRIDIFV1_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMPHY,ONLY:TPHY
USE YOMPHY0,ONLY:TPHY0

#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


TYPE (TPHY), INTENT (IN)::YDPHY
TYPE (TPHY0), INTENT (IN)::YDPHY0
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KTDIA
REAL (KIND=JPRB), INTENT (IN)::PA (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PB (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PC (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PY (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PF (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDFDT (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PIPOI (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PCFA (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PCFB (KLON, KLEV)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
TYPE(FXTRAN_ACDC_STACK) :: YLSTACK
fxtran_acdc_temp (REAL (KIND=JPRB), ZY, (KLON, KLEV))
REAL (KIND=JPRB)::ZMUL 

INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JLEV

YLSTACK = YDSTACK



IF (KIND (ZY) == 8) THEN
    fxtran_acdc_alloc8 (ZY)
ELSEIF (KIND (ZY) == 4) THEN
    fxtran_acdc_alloc4 (ZY)
ELSE
    STOP 1
ENDIF


JLON = KIDIA





IF (YDPHY%LEDMFI) THEN
  
  ZY (JLON, KTDIA)=PY (JLON, KTDIA)+PF (JLON, KTDIA-1)*PIPOI (JLON, KTDIA)-PF (JLON, KTDIA)*PIPOI&
  & (JLON, KTDIA)-PDFDT (JLON, KTDIA)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, KTDIA)*PIPOI (JLON, KTDIA&
  &)-PDFDT (JLON, KTDIA)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, KTDIA+1)*PIPOI (JLON, KTDIA)

  

  DO JLEV=KTDIA+1, KLEV-1
    
    ZY (JLON, JLEV)=PY (JLON, JLEV)+PF (JLON, JLEV-1)*PIPOI (JLON, JLEV)-PF (JLON, JLEV)*PIPOI&
    & (JLON, JLEV)+PDFDT (JLON, JLEV-1)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, JLEV-1)*PIPOI (JLON&
    &, JLEV)+PDFDT (JLON, JLEV-1)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, JLEV)*PIPOI (JLON, JLEV&
    &)-PDFDT (JLON, JLEV)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, JLEV)*PIPOI (JLON, JLEV)-PDFDT&
    & (JLON, JLEV)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, JLEV+1)*PIPOI (JLON, JLEV)
  
  ENDDO

  
  ZY (JLON, KLEV)=PY (JLON, KLEV)+PF (JLON, KLEV-1)*PIPOI (JLON, KLEV)+PDFDT (JLON&
  &, KLEV-1)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, KLEV-1)*PIPOI (JLON, KLEV)+PDFDT &
  &(JLON, KLEV-1)*0.5_JPRB*YDPHY0%GCVIMPT*PY (JLON, KLEV)*PIPOI (JLON, KLEV)
  
ELSE

  DO JLEV=KTDIA, KLEV
    
    ZY (JLON, JLEV)=PY (JLON, JLEV)
  
  ENDDO

ENDIF


ZMUL =1._JPRB/PB (JLON, KTDIA)
PCFA (JLON, KTDIA)=-ZMUL *PC (JLON, KTDIA)
PCFB (JLON, KTDIA)=ZMUL *ZY (JLON, KTDIA)


DO JLEV=KTDIA+1, KLEV-1
  
  ZMUL =1._JPRB/(PB (JLON, JLEV)+PA (JLON, JLEV)*PCFA (JLON, JLEV-1))
  PCFA (JLON, JLEV)=-ZMUL *PC (JLON, JLEV)
  PCFB (JLON, JLEV)=ZMUL *(ZY (JLON, JLEV)-PA (JLON, JLEV)*PCFB (JLON, JLEV-1))
  
ENDDO


ZMUL =1._JPRB/(PB (JLON, KLEV)+PA (JLON, KLEV)*PCFA (JLON, KLEV-1))
PCFA (JLON, KLEV)=-ZMUL *PC (JLON, KLEV)
PCFB (JLON, KLEV)=ZMUL *(ZY (JLON, KLEV)-PA (JLON, KLEV)*PCFB (JLON, KLEV-1))



END SUBROUTINE TRIDIFV1_OPENACC
