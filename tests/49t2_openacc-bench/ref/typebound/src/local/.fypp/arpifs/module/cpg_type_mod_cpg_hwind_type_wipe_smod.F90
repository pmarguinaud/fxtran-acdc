SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_HWIND_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_HWIND_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_HWIND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_UH, LF_VH, LF_WWI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LF_UH = ASSOCIATED (SELF%F_UH)
IF (LF_UH .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_UH)
ENDIF

LF_VH = ASSOCIATED (SELF%F_VH)
IF (LF_VH .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_VH)
ENDIF

LF_WWI = ASSOCIATED (SELF%F_WWI)
IF (LF_WWI .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_WWI)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
