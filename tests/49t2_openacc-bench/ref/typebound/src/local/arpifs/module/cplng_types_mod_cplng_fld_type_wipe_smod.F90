MODULE SUBROUTINE ACDC_WIPE_CPLNG_FLD_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (CPLNG_FLD_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LD, LID
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LID = ALLOCATED (SELF%ID)
IF (LID) THEN
  !$ACC EXIT DATA DETACH (SELF%ID)
  !$ACC EXIT DATA DELETE (SELF%ID)
ENDIF

LD = ALLOCATED (SELF%D)
IF (LD) THEN
  !$ACC EXIT DATA DETACH (SELF%D)
  !$ACC EXIT DATA DELETE (SELF%D)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

