MODULE UTIL_SURFACE_VIEW_GROUP_VPRECIP2_MOD

USE SURFACE_VIEWS_DIAGNOSTIC_MODULE, ONLY : SURFACE_VIEW_GROUP_VPRECIP2



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_SURFACE_VIEW_GROUP_VPRECIP2
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_SURFACE_VIEW_GROUP_VPRECIP2
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_SURFACE_VIEW_GROUP_VPRECIP2
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_VPRECIP2 (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VPRECIP2), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_PRECIP2
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_PRECIP2 = ASSOCIATED (SELF%F_PRECIP2)
IF (LF_PRECIP2 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_PRECIP2)
  !$ACC ENTER DATA ATTACH (SELF%F_PRECIP2)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_VPRECIP2 (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VPRECIP2), TARGET :: SELF
LOGICAL :: LF_PRECIP2
LF_PRECIP2 = ASSOCIATED (SELF%F_PRECIP2)
IF (LF_PRECIP2) THEN
  CALL HOST_FIELD_3RB (SELF%F_PRECIP2)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_SURFACE_VIEW_GROUP_VPRECIP2 (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VPRECIP2), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_PRECIP2
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_PRECIP2 = ASSOCIATED (SELF%F_PRECIP2)
IF (LF_PRECIP2 .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PRECIP2)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
