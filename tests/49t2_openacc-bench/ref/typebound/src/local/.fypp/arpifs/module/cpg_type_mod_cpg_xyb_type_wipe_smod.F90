  CALL WIPE_FIELD_3RB (SELF%F_ALPH)
ENDIF

LF_RTGR = ASSOCIATED (SELF%F_RTGR)
IF (LF_RTGR .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_RTGR)
ENDIF

LF_RPRE = ASSOCIATED (SELF%F_RPRE)
IF (LF_RPRE .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_RPRE)
ENDIF

LF_RPP = ASSOCIATED (SELF%F_RPP)
IF (LF_RPP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_RPP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
