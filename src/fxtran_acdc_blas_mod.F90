SUBROUTINE CHECKCONTIGUOUS1 (P, CDMESS)

REAL*8 :: P (:)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (2)) - LOC (P (1)) /= 8) THEN
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

SUBROUTINE CHECKCONTIGUOUS2 (P, CDMESS)

REAL*8 :: P (:, :)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (1, 2)) - LOC (P (1, 1)) /= SIZE (P, 1) * 8) THEN  
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE0 (K)
