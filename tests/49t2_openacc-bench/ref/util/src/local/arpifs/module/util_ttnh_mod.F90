SUBROUTINE ACDC_SAVE_TTNH (SELF, KLUN)

IMPLICIT NONE
TYPE (TTNH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LTRAJNH
WRITE (KLUN) SELF%NLENNH95B
LTRAJNH = ALLOCATED (SELF%TRAJNH)
WRITE (KLUN) LTRAJNH
IF (LTRAJNH) THEN
  WRITE (KLUN) LBOUND (SELF%TRAJNH)
  WRITE (KLUN) UBOUND (SELF%TRAJNH)
  WRITE (KLUN) SELF%TRAJNH
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TTNH (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TTNH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LTRAJNH
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%NLENNH95B
READ (KLUN) LTRAJNH
IF (LTRAJNH) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%TRAJNH (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%TRAJNH,1):UBOUND(SELF%TRAJNH,1), LBOUND(SELF%TRAJNH,2):UBOUND(SELF%TRAJNH,2)))
  READ (KLUN) ZTMP2
  SELF%TRAJNH = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE

