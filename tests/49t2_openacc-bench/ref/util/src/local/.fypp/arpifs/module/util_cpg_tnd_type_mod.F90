SUBROUTINE ACDC_COPY_CPG_TND_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_TND_TYPE), INTENT (IN), TARGET :: SELF
