FUNCTION ACDC_SIZE_TVSPLIP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TVSPLIP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRFVV, LRVSPC, LRVSPTRI
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRVSPTRI = ALLOCATED (SELF%RVSPTRI)
IF (LRVSPTRI) THEN
  ISIZE = KIND (SELF%RVSPTRI) * SIZE (SELF%RVSPTRI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RVSPTRI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRVSPC = ALLOCATED (SELF%RVSPC)
IF (LRVSPC) THEN
  ISIZE = KIND (SELF%RVSPC) * SIZE (SELF%RVSPC)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RVSPC'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRFVV = ALLOCATED (SELF%RFVV)
IF (LRFVV) THEN
  ISIZE = KIND (SELF%RFVV) * SIZE (SELF%RFVV)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RFVV'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

