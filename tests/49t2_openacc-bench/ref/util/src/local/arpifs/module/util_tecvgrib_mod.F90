SUBROUTINE ACDC_LOAD_TECVGRIB (SELF, KLUN)

IMPLICIT NONE
TYPE (TECVGRIB), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMECVGRB_1D, LMECVGRB_2D, LMECVGRB_3D, LMECVGRB_ALL
READ (KLUN) LMECVGRB_1D
IF (LMECVGRB_1D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MECVGRB_1D (IL1(1):IU1(1)))
  READ (KLUN) SELF%MECVGRB_1D
ENDIF
READ (KLUN) LMECVGRB_2D
IF (LMECVGRB_2D) THEN
  READ (KLUN) IL1
