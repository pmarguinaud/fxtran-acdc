SUBROUTINE APL_ARPEGE_TURBULENCE_PART2_OPENACC (YDCST, YDML_PHY_MF, KIDIA, KFDIA, KLON, KTDIAT&
&, KTDIAN, KLEV, PAPHI, PAPHIF, PAPRS, PAPRSF, PDELP, PR, PCP, PT, PQ, PU, PV, PECT, PPRODTH, PKUROV&
&, PDIFCQ, PDIFCS, PSTRCU, PSTRCV, PUSLE, PKCLS, PECTCLS, PFECT, PECT1, PEDR, YDSTACK)
!$ACC ROUTINE (APL_ARPEGE_TURBULENCE_PART2_OPENACC) SEQ

USE PARKIND1,ONLY:JPIM, JPRB
USE YOMCST,ONLY:TCST
USE MODEL_PHYSICS_MF_MOD,ONLY:MODEL_PHYSICS_MF_TYPE
#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


TYPE (TCST), INTENT (IN)::YDCST
TYPE (MODEL_PHYSICS_MF_TYPE), INTENT (IN)::YDML_PHY_MF
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KTDIAT
INTEGER (KIND=JPIM), INTENT (IN)::KTDIAN
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
REAL (KIND=JPRB), INTENT (IN)::PAPHI (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPHIF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPRS (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPRSF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDELP (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PR (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PT (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PQ (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PU (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PV (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PCP (KLON, KLEV)
REAL (KIND=JPRB), INTENT (INOUT)::PECT (KLON, KLEV)
REAL (KIND=JPRB), INTENT (INOUT)::PPRODTH (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PECT1 (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PFECT (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PEDR (KLON, KLEV)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
TYPE(FXTRAN_ACDC_STACK) :: YLSTACK
REAL (KIND=JPRB), INTENT (IN)::PUSLE (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PKCLS (KLON)
REAL (KIND=JPRB), INTENT (IN)::PECTCLS (KLON)
REAL (KIND=JPRB), INTENT (INOUT)::PKUROV (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDIFCQ (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDIFCS (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PSTRCU (KLON, 0:KLEV)
REAL (KIND=JPRB), INTENT (IN)::PSTRCV (KLON, 0:KLEV)
#include "acprod_openacc.intfb.h"
#include "actke2_openacc.intfb.h"
fxtran_acdc_temp (REAL (KIND=JPRB), ZTPRDY, (KLON, KLEV))

INTEGER (KIND=JPIM) :: JL

YLSTACK = YDSTACK



IF (KIND (ZTPRDY) == 8) THEN
    fxtran_acdc_alloc8 (ZTPRDY)
ELSEIF (KIND (ZTPRDY) == 4) THEN
    fxtran_acdc_alloc4 (ZTPRDY)
ELSE
    STOP 1
ENDIF


JL = KIDIA


CALL ACPROD_OPENACC (YDCST, KIDIA, KFDIA, KLON, KLEV, PR, PCP, PT, PQ, PU, PV,&
& PAPRSF, PAPHIF, PDIFCQ, PDIFCS, PSTRCU, PSTRCV, PPRODTH, YDSTACK=YLSTACK)
CALL ACTKE2_OPENACC (YDCST, YDML_PHY_MF, KIDIA, KFDIA, KLON, KTDIAT, KTDIAN, KLEV&
&, PAPHI, PAPHIF, PAPRS, PAPRSF, PDELP, PR, PT, PU, PV, PECT, PPRODTH, PKUROV&
&, PUSLE, PKCLS, PECTCLS, PFECT, PECT1, ZTPRDY, PEDR, YDSTACK=YLSTACK)


END SUBROUTINE APL_ARPEGE_TURBULENCE_PART2_OPENACC
