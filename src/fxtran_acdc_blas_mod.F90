LOGICAL FUNCTION FXTRAN_ACDC_USE_SIMPLE_DGEMM ()

IF (CLSIMPLE_DGEMM == ' ') THEN
  CALL GETENV ('FXTRAN_ACDC_SIMPLE_DGEMM', CLSIMPLE_DGEMM)
  LLSIMPLE_DGEMM = CLSIMPLE_DGEMM (1:1) == '1' 
  CLSIMPLE_DGEMM = '0'
ENDIF

FXTRAN_ACDC_USE_SIMPLE_DGEMM = LLSIMPLE_DGEMM

END FUNCTION

SUBROUTINE FXTRAN_ACDC_SET_SIMPLE_DGEMM (LDSIMPLE_DGEMM)

LOGICAL, INTENT (IN) :: LDSIMPLE_DGEMM

LLSIMPLE_DGEMM = LDSIMPLE_DGEMM
CLSIMPLE_DGEMM = '0'

END SUBROUTINE

