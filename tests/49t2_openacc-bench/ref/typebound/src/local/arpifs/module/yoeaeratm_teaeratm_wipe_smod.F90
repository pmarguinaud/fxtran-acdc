SUBMODULE (YOEAERATM) YOEAERATM_TEAERATM_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TEAERATM (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEAERATM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYAERO_DESC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LYAERO_DESC = ASSOCIATED (SELF%YAERO_DESC)
IF (LYAERO_DESC) THEN
  !$ACC EXIT DATA DETACH (SELF%YAERO_DESC)
  DO J1 = LBOUND (SELF%YAERO_DESC, 1), UBOUND (SELF%YAERO_DESC, 1)
    CALL SELF%YAERO_DESC (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%YAERO_DESC)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
