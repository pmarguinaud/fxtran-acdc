SUBROUTINE ACDC_COPY_TEAERATM (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_AERO_DESC_MOD
IMPLICIT NONE
TYPE (TEAERATM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYAERO_DESC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LYAERO_DESC = ASSOCIATED (SELF%YAERO_DESC)
IF (LYAERO_DESC) THEN
  !$ACC ENTER DATA CREATE (SELF%YAERO_DESC)
  !$ACC UPDATE DEVICE (SELF%YAERO_DESC)
  DO J1 = LBOUND (SELF%YAERO_DESC, 1), UBOUND (SELF%YAERO_DESC, 1)
    CALL ACDC_COPY_TYPE_AERO_DESC (SELF%YAERO_DESC (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YAERO_DESC)
ENDIF

