TYPE (CUBLASHANDLE) FUNCTION GETCUBLASHANDLE ()

TYPE (CUBLASHANDLE), SAVE :: YLCBH
LOGICAL, SAVE :: LLCBH_INITIALIZED = .FALSE.

IF (.NOT. LLCBH_INITIALIZED) THEN
  CALL CHECKCUBLAS (CUBLASCREATE (YLCBH))
  CALL CHECKCUBLAS (CUBLASGETSTREAM (YLCBH, STREAM))
  LLCBH_INITIALIZED = .TRUE.
ENDIF

GETCUBLASHANDLE = YLCBH

END FUNCTION

SUBROUTINE CHECKCUBLAS (KSTATUS)

INTEGER :: KSTATUS

IF (KSTATUS /= CUBLAS_STATUS_SUCCESS) THEN
  PRINT *, 'CUBLAS ERROR: STATUS =', KSTATUS
  CALL FXTRAN_ACDC_ABORT ('CHECKCUBLAS')
END IF

END SUBROUTINE CHECKCUBLAS

SUBROUTINE CHECKCUDA (KSTATUS)

INTEGER :: KSTATUS

IF (KSTATUS /= CUDASUCCESS) THEN
  PRINT *, 'CUDA ERROR:', CUDAGETERRORSTRING (KSTATUS)
  CALL FXTRAN_ACDC_ABORT ('CHECKCUDA')
END IF

END SUBROUTINE CHECKCUDA

SUBROUTINE FXTRAN_ACDC_CHECK_BLAS_STREAM
CALL CHECKCUDA (CUDASTREAMSYNCHRONIZE (STREAM))
END SUBROUTINE

SUBROUTINE FXTRAN_ACDC_CHECK_BLAS (KSTATUS)

INTEGER :: KSTATUS

CALL CHECKCUBLAS (KSTATUS)

END SUBROUTINE
#endif

