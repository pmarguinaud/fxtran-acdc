SUBROUTINE ACDC_SAVE_TMODERR (SELF, KLUN)

IMPLICIT NONE
TYPE (TMODERR), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR
WRITE (KLUN) SELF%LSCALERR
WRITE (KLUN) SELF%LTAPERERR
WRITE (KLUN) SELF%NTYPE_MODERR
WRITE (KLUN) SELF%NSTEP_MODERR
WRITE (KLUN) SELF%NORDER_MODERR
WRITE (KLUN) SELF%NPRTMODERR
WRITE (KLUN) SELF%TAPERERR_INI
WRITE (KLUN) SELF%TAPERERR_END
WRITE (KLUN) SELF%TSTEP_ERR
LMSTEPERR = ALLOCATED (SELF%MSTEPERR)
WRITE (KLUN) LMSTEPERR
IF (LMSTEPERR) THEN
  WRITE (KLUN) LBOUND (SELF%MSTEPERR)
  WRITE (KLUN) UBOUND (SELF%MSTEPERR)
  WRITE (KLUN) SELF%MSTEPERR
ENDIF
LTSTEPERR = ALLOCATED (SELF%TSTEPERR)
WRITE (KLUN) LTSTEPERR
IF (LTSTEPERR) THEN
  WRITE (KLUN) LBOUND (SELF%TSTEPERR)
  WRITE (KLUN) UBOUND (SELF%TSTEPERR)
  WRITE (KLUN) SELF%TSTEPERR
ENDIF
LFSTEPERR = ALLOCATED (SELF%FSTEPERR)
WRITE (KLUN) LFSTEPERR
IF (LFSTEPERR) THEN
  WRITE (KLUN) LBOUND (SELF%FSTEPERR)
  WRITE (KLUN) UBOUND (SELF%FSTEPERR)
  WRITE (KLUN) SELF%FSTEPERR
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TMODERR (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TMODERR), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%LSCALERR
READ (KLUN) SELF%LTAPERERR
READ (KLUN) SELF%NTYPE_MODERR
READ (KLUN) SELF%NSTEP_MODERR
READ (KLUN) SELF%NORDER_MODERR
READ (KLUN) SELF%NPRTMODERR
READ (KLUN) SELF%TAPERERR_INI
READ (KLUN) SELF%TAPERERR_END
READ (KLUN) ZTMP0
SELF%TSTEP_ERR = ZTMP0
READ (KLUN) LMSTEPERR
IF (LMSTEPERR) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MSTEPERR (IL1(1):IU1(1)))
  READ (KLUN) SELF%MSTEPERR
ENDIF
READ (KLUN) LTSTEPERR
IF (LTSTEPERR) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%TSTEPERR (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%TSTEPERR,1):UBOUND(SELF%TSTEPERR,1)))
  READ (KLUN) ZTMP1
  SELF%TSTEPERR = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LFSTEPERR
IF (LFSTEPERR) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%FSTEPERR (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%FSTEPERR,1):UBOUND(SELF%FSTEPERR,1), LBOUND(SELF%FSTEPERR,2):UBOUND(SELF%FSTEPERR,2)))
  READ (KLUN) ZTMP2
  SELF%FSTEPERR = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE

