SUBMODULE (YOMMCC) YOMMCC_TMCC_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TMCC (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TMCC), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LCLIMR, LCLIMRAER, LCLIMRCO2, LCLIMRCOMPO, LCPLNG_FLD, LOCEANBC, LPARTMASK, LPARTSST0, LRNEMOLVLS, LRNEMOMASK
LOGICAL :: LZLAKE
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LCLIMRCO2 = ALLOCATED (SELF%CLIMRCO2)
IF (LCLIMRCO2) THEN
  !$ACC EXIT DATA DETACH (SELF%CLIMRCO2)
  !$ACC EXIT DATA DELETE (SELF%CLIMRCO2)
ENDIF

LCLIMRAER = ALLOCATED (SELF%CLIMRAER)
IF (LCLIMRAER) THEN
  !$ACC EXIT DATA DETACH (SELF%CLIMRAER)
  !$ACC EXIT DATA DELETE (SELF%CLIMRAER)
ENDIF

LCLIMRCOMPO = ALLOCATED (SELF%CLIMRCOMPO)
IF (LCLIMRCOMPO) THEN
  !$ACC EXIT DATA DETACH (SELF%CLIMRCOMPO)
  !$ACC EXIT DATA DELETE (SELF%CLIMRCOMPO)
ENDIF

LZLAKE = ALLOCATED (SELF%ZLAKE)
IF (LZLAKE) THEN
  !$ACC EXIT DATA DETACH (SELF%ZLAKE)
  !$ACC EXIT DATA DELETE (SELF%ZLAKE)
ENDIF

LOCEANBC = ALLOCATED (SELF%OCEANBC)
IF (LOCEANBC) THEN
  !$ACC EXIT DATA DETACH (SELF%OCEANBC)
  !$ACC EXIT DATA DELETE (SELF%OCEANBC)
ENDIF

LCLIMR = ALLOCATED (SELF%CLIMR)
IF (LCLIMR) THEN
  !$ACC EXIT DATA DETACH (SELF%CLIMR)
  !$ACC EXIT DATA DELETE (SELF%CLIMR)
ENDIF

LRNEMOLVLS = ALLOCATED (SELF%RNEMOLVLS)
IF (LRNEMOLVLS) THEN
  !$ACC EXIT DATA DETACH (SELF%RNEMOLVLS)
  !$ACC EXIT DATA DELETE (SELF%RNEMOLVLS)
ENDIF

LRNEMOMASK = ALLOCATED (SELF%RNEMOMASK)
IF (LRNEMOMASK) THEN
  !$ACC EXIT DATA DETACH (SELF%RNEMOMASK)
  !$ACC EXIT DATA DELETE (SELF%RNEMOMASK)
ENDIF

LPARTSST0 = ALLOCATED (SELF%PARTSST0)
IF (LPARTSST0) THEN
  !$ACC EXIT DATA DETACH (SELF%PARTSST0)
  !$ACC EXIT DATA DELETE (SELF%PARTSST0)
ENDIF

LPARTMASK = ALLOCATED (SELF%PARTMASK)
IF (LPARTMASK) THEN
  !$ACC EXIT DATA DETACH (SELF%PARTMASK)
  !$ACC EXIT DATA DELETE (SELF%PARTMASK)
ENDIF

LCPLNG_FLD = ALLOCATED (SELF%CPLNG_FLD)
IF (LCPLNG_FLD) THEN
  !$ACC EXIT DATA DETACH (SELF%CPLNG_FLD)
  DO J1 = LBOUND (SELF%CPLNG_FLD, 1), UBOUND (SELF%CPLNG_FLD, 1)
    CALL SELF%CPLNG_FLD (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%CPLNG_FLD)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
