SUBROUTINE ACDC_LOAD_TCOMPO (SELF, KLUN)
USE UTIL_TCOMPO_EMIS_AUX_MOD
USE UTIL_TCOMPO_EMIS_MOD
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TCOMPO), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYEMIS2DAUX_DESC, LYEMIS2D_DESC, LYEMIS3D_DESC
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) SELF%LCHEM_DIA
READ (KLUN) ZTMP0
SELF%RCHEM_DIA_PERIOD = ZTMP0
READ (KLUN) SELF%LCHEM_DDFLX
READ (KLUN) SELF%LCOMPO_DDFLX_DIR
READ (KLUN) SELF%LCHEM_TROPO
READ (KLUN) SELF%LAEREQSAM4CLIM
READ (KLUN) SELF%LAEROSFC
READ (KLUN) SELF%LCOMPO_DCDD
READ (KLUN) SELF%LAERNITRATE
READ (KLUN) SELF%LAERRESUSPENSION
READ (KLUN) SELF%LAERSOA
READ (KLUN) SELF%LAERSOA_COUPLED
READ (KLUN) SELF%KGHG_CHEMTEND_CH4
READ (KLUN) SELF%LAERNUCL
READ (KLUN) SELF%AERO_SCHEME
READ (KLUN) SELF%NEMIS2D_DESC
READ (KLUN) LYEMIS2D_DESC
IF (LYEMIS2D_DESC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YEMIS2D_DESC (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YEMIS2D_DESC, 1), UBOUND (SELF%YEMIS2D_DESC, 1)
    CALL ACDC_LOAD_TCOMPO_EMIS (SELF%YEMIS2D_DESC (J1), KLUN)
  ENDDO
ENDIF
READ (KLUN) SELF%NEMIS3D_DESC
READ (KLUN) LYEMIS3D_DESC
IF (LYEMIS3D_DESC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YEMIS3D_DESC (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YEMIS3D_DESC, 1), UBOUND (SELF%YEMIS3D_DESC, 1)
    CALL ACDC_LOAD_TCOMPO_EMIS (SELF%YEMIS3D_DESC (J1), KLUN)
  ENDDO
ENDIF
READ (KLUN) LYEMIS2DAUX_DESC
IF (LYEMIS2DAUX_DESC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YEMIS2DAUX_DESC (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YEMIS2DAUX_DESC, 1), UBOUND (SELF%YEMIS2DAUX_DESC, 1)
    CALL ACDC_LOAD_TCOMPO_EMIS_AUX (SELF%YEMIS2DAUX_DESC (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

