SUBROUTINE ACDC_LOAD_TOROG_BLOCKED (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TOROG_BLOCKED), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LOROG
IF (LOROG) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%OROG (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%OROG,1):UBOUND(SELF%OROG,1), LBOUND(SELF%OROG,2):UBOUND(SELF%OROG,2)))
  READ (KLUN) ZTMP2
  SELF%OROG = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LOROGL
IF (LOROGL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%OROGL (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%OROGL,1):UBOUND(SELF%OROGL,1), LBOUND(SELF%OROGL,2):UBOUND(SELF%OROGL,2)))
  READ (KLUN) ZTMP2
  SELF%OROGL = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LOROGM
IF (LOROGM) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%OROGM (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%OROGM,1):UBOUND(SELF%OROGM,1), LBOUND(SELF%OROGM,2):UBOUND(SELF%OROGM,2)))
  READ (KLUN) ZTMP2
  SELF%OROGM = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LOROGLL
IF (LOROGLL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%OROGLL (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%OROGLL,1):UBOUND(SELF%OROGLL,1), LBOUND(SELF%OROGLL,2):UBOUND(SELF%OROGLL,2)))
  READ (KLUN) ZTMP2
  SELF%OROGLL = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LOROGMM
IF (LOROGMM) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%OROGMM (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%OROGMM,1):UBOUND(SELF%OROGMM,1), LBOUND(SELF%OROGMM,2):UBOUND(SELF%OROGMM,2)))
  READ (KLUN) ZTMP2
  SELF%OROGMM = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LOROGLM
IF (LOROGLM) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%OROGLM (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%OROGLM,1):UBOUND(SELF%OROGLM,1), LBOUND(SELF%OROGLM,2):UBOUND(SELF%OROGLM,2)))
  READ (KLUN) ZTMP2
  SELF%OROGLM = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE

