SUBROUTINE ACDC_SAVE_TSPNG (SELF, KLUN)

IMPLICIT NONE
TYPE (TSPNG), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRSPONGF
WRITE (KLUN) SELF%LNSPONGE
LRSPONGF = ALLOCATED (SELF%RSPONGF)
WRITE (KLUN) LRSPONGF
IF (LRSPONGF) THEN
  WRITE (KLUN) LBOUND (SELF%RSPONGF)
  WRITE (KLUN) UBOUND (SELF%RSPONGF)
  WRITE (KLUN) SELF%RSPONGF
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TSPNG (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TSPNG), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LRSPONGF
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) SELF%LNSPONGE
READ (KLUN) LRSPONGF
IF (LRSPONGF) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RSPONGF (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RSPONGF,1):UBOUND(SELF%RSPONGF,1), LBOUND(SELF%RSPONGF,2):UBOUND(SELF%RSPONGF,2)))
  READ (KLUN) ZTMP2
  SELF%RSPONGF = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE

