CALL ACDC_WIPE_TECVDIM (SELF%YRDIMECV, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_TMODERR (SELF%YRMODERR, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_TSPPT_CONFIG (SELF%YRSPPT_CONFIG, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL ACDC_WIPE_TSPP_CONFIG (SELF%YRSPP_CONFIG, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION ACDC_SIZE_MODEL_GENERAL_CONF_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_GEOMETRY_MOD
USE UTIL_TDIMF_MOD
USE UTIL_TECVDIM_MOD
USE UTIL_TMODERR_MOD
USE UTIL_TRIP_MOD
USE UTIL_TSPPT_CONFIG_MOD
USE UTIL_TSPP_CONFIG_MOD
USE UTIL_TYPE_GFLD_MOD
IMPLICIT NONE
TYPE (MODEL_GENERAL_CONF_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGEOM
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LGEOM = ASSOCIATED (SELF%GEOM)
IF (LGEOM) THEN
  JSIZE = 0
  ISIZE = ACDC_SIZE_GEOMETRY (SELF%GEOM, CLPATH//'%GEOM', .FALSE.)
  JSIZE = JSIZE + ISIZE
  KSIZE = KSIZE + ISIZE
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%GEOM'
  ENDIF
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TDIMF (SELF%YRDIMF, CLPATH//'%YRDIMF', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDIMF'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TYPE_GFLD (SELF%YGFL, CLPATH//'%YGFL', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YGFL'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TRIP (SELF%YRRIP, CLPATH//'%YRRIP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRRIP'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TECVDIM (SELF%YRDIMECV, CLPATH//'%YRDIMECV', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDIMECV'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TMODERR (SELF%YRMODERR, CLPATH//'%YRMODERR', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMODERR'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TSPPT_CONFIG (SELF%YRSPPT_CONFIG, CLPATH//'%YRSPPT_CONFIG', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSPPT_CONFIG'
ENDIF
JSIZE = 0
ISIZE = ACDC_SIZE_TSPP_CONFIG (SELF%YRSPP_CONFIG, CLPATH//'%YRSPP_CONFIG', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSPP_CONFIG'
ENDIF
END FUNCTION

