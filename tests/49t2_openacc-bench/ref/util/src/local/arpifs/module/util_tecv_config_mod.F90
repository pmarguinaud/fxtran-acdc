MODULE UTIL_TECV_CONFIG_MOD

USE TYPE_ECV, ONLY : TECV_CONFIG

INTERFACE ACDC_SAVE
MODULE PROCEDURE ACDC_SAVE_TECV_CONFIG
END INTERFACE

INTERFACE ACDC_LOAD
MODULE PROCEDURE ACDC_LOAD_TECV_CONFIG
END INTERFACE

INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_TECV_CONFIG
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_TECV_CONFIG
END INTERFACE


INTERFACE ACDC_CRC64
MODULE PROCEDURE ACDC_CRC64_TECV_CONFIG
END INTERFACE

INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_TECV_CONFIG
END INTERFACE

INTERFACE ACDC_SIZE
MODULE PROCEDURE ACDC_SIZE_TECV_CONFIG
END INTERFACE

CONTAINS

SUBROUTINE ACDC_SAVE_TECV_CONFIG (SELF, KLUN)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYRCONFIG
WRITE (KLUN) SELF%NECV
LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
WRITE (KLUN) LYRCONFIG
IF (LYRCONFIG) THEN
  WRITE (KLUN) LBOUND (SELF%YRCONFIG)
  WRITE (KLUN) UBOUND (SELF%YRCONFIG)
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL ACDC_SAVE_TECV_CONFIG_BASE (SELF%YRCONFIG (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TECV_CONFIG (SELF, KLUN)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYRCONFIG
READ (KLUN) SELF%NECV
READ (KLUN) LYRCONFIG
IF (LYRCONFIG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YRCONFIG (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL ACDC_LOAD_TECV_CONFIG_BASE (SELF%YRCONFIG (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_COPY_TECV_CONFIG (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYRCONFIG
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  !$ACC ENTER DATA CREATE (SELF%YRCONFIG)
  !$ACC UPDATE DEVICE (SELF%YRCONFIG)
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL ACDC_COPY_TECV_CONFIG_BASE (SELF%YRCONFIG (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YRCONFIG)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_TECV_CONFIG (SELF)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LYRCONFIG

LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL ACDC_HOST_TECV_CONFIG_BASE (SELF%YRCONFIG (J1))
  ENDDO
ENDIF

END SUBROUTINE



SUBROUTINE ACDC_CRC64_TECV_CONFIG (SELF, KLUN, CDPATH)
USE UTIL_TECV_CONFIG_BASE_MOD
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (TECV_CONFIG), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYRCONFIG
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NECV), CDPATH//'%NECV'

LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL ACDC_CRC64_TECV_CONFIG_BASE (SELF%YRCONFIG (J1), KLUN, CDPATH//'%YRCONFIG'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_WIPE_TECV_CONFIG (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYRCONFIG
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  !$ACC EXIT DATA DETACH (SELF%YRCONFIG)
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    CALL ACDC_WIPE_TECV_CONFIG_BASE (SELF%YRCONFIG (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%YRCONFIG)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION ACDC_SIZE_TECV_CONFIG (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
INTEGER :: J1
LOGICAL :: LYRCONFIG
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NECV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECV'
ENDIF
KSIZE = KSIZE + ISIZE
LYRCONFIG = ALLOCATED (SELF%YRCONFIG)
IF (LYRCONFIG) THEN
  JSIZE = 0
  DO J1 = LBOUND (SELF%YRCONFIG, 1), UBOUND (SELF%YRCONFIG, 1)
    ISIZE = ACDC_SIZE_TECV_CONFIG_BASE (SELF%YRCONFIG (J1), CLPATH//'%YRCONFIG', .FALSE.)
    JSIZE = JSIZE + ISIZE
    KSIZE = KSIZE + ISIZE
  ENDDO
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%YRCONFIG'
  ENDIF
ENDIF
END FUNCTION

END MODULE
