MODULE UTIL_CPG_RCP_TYPE_MOD

USE CPG_TYPE_MOD, ONLY : CPG_RCP_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_CPG_RCP_TYPE
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_CPG_RCP_TYPE
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_CPG_RCP_TYPE
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_CPG_RCP_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_RCP_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CP, LF_KAP, LF_R
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LF_CP = ASSOCIATED (SELF%F_CP)
IF (LF_CP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_CP)
  !$ACC ENTER DATA ATTACH (SELF%F_CP)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_R)
  !$ACC ENTER DATA ATTACH (SELF%F_R)
ENDIF

LF_KAP = ASSOCIATED (SELF%F_KAP)
IF (LF_KAP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_KAP)
  !$ACC ENTER DATA ATTACH (SELF%F_KAP)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_CPG_RCP_TYPE (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_RCP_TYPE), TARGET :: SELF
LOGICAL :: LF_CP, LF_KAP, LF_R

LF_CP = ASSOCIATED (SELF%F_CP)
IF (LF_CP) THEN
  CALL HOST_FIELD_3RB (SELF%F_CP)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R) THEN
  CALL HOST_FIELD_3RB (SELF%F_R)
ENDIF

LF_KAP = ASSOCIATED (SELF%F_KAP)
IF (LF_KAP) THEN
  CALL HOST_FIELD_3RB (SELF%F_KAP)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_CPG_RCP_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_RCP_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CP, LF_KAP, LF_R
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LF_CP = ASSOCIATED (SELF%F_CP)
IF (LF_CP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_CP)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_R)
ENDIF

LF_KAP = ASSOCIATED (SELF%F_KAP)
IF (LF_KAP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_KAP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
