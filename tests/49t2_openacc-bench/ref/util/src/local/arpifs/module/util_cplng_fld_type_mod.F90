SUBROUTINE ACDC_SAVE_CPLNG_FLD_TYPE (SELF, KLUN)

IMPLICIT NONE
TYPE (CPLNG_FLD_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LD, LID
LID = ALLOCATED (SELF%ID)
WRITE (KLUN) LID
IF (LID) THEN
  WRITE (KLUN) LBOUND (SELF%ID)
  WRITE (KLUN) UBOUND (SELF%ID)
  WRITE (KLUN) SELF%ID
ENDIF
WRITE (KLUN) SELF%NAME
WRITE (KLUN) SELF%TYPE
WRITE (KLUN) SELF%INOUT
WRITE (KLUN) SELF%STAGE
WRITE (KLUN) SELF%NUM_LVL
WRITE (KLUN) SELF%NUM_CAT
LD = ALLOCATED (SELF%D)
WRITE (KLUN) LD
IF (LD) THEN
  WRITE (KLUN) LBOUND (SELF%D)
  WRITE (KLUN) UBOUND (SELF%D)
  WRITE (KLUN) SELF%D
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_CPLNG_FLD_TYPE (SELF, KLUN)

IMPLICIT NONE
TYPE (CPLNG_FLD_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LD, LID
READ (KLUN) LID
IF (LID) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%ID (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%ID
ENDIF
READ (KLUN) SELF%NAME
READ (KLUN) SELF%TYPE
READ (KLUN) SELF%INOUT
READ (KLUN) SELF%STAGE
READ (KLUN) SELF%NUM_LVL
READ (KLUN) SELF%NUM_CAT
READ (KLUN) LD
IF (LD) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%D (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  READ (KLUN) SELF%D
ENDIF
END SUBROUTINE

