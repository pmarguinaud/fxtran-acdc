IMPLICIT NONE
CLASS (TEMMP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LMYENS, LNEALLNS, LNEPROCN, LNEPTRNS, LNUEMPP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LNEPROCN = ASSOCIATED (SELF%NEPROCN)
IF (LNEPROCN) THEN
  !$ACC EXIT DATA DETACH (SELF%NEPROCN)
  !$ACC EXIT DATA DELETE (SELF%NEPROCN)
ENDIF

LMYENS = ASSOCIATED (SELF%MYENS)
IF (LMYENS) THEN
  !$ACC EXIT DATA DETACH (SELF%MYENS)
  !$ACC EXIT DATA DELETE (SELF%MYENS)
ENDIF

