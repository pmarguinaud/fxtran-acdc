IMPLICIT NONE

PRIVATE

PUBLIC :: FXTRAN_ACDC_CHECKCONTIGUOUS

INTERFACE FXTRAN_ACDC_CHECKCONTIGUOUS
  MODULE PROCEDURE CHECKCONTIGUOUS1
  MODULE PROCEDURE CHECKCONTIGUOUS2
END INTERFACE

CONTAINS

SUBROUTINE CHECKCONTIGUOUS1 (P, CDMESS)

USE FXTRAN_ACDC_ABORT_MOD

REAL*8 :: P (:)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (2)) - LOC (P (1)) /= 8) THEN
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

SUBROUTINE CHECKCONTIGUOUS2 (P, CDMESS)

USE FXTRAN_ACDC_ABORT_MOD

REAL*8 :: P (:, :)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (1, 2)) - LOC (P (1, 1)) /= SIZE (P, 1) * 8) THEN  
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

END MODULE
