READ (KLUN) ZTMP0
SELF%RDECORR_CW = ZTMP0
READ (KLUN) ZTMP0
SELF%RETAHB = ZTMP0
READ (KLUN) ZTMP0
SELF%RETAMB = ZTMP0
READ (KLUN) ZTMP0
SELF%RLOIA = ZTMP0
READ (KLUN) ZTMP0
SELF%RLOIB = ZTMP0
READ (KLUN) ZTMP0
SELF%RLOIC = ZTMP0
