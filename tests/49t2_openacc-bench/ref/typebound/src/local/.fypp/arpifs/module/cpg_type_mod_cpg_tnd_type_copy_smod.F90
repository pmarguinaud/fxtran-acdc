SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_TND_TYPE_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_CPG_TND_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_TND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_TNDGW, LF_TNDPD, LF_TNDT, LF_TNDU, LF_TNDU_NOC, LF_TNDV, LF_TNDVD, LF_TNDV_NOC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LF_TNDU = ASSOCIATED (SELF%F_TNDU)
IF (LF_TNDU .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDU)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDU)
ENDIF

LF_TNDV = ASSOCIATED (SELF%F_TNDV)
IF (LF_TNDV .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDV)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDV)
ENDIF

LF_TNDU_NOC = ASSOCIATED (SELF%F_TNDU_NOC)
IF (LF_TNDU_NOC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDU_NOC)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDU_NOC)
ENDIF

LF_TNDV_NOC = ASSOCIATED (SELF%F_TNDV_NOC)
IF (LF_TNDV_NOC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDV_NOC)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDV_NOC)
ENDIF

LF_TNDT = ASSOCIATED (SELF%F_TNDT)
IF (LF_TNDT .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDT)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDT)
ENDIF

LF_TNDPD = ASSOCIATED (SELF%F_TNDPD)
IF (LF_TNDPD .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDPD)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDPD)
ENDIF

LF_TNDVD = ASSOCIATED (SELF%F_TNDVD)
IF (LF_TNDVD .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDVD)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDVD)
ENDIF

LF_TNDGW = ASSOCIATED (SELF%F_TNDGW)
IF (LF_TNDGW .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TNDGW)
  !$ACC ENTER DATA ATTACH (SELF%F_TNDGW)
ENDIF

END SUBROUTINE

END SUBMODULE
