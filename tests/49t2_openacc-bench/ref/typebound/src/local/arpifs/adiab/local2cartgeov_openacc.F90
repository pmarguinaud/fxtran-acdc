SUBROUTINE LOCAL2CARTGEOV_OPENACC (KL1, KFLEV, KPROMA, KST, KEND&
&, PGEMU, PGSQM2, PGECLO, PGESLO, PU, PV, PZ, YDSTACK)
!$ACC ROUTINE (LOCAL2CARTGEOV_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


INTEGER (KIND=JPIM), INTENT (IN)::KEND
INTEGER (KIND=JPIM), INTENT (IN)::KST
INTEGER (KIND=JPIM), INTENT (IN)::KPROMA
INTEGER (KIND=JPIM), INTENT (IN)::KFLEV
INTEGER (KIND=JPIM), INTENT (IN)::KL1
REAL (KIND=JPRB), INTENT (IN)::PGESLO (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGECLO (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGSQM2 (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGEMU (KPROMA)
REAL (KIND=JPRB), INTENT (INOUT)::PU (KPROMA, KL1:KFLEV)
REAL (KIND=JPRB), INTENT (INOUT)::PV (KPROMA, KL1:KFLEV)
REAL (KIND=JPRB), INTENT (OUT)::PZ (KPROMA, KL1:KFLEV)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPRB)::ZV
REAL (KIND=JPRB)::ZU


JROF = KST



DO JLEV=1, KFLEV
  
  ZU=PU (JROF, JLEV)
  ZV=PV (JROF, JLEV)
  PU (JROF, JLEV)=-ZU*PGESLO (JROF)-ZV*PGECLO (JROF)*PGEMU (JROF)
  PV (JROF, JLEV)=ZU*PGECLO (JROF)-ZV*PGESLO (JROF)*PGEMU (JROF)
  PZ (JROF, JLEV)=ZV*PGSQM2 (JROF)
  
ENDDO


END SUBROUTINE LOCAL2CARTGEOV_OPENACC
