MODULE UTIL_CPG_HWIND_TYPE_MOD

USE CPG_TYPE_MOD, ONLY : CPG_HWIND_TYPE



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_CPG_HWIND_TYPE
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_CPG_HWIND_TYPE
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_CPG_HWIND_TYPE
END INTERFACE



CONTAINS





SUBROUTINE ACDC_COPY_CPG_HWIND_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_HWIND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_UH, LF_VH, LF_WWI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$OMP TARGET ENTER DATA MAP (ALLOC: SELF)
  !$OMP TARGET UPDATE TO (SELF)
ENDIF

LF_UH = ASSOCIATED (SELF%F_UH)
IF (LF_UH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_UH)
  !$OMP TARGET ENTER DATA MAP (TO: SELF%F_UH)
ENDIF

LF_VH = ASSOCIATED (SELF%F_VH)
IF (LF_VH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_VH)
  !$OMP TARGET ENTER DATA MAP (TO: SELF%F_VH)
ENDIF

LF_WWI = ASSOCIATED (SELF%F_WWI)
IF (LF_WWI .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_WWI)
  !$OMP TARGET ENTER DATA MAP (TO: SELF%F_WWI)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_CPG_HWIND_TYPE (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_HWIND_TYPE), TARGET :: SELF
LOGICAL :: LF_UH, LF_VH, LF_WWI

LF_UH = ASSOCIATED (SELF%F_UH)
IF (LF_UH) THEN
  CALL HOST_FIELD_3RB (SELF%F_UH)
ENDIF

LF_VH = ASSOCIATED (SELF%F_VH)
IF (LF_VH) THEN
  CALL HOST_FIELD_3RB (SELF%F_VH)
ENDIF

LF_WWI = ASSOCIATED (SELF%F_WWI)
IF (LF_WWI) THEN
  CALL HOST_FIELD_3RB (SELF%F_WWI)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_CPG_HWIND_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_HWIND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_UH, LF_VH, LF_WWI
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LF_UH = ASSOCIATED (SELF%F_UH)
IF (LF_UH .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_UH)
ENDIF

LF_VH = ASSOCIATED (SELF%F_VH)
IF (LF_VH .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_VH)
ENDIF

LF_WWI = ASSOCIATED (SELF%F_WWI)
IF (LF_WWI .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_WWI)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$OMP TARGET EXIT DATA MAP (DELETE: SELF)
ENDIF
END SUBROUTINE





END MODULE
