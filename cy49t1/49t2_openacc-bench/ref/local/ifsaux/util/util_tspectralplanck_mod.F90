INTEGER*8 FUNCTION SIZE_TSPECTRALPLANCK (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TSPECTRALPLANCK),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LINTERVAL_MAP, LPLANCK_LUT, LWAVLEN_BOUND

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NINTERVALS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NINTERVALS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NTEMPS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NTEMPS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%TEMP1)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TEMP1'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%DTEMP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%DTEMP'
ENDIF
KSIZE = KSIZE + ISIZE
LPLANCK_LUT = ALLOCATED (YD%PLANCK_LUT)
IF (LPLANCK_LUT) THEN
  ISIZE = KIND (YD%PLANCK_LUT) * SIZE (YD%PLANCK_LUT)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PLANCK_LUT'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LWAVLEN_BOUND = ALLOCATED (YD%WAVLEN_BOUND)
IF (LWAVLEN_BOUND) THEN
  ISIZE = KIND (YD%WAVLEN_BOUND) * SIZE (YD%WAVLEN_BOUND)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%WAVLEN_BOUND'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LINTERVAL_MAP = ALLOCATED (YD%INTERVAL_MAP)
IF (LINTERVAL_MAP) THEN
  ISIZE = KIND (YD%INTERVAL_MAP) * SIZE (YD%INTERVAL_MAP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%INTERVAL_MAP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

