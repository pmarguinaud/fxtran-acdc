IF (KIND (ZSUH) == 8) THEN
    fxtran_acdc_alloc8 (ZSUH)
ELSEIF (KIND (ZSUH) == 4) THEN
    fxtran_acdc_alloc4 (ZSUH)
ELSE
    STOP 1
