SUBROUTINE ACDC_LOAD_TECMIP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TECMIP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL3(3), IU3(3), IL4(4), IU4(4)
LOGICAL :: LRLATCLI, LRLONCLI, LROZT1, LRPROC1, LRSINC1, LZOZCL
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) SELF%NRCP
READ (KLUN) SELF%NO3CMIP
READ (KLUN) SELF%NGHGCMIP
READ (KLUN) SELF%NCMIPFIXYR
READ (KLUN) SELF%NCURRYR
READ (KLUN) SELF%GHGDATADIR
READ (KLUN) SELF%CSOLARDATA
READ (KLUN) SELF%CO3DATADIR
READ (KLUN) SELF%CO3DATAFIL
READ (KLUN) LZOZCL
IF (LZOZCL) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%ZOZCL (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%ZOZCL,1):UBOUND(SELF%ZOZCL,1), LBOUND(SELF%ZOZCL,2):UBOUND(SELF%ZOZCL,2), LBOUND(SELF%ZOZCL,3):UBOUND(SELF%ZOZCL,3), LBOUND(SELF%ZOZCL,4):UBOUND(SELF%ZOZCL,4)))
  READ (KLUN) ZTMP4
  SELF%ZOZCL = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LRSINC1
IF (LRSINC1) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RSINC1 (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RSINC1,1):UBOUND(SELF%RSINC1,1)))
  READ (KLUN) ZTMP1
  SELF%RSINC1 = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRPROC1
IF (LRPROC1) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RPROC1 (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RPROC1,1):UBOUND(SELF%RPROC1,1)))
  READ (KLUN) ZTMP1
  SELF%RPROC1 = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLATCLI
IF (LRLATCLI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLATCLI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLATCLI,1):UBOUND(SELF%RLATCLI,1)))
  READ (KLUN) ZTMP1
  SELF%RLATCLI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLONCLI
IF (LRLONCLI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RLONCLI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RLONCLI,1):UBOUND(SELF%RLONCLI,1)))
  READ (KLUN) ZTMP1
  SELF%RLONCLI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LROZT1
IF (LROZT1) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%ROZT1 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%ROZT1,1):UBOUND(SELF%ROZT1,1), LBOUND(SELF%ROZT1,2):UBOUND(SELF%ROZT1,2), LBOUND(SELF%ROZT1,3):UBOUND(SELF%ROZT1,3)))
  READ (KLUN) ZTMP3
  SELF%ROZT1 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
END SUBROUTINE

