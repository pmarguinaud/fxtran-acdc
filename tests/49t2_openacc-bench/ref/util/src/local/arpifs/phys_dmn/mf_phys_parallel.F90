SUBROUTINE MF_PHYS_PARALLEL (YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDCPG_MISC, YDCPG_GPAR&
&, YDCPG_PHY0, YDCPG_PHY9, YDMF_PHYS, YDHGRAD, YDCPG_DYN0, YDCPG_DYN9, YDMF_PHYS_SURF&
&, YDCPG_SL1, YDCPG_SL2, YDVARS, YDXFU, YDCFU, YDMODEL, YDFIELDS, YDPHYSMWAVE, YDA_GFLPT&
&, PGPSDT2D, PGFL, PGMVT1, PGFLT1, PTRAJ_PHYS, YDDDH)

USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE CPG_TYPE_MOD,ONLY:CPG_DYN_TYPE, CPG_GPAR_TYPE, CPG_MISC_TYPE, CPG_PHY_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE CPG_SL2_TYPE_MOD,ONLY:CPG_SL2_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE TYPE_MODEL,ONLY:MODEL
USE FIELDS_MOD,ONLY:FIELDS
USE YOMTRAJ,ONLY:TRAJ_PHYS_TYPE
USE DDH_MIX,ONLY:TYP_DDH
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE MF_PHYS_NEXT_STATE_TYPE_MOD,ONLY:MF_PHYS_NEXT_STATE_TYPE
USE YOMXFU,ONLY:TXFU
USE YOMCFU,ONLY:TCFU
USE FIELD_ARRAY_MODULE
USE YOMDGRADIENT_TYPE_MOD,ONLY:GRADIENT_TYPE
USE YOE_PHYS_MWAVE,ONLY:TEPHYSMWAVE
USE FIELD_MODULE
USE FIELD_FACTORY_MODULE
USE FIELD_ACCESS_MODULE
USE FXTRAN_ACDC_PARALLELMETHOD_MOD
USE FXTRAN_ACDC_STACK_MOD
USE PARKIND1
USE FXTRAN_ACDC_ARRAY_COPY_MOD
USE FXTRAN_ACDC_GPUMEM_MOD
#include "fxtran_acdc_stack.h"

IMPLICIT NONE


TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_MISC_TYPE), INTENT (INOUT)::YDCPG_MISC
TYPE (CPG_GPAR_TYPE), INTENT (INOUT)::YDCPG_GPAR
TYPE (CPG_PHY_TYPE), INTENT (INOUT)::YDCPG_PHY0
TYPE (CPG_PHY_TYPE), INTENT (INOUT)::YDCPG_PHY9
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (GRADIENT_TYPE), INTENT (IN)::YDHGRAD
TYPE (CPG_DYN_TYPE), INTENT (IN)::YDCPG_DYN0
TYPE (CPG_DYN_TYPE), INTENT (IN)::YDCPG_DYN9
TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
TYPE (CPG_SL2_TYPE), INTENT (INOUT)::YDCPG_SL2
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (TXFU), INTENT (INOUT)::YDXFU
TYPE (TCFU), INTENT (INOUT)::YDCFU
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELDS), INTENT (IN)::YDFIELDS
TYPE (TEPHYSMWAVE), INTENT (INOUT)::YDPHYSMWAVE
TYPE (FIELD_4RB_ARRAY), INTENT (INOUT), OPTIONAL::YDA_GFLPT
REAL (KIND=JPRB), INTENT (IN), OPTIONAL::PGPSDT2D (YDCPG_OPTS%KLON&
&, YDMODEL%YRML_SPPT%YGPSDT (1)%NG2D, YDMODEL%YRML_SPPT%N2D)
REAL (KIND=JPRB), INTENT (INOUT), OPTIONAL::PGFL (YDCPG_OPTS&
&%KLON, YDCPG_OPTS%KFLEVG, YDMODEL%YRML_GCONF%YGFL%NDIM)
REAL (KIND=JPRB), INTENT (INOUT), OPTIONAL::PGMVT1 (YDCPG_OPTS&
&%KLON, YDCPG_OPTS%KFLEVG, YDFIELDS%YRGMV%YT1%NDIM)
REAL (KIND=JPRB), INTENT (INOUT), OPTIONAL::PGFLT1 (YDCPG_OPTS&
&%KLON, YDCPG_OPTS%KFLEVG, YDMODEL%YRML_GCONF%YGFL%NDIM1)
TYPE (TRAJ_PHYS_TYPE), INTENT (INOUT), OPTIONAL::PTRAJ_PHYS
TYPE (TYP_DDH), INTENT (INOUT), OPTIONAL::YDDDH
#include "apl_arpege_parallel.intfb.h"
#include "mf_phys_prep_parallel.intfb.h"
#include "mf_phys_init_parallel.intfb.h"
CLASS (FIELD_3RB), POINTER :: YL_ZNHQ_TT0L
REAL (KIND=JPRB), POINTER::ZNHQ_TT0L (:,:,:)
CLASS (FIELD_3RB), POINTER :: YL_ZNHQ_TT9
REAL (KIND=JPRB), POINTER::ZNHQ_TT9 (:,:,:)
CLASS (FIELD_3RB), POINTER :: YL_ZNHQ_TT0
REAL (KIND=JPRB), POINTER::ZNHQ_TT0 (:,:,:)
CLASS (FIELD_3RB), POINTER :: YL_ZNHQ_TT0M
REAL (KIND=JPRB), POINTER::ZNHQ_TT0M (:,:,:)
CHARACTER (LEN=1024)::CL_APL_ALARO
LOGICAL::LL_APL_ALARO
TYPE (MF_PHYS_BASE_STATE_TYPE)::YLMF_PHYS_BASE_STATE
TYPE (MF_PHYS_NEXT_STATE_TYPE)::YLMF_PHYS_NEXT_STATE
REAL (KIND=JPHOOK)::ZHOOK_HANDLE_1
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
LOGICAL::LLGET
INTEGER (KIND=JPIM) :: JLON
INTEGER(KIND=JPIM) :: JBLK
TYPE(CPG_BNDS_TYPE) :: YLCPG_BNDS
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE_FIELD_API
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE_PARALLEL
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE_COMPUTE
TYPE(FXTRAN_ACDC_STACK) :: YLSTACK
TYPE(FXTRAN_ACDC_STACK) :: YLOFFSET
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_PARALLEL', 0, ZHOOK_HANDLE)

CALL FXTRAN_ACDC_GPUMEMSTAT (__FILE__, __LINE__, "BEGIN")


IF (LHOOK) CALL DR_HOOK ('CREATE_TEMPORARIES',0,ZHOOK_HANDLE_FIELD_API)
CALL FIELD_NEW (YL_ZNHQ_TT9, UBOUNDS=[YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, YDCPG_OPTS&
&%JBLKMAX], LBOUNDS=[1, 1, YDCPG_OPTS%JBLKMIN], PERSISTENT=.TRUE.)
CALL FIELD_NEW (YL_ZNHQ_TT0M, UBOUNDS=[YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, YDCPG_OPTS&
&%JBLKMAX], LBOUNDS=[1, 1, YDCPG_OPTS%JBLKMIN], PERSISTENT=.TRUE.)
CALL FIELD_NEW (YL_ZNHQ_TT0L, UBOUNDS=[YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, YDCPG_OPTS&
&%JBLKMAX], LBOUNDS=[1, 1, YDCPG_OPTS%JBLKMIN], PERSISTENT=.TRUE.)
CALL FIELD_NEW (YL_ZNHQ_TT0, UBOUNDS=[YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, YDCPG_OPTS&
&%JBLKMAX], LBOUNDS=[1, 1, YDCPG_OPTS%JBLKMIN], PERSISTENT=.TRUE.)
IF (LHOOK) CALL DR_HOOK ('CREATE_TEMPORARIES',1,ZHOOK_HANDLE_FIELD_API)
CALL GETENV ('APL_ALARO', CL_APL_ALARO)
LL_APL_ALARO=(CL_APL_ALARO/='').AND.(CL_APL_ALARO/='0')

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_PARALLEL:MF_PHYS_NEXT_STATE%INIT', 0, ZHOOK_HANDLE_1)
CALL YLMF_PHYS_NEXT_STATE%INIT (YDCPG_SL1, YDGEOMETRY, YDVARS, YDMODEL)
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_PARALLEL:MF_PHYS_NEXT_STATE%INIT', 1, ZHOOK_HANDLE_1)
LLGET=YDMODEL%YRML_DYN%YRDYN%NCURRENT_ITER>0


IF (.NOT.LLGET) THEN
  
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_PARALLEL:MF_PHYS_BASE_STATE%INIT', 0, ZHOOK_HANDLE_1)
  CALL YLMF_PHYS_BASE_STATE%INIT (YDCPG_DYN0, YDCPG_DYN9, YDCPG_PHY0,&
  & YDCPG_PHY9, YDVARS, YDMF_PHYS_SURF, PGFL=PGFL, YDMODEL=YDMODEL)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_PARALLEL:MF_PHYS_BASE_STATE%INIT', 1, ZHOOK_HANDLE_1)
  
  CALL MF_PHYS_PREP_PARALLEL (YDGEOMETRY, YDCPG_BNDS, YDCPG_OPTS, YDMODEL%YRML_PHY_MF%YRARPHY&
  &, YDMODEL%YRML_DYN%YRDYNA, YDCPG_DYN0, YDCPG_DYN9, YDCPG_PHY0, YDCPG_PHY9, YDVARS)
  CALL MF_PHYS_INIT_PARALLEL (YDCPG_BNDS, YDCPG_OPTS, YDMF_PHYS, YDCPG_MISC)

  IF (YDMODEL%YRML_DYN%YRDYNA%LNHQE.AND.YDMODEL%YRML_DYN%YRDYNA%LNHQE_SOLVER_QE) THEN
    
    
    CALL ABOR1 ("ERROR: WRONG SETTINGS")
    
  ENDIF


  IF (YDMODEL%YRML_PHY_MF%YRSIMPHL%LSIMPH) THEN
    
    
    CALL ABOR1 ("ERROR: WRONG SETTINGS")
    
  ELSEIF (YDMODEL%YRML_PHY_MF%YRARPHY%LMPA) THEN
    
    
    CALL ABOR1 ("ERROR: WRONG SETTINGS")
    
  ELSEIF (YDMODEL%YRML_PHY_MF%YRPHY%LAPL_ARPEGE) THEN
    CALL APL_ARPEGE_PARALLEL (YLMF_PHYS_BASE_STATE, YLMF_PHYS_NEXT_STATE, YDGEOMETRY&
    &, YDCPG_BNDS, YDCPG_OPTS, YDCPG_MISC, YDCPG_GPAR, YDCPG_PHY0, YDMF_PHYS, YDCPG_DYN0&
    &, YDMF_PHYS_SURF, YDCPG_SL2, YDVARS, YDCFU, YDMODEL, YDDDH, YDPHYSMWAVE)
  ELSEIF (LL_APL_ALARO) THEN
    
    
    CALL ABOR1 ("ERROR: WRONG SETTINGS")
    
  ELSE
    
    
    CALL ABOR1 ("ERROR: WRONG SETTINGS")
    
  ENDIF


  IF (YDMODEL%YRML_DYN%YRDYNA%LNHQE) THEN
    
    
    CALL ABOR1 ("ERROR: WRONG SETTINGS")
    
  ENDIF

ENDIF


IF (YDMODEL%YRML_DYN%YRDYNA%LPC_FULL.AND.(.NOT.YDMODEL%YRML_DYN&
  &%YRDYNA%LPC_CHEAP).AND.YDMODEL%YRML_DYN%YRDYNA%LSLAG) THEN
  
  
  CALL ABOR1 ("ERROR: WRONG SETTINGS")
  
ENDIF


CALL FXTRAN_ACDC_GPUMEMSTAT (__FILE__, __LINE__, "END")

IF (LHOOK) CALL DR_HOOK ('DELETE_TEMPORARIES',0,ZHOOK_HANDLE_FIELD_API)
IF (ASSOCIATED (YL_ZNHQ_TT0)) CALL FIELD_DELETE (YL_ZNHQ_TT0)
IF (ASSOCIATED (YL_ZNHQ_TT0L)) CALL FIELD_DELETE (YL_ZNHQ_TT0L)
IF (ASSOCIATED (YL_ZNHQ_TT0M)) CALL FIELD_DELETE (YL_ZNHQ_TT0M)
IF (ASSOCIATED (YL_ZNHQ_TT9)) CALL FIELD_DELETE (YL_ZNHQ_TT9)
IF (LHOOK) CALL DR_HOOK ('DELETE_TEMPORARIES',1,ZHOOK_HANDLE_FIELD_API)
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_PARALLEL', 1, ZHOOK_HANDLE)
END SUBROUTINE MF_PHYS_PARALLEL
