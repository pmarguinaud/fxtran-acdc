SUBROUTINE ACDC_SAVE_TDIMF (SELF, KLUN)

IMPLICIT NONE
TYPE (TDIMF), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LNGRBSP2, LNGRBSP3
WRITE (KLUN) SELF%NFTHER
WRITE (KLUN) SELF%NF3D
WRITE (KLUN) SELF%NFD2D
WRITE (KLUN) SELF%NFC2D
WRITE (KLUN) SELF%NS3D
WRITE (KLUN) SELF%NS2D
WRITE (KLUN) SELF%NS1D
LNGRBSP3 = ALLOCATED (SELF%NGRBSP3)
WRITE (KLUN) LNGRBSP3
IF (LNGRBSP3) THEN
  WRITE (KLUN) LBOUND (SELF%NGRBSP3)
  WRITE (KLUN) UBOUND (SELF%NGRBSP3)
  WRITE (KLUN) SELF%NGRBSP3
ENDIF
LNGRBSP2 = ALLOCATED (SELF%NGRBSP2)
WRITE (KLUN) LNGRBSP2
IF (LNGRBSP2) THEN
  WRITE (KLUN) LBOUND (SELF%NGRBSP2)
  WRITE (KLUN) UBOUND (SELF%NGRBSP2)
  WRITE (KLUN) SELF%NGRBSP2
ENDIF
WRITE (KLUN) SELF%LVOR
WRITE (KLUN) SELF%LADER
WRITE (KLUN) SELF%LUVDER
WRITE (KLUN) SELF%LSPT
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TDIMF (SELF, KLUN)

IMPLICIT NONE
TYPE (TDIMF), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LNGRBSP2, LNGRBSP3
READ (KLUN) SELF%NFTHER
READ (KLUN) SELF%NF3D
READ (KLUN) SELF%NFD2D
READ (KLUN) SELF%NFC2D
READ (KLUN) SELF%NS3D
READ (KLUN) SELF%NS2D
READ (KLUN) SELF%NS1D
READ (KLUN) LNGRBSP3
IF (LNGRBSP3) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NGRBSP3 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NGRBSP3
ENDIF
READ (KLUN) LNGRBSP2
IF (LNGRBSP2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NGRBSP2 (IL1(1):IU1(1)))
  READ (KLUN) SELF%NGRBSP2
ENDIF
READ (KLUN) SELF%LVOR
READ (KLUN) SELF%LADER
READ (KLUN) SELF%LUVDER
READ (KLUN) SELF%LSPT
END SUBROUTINE

SUBROUTINE ACDC_COPY_TDIMF (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TDIMF), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LNGRBSP2, LNGRBSP3
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
