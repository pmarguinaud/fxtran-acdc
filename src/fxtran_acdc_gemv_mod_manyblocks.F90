IMPLICIT NONE

PRIVATE

PUBLIC :: FXTRAN_ACDC_GEMV_MANYBLOCKS

CONTAINS

SUBROUTINE FXTRAN_ACDC_GEMV_MANYBLOCKS (KIDIA, KFDIA, TRANSA, TRANSB, M, N, K, ALPHA, A, &
                                       & LDA, B, LDB, BETA, C, LDC, LDDONE, LDACC,KGPBLKS,YDOFFSET)

USE FXTRAN_ACDC_BLAS_MOD
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

                                ! VERINTS
INTEGER     :: KIDIA
INTEGER     :: KFDIA
CHARACTER*1 :: TRANSA           ! 'N'
CHARACTER*1 :: TRANSB           ! 'T'
INTEGER     :: M                ! KPROMA
INTEGER     :: N                ! 1
INTEGER     :: K                ! KLEVIN
REAL*8      :: ALPHA            ! 1.0_JPRD
REAL*8      :: A (:, :,:)         ! ZIN
INTEGER     :: LDA              ! KPROMA
REAL*8      :: B (:, :)         ! PINTE
INTEGER     :: LDB              ! KLEVOUT
REAL*8      :: BETA             ! 0.0_JPRB
REAL*8      :: C (:,:)            ! POUTS
INTEGER     :: LDC              ! KPROMA
LOGICAL     :: LDDONE
LOGICAL     :: LDACC
INTEGER                  :: KGPBLKS
TYPE (FXTRAN_ACDC_STACK), OPTIONAL :: YDOFFSET

INTEGER :: JM, JN, JK, JBLK
CHARACTER (LEN=*), PARAMETER :: CLNAME = 'FXTRAN_ACDC_GEMV'

IF (TRANSA /= 'N') CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (TRANSB /= 'T') CALL FXTRAN_ACDC_ABORT (CLNAME)

CALL FXTRAN_ACDC_CHECKCONTIGUOUS (A(:,:,1), CLNAME)
CALL FXTRAN_ACDC_CHECKCONTIGUOUS (B, CLNAME)
CALL FXTRAN_ACDC_CHECKCONTIGUOUS (C(:,1), CLNAME)

IF (LDA /= SIZE (A, 1)) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (LDB /= SIZE (B, 1)) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (LDC /= SIZE (C, 1)) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (KIDIA /= 1) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (ALPHA /= 1._8) CALL FXTRAN_ACDC_ABORT (CLNAME)
IF (BETA /= 0._8) CALL FXTRAN_ACDC_ABORT (CLNAME)

IF (FXTRAN_ACDC_USE_SIMPLE_DGEMM ()) THEN

#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC DATA PRESENT (A, B, C) IF (LDACC)
!$ACC PARALLEL LOOP GANG VECTOR PRIVATE (JBLK, JM, JK) VECTOR_LENGTH (LDA) IF (LDACC) 
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TOFROM: A (1:LDA, 1:K, 1:KGPBLKS), B (1:LDB, 1:K), C (1:LDC, 1:KGPBLKS)) IF (LDACC)
!$OMP TARGET TEAMS DISTRIBUTE PRIVATE (JBLK, JM, JK) THREAD_LIMIT (LDA) IF (LDACC)
#endif
  DO JBLK=1,KGPBLKS
    DO JM = KIDIA, MERGE (M, KFDIA, JBLK < KGPBLKS)
      C (JM,JBLK) = 0.
      DO JK = 1, K
        C (JM,JBLK) = C (JM,JBLK) + B (LDB , JK) * A (JM, JK, JBLK)
      ENDDO
    ENDDO
  ENDDO
#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

ELSE

  IF (LDACC) THEN

#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC DATA PRESENT (A, B, C)
!$ACC HOST_DATA USE_DEVICE (A, B, C)
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TOFROM: A (1:LDA, 1:K, 1:KGPBLKS), B (1:LDB, 1:K), C (1:LDC, 1:KGPBLKS))
!$OMP TARGET DATA USE_DEVICE_ADDR (A, B, C)
#endif

     CALL FXTRAN_ACDC_CHECK_BLAS (&
      & FXTRAN_ACDC_DGEMM_STRIDED_BATCHED (FXTRAN_ACDC_BLAS_GET_HANDLE (), FXTRAN_ACDC_OP_N, FXTRAN_ACDC_OP_T, M, 1, K, &
      &                        ALPHA, A (1, 1, 1), LDA, FXTRAN_ACDC_STRIDE (A), &
      &                               B (LDB, 1),  LDB, FXTRAN_ACDC_STRIDE (0), &
      &                        BETA,  C (1, 1),    LDC, FXTRAN_ACDC_STRIDE (C), &
      &                               KGPBLKS))

#ifdef _FXTRAN_ACDC_USE_OPENACC
!$ACC END HOST_DATA
!$ACC END DATA
#endif
#ifdef _FXTRAN_ACDC_USE_OPENMPTARGET
!$OMP END TARGET DATA 
!$OMP END TARGET DATA
#endif

    CALL FXTRAN_ACDC_CHECK_BLAS_STREAM

  ELSE
    DO JBLK=1,KGPBLKS
      CALL DGEMM ('N','T', M, N, K, ALPHA, A (1, 1, JBLK), LDA, B (LDB, 1), LDB, BETA, C (1, JBLK), LDC)
    ENDDO
  ENDIF

ENDIF

LDDONE = .TRUE.

END SUBROUTINE

END MODULE
