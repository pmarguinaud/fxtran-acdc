#ifdef _FXTRAN_USE_CUBLAS
USE CUBLAS_V2
USE CUDAFOR
#endif

IMPLICIT NONE

PRIVATE

#ifdef _FXTRAN_USE_CUBLAS
PUBLIC :: CHECKCUBLAS, CHECKCUSTREAM, GETCUHANDLE
PUBLIC :: CUBLASDGEMMSTRIDEDBATCHED_V2, CUBLASDGEMM_V2
PUBLIC :: CUBLAS_OP_N, CUBLAS_OP_T
INTEGER (KIND=CUDA_STREAM_KIND), SAVE :: STREAM = 0
#endif

PUBLIC :: FXTRAN_ACDC_USE_SIMPLE_DGEMM

PUBLIC :: FXTRAN_ACDC_CHECKCONTIGUOUS, FXTRAN_ACDC_STRIDE

INTERFACE FXTRAN_ACDC_CHECKCONTIGUOUS
  MODULE PROCEDURE CHECKCONTIGUOUS1
  MODULE PROCEDURE CHECKCONTIGUOUS2
END INTERFACE

INTERFACE FXTRAN_ACDC_STRIDE
  MODULE PROCEDURE STRIDE2
  MODULE PROCEDURE STRIDE3
END INTERFACE

#ifdef _FXTRAN_USE_CUBLAS
INTEGER, PARAMETER :: JPSTRIDE = 4
#endif

CONTAINS

LOGICAL FUNCTION FXTRAN_ACDC_USE_SIMPLE_DGEMM ()

CHARACTER*1, SAVE :: CLENV = ''
LOGICAL, SAVE :: LLSIMPLE_DGEMM = .FALSE.

IF (CLENV == '') THEN
  CALL GETENV ('FXTRAN_ACDC_SIMPLE_DGEMM', CLENV)
  LLSIMPLE_DGEMM = CLENV (1:1) == '1' 
  CLENV = '0'
ENDIF

END FUNCTION

#ifdef _FXTRAN_USE_CUBLAS
TYPE (CUBLASHANDLE) FUNCTION GETCUHANDLE ()

USE FXTRAN_ACDC_ABORT_MOD

TYPE (CUBLASHANDLE), SAVE :: YLCBH
LOGICAL, SAVE :: LLCBH_INITIALIZED = .FALSE.

IF (.NOT. LLCBH_INITIALIZED) THEN
  CALL CHECKCUBLAS (CUBLASCREATE (YLCBH))
  CALL CHECKCUBLAS (CUBLASGETSTREAM (YLCBH, STREAM))
  LLCBH_INITIALIZED = .TRUE.
ENDIF

GETCUHANDLE = YLCBH

END FUNCTION

SUBROUTINE CHECKCUBLAS (STATUS)

USE FXTRAN_ACDC_ABORT_MOD

INTEGER :: STATUS

IF (STATUS /= CUBLAS_STATUS_SUCCESS) THEN
  PRINT *, 'CUBLAS ERROR: STATUS =', STATUS
  CALL FXTRAN_ACDC_ABORT ('CHECKCUBLAS')
END IF

END SUBROUTINE CHECKCUBLAS

SUBROUTINE CHECKCUDA (STATUS)

USE FXTRAN_ACDC_ABORT_MOD

INTEGER :: STATUS

IF (STATUS /= CUDASUCCESS) THEN
  PRINT *, 'CUDA ERROR:', CUDAGETERRORSTRING (STATUS)
  CALL FXTRAN_ACDC_ABORT ('CHECKCUDA')
END IF

END SUBROUTINE CHECKCUDA

SUBROUTINE CHECKCUSTREAM

CALL CHECKCUDA (CUDASTREAMSYNCHRONIZE (STREAM))

END SUBROUTINE

#endif

SUBROUTINE CHECKCONTIGUOUS1 (P, CDMESS)

USE FXTRAN_ACDC_ABORT_MOD

REAL*8 :: P (:)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (2)) - LOC (P (1)) /= 8) THEN
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

SUBROUTINE CHECKCONTIGUOUS2 (P, CDMESS)

USE FXTRAN_ACDC_ABORT_MOD

REAL*8 :: P (:, :)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (1, 2)) - LOC (P (1, 1)) /= SIZE (P, 1) * 8) THEN  
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE3 (P)
REAL*8 :: P (:,:,:)
STRIDE3 = (LOC (P (1, 1, 2)) - LOC (P (1, 1, 1))) / 8
END FUNCTION

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE2 (P)
REAL*8 :: P (:,:)
STRIDE2 = (LOC (P (1, 2)) - LOC (P (1, 1))) / 8
END FUNCTION

END MODULE
