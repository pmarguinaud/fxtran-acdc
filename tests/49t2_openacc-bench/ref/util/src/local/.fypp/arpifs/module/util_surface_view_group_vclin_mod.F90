SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_VCLIN (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIN), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ACCPR, LF_ACCPR5, LF_ACPR, LF_BAS, LF_TOP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_TOP = ASSOCIATED (SELF%F_TOP)
IF (LF_TOP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_TOP)
  !$ACC ENTER DATA ATTACH (SELF%F_TOP)
ENDIF

LF_BAS = ASSOCIATED (SELF%F_BAS)
IF (LF_BAS .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_BAS)
  !$ACC ENTER DATA ATTACH (SELF%F_BAS)
ENDIF

LF_ACPR = ASSOCIATED (SELF%F_ACPR)
IF (LF_ACPR .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_ACPR)
  !$ACC ENTER DATA ATTACH (SELF%F_ACPR)
ENDIF

LF_ACCPR = ASSOCIATED (SELF%F_ACCPR)
IF (LF_ACCPR .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_ACCPR)
  !$ACC ENTER DATA ATTACH (SELF%F_ACCPR)
ENDIF

LF_ACCPR5 = ASSOCIATED (SELF%F_ACCPR5)
IF (LF_ACCPR5 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_ACCPR5)
  !$ACC ENTER DATA ATTACH (SELF%F_ACCPR5)
ENDIF

END SUBROUTINE

