  ALLOCATE (ZTMP3(LBOUND(SELF%RMACBC,1):UBOUND(SELF%RMACBC,1), LBOUND(SELF%RMACBC,2):UBOUND(SELF%RMACBC,2), LBOUND(SELF%RMACBC,3):UBOUND(SELF%RMACBC,3)))
