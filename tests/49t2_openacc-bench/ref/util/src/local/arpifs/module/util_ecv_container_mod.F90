SUBROUTINE ACDC_LOAD_ECV_CONTAINER (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (ECV_CONTAINER), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2), IL3(3), IU3(3), IL4(4), IU4(4)
LOGICAL :: LRECV1D, LRGPECV2D, LRGPECV3D, LRLSM, LRSPECV2D, LRSPECV3D
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) LRECV1D
IF (LRECV1D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RECV1D (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RECV1D,1):UBOUND(SELF%RECV1D,1)))
  READ (KLUN) ZTMP1
  SELF%RECV1D = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRSPECV2D
IF (LRSPECV2D) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RSPECV2D (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RSPECV2D,1):UBOUND(SELF%RSPECV2D,1), LBOUND(SELF%RSPECV2D,2):UBOUND(SELF%RSPECV2D,2)))
  READ (KLUN) ZTMP2
  SELF%RSPECV2D = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LRGPECV2D
IF (LRGPECV2D) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%RGPECV2D (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%RGPECV2D,1):UBOUND(SELF%RGPECV2D,1), LBOUND(SELF%RGPECV2D,2):UBOUND(SELF%RGPECV2D,2), LBOUND(SELF%RGPECV2D,3):UBOUND(SELF%RGPECV2D,3)))
  READ (KLUN) ZTMP3
  SELF%RGPECV2D = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LRSPECV3D
IF (LRSPECV3D) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%RSPECV3D (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%RSPECV3D,1):UBOUND(SELF%RSPECV3D,1), LBOUND(SELF%RSPECV3D,2):UBOUND(SELF%RSPECV3D,2), LBOUND(SELF%RSPECV3D,3):UBOUND(SELF%RSPECV3D,3)))
  READ (KLUN) ZTMP3
  SELF%RSPECV3D = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LRGPECV3D
IF (LRGPECV3D) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%RGPECV3D (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%RGPECV3D,1):UBOUND(SELF%RGPECV3D,1), LBOUND(SELF%RGPECV3D,2):UBOUND(SELF%RGPECV3D,2), LBOUND(SELF%RGPECV3D,3):UBOUND(SELF%RGPECV3D,3), LBOUND(SELF%RGPECV3D,4):UBOUND(SELF%RGPECV3D,4)))
  READ (KLUN) ZTMP4
  SELF%RGPECV3D = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LRLSM
IF (LRLSM) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%RLSM (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%RLSM,1):UBOUND(SELF%RLSM,1), LBOUND(SELF%RLSM,2):UBOUND(SELF%RLSM,2)))
  READ (KLUN) ZTMP2
  SELF%RLSM = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE

