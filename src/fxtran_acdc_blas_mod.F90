#ifdef _FXTRAN_USE_CUBLAS
USE CUDAFOR
USE CUBLAS_V2
USE CUBLAS_V2, ONLY : CUBLASDGEMMSTRIDEDBATCHED_V2, &
                    & CUBLASDGEMM_V2,               &
                    & CUBLAS_OP_N,                  &
                    & CUBLAS_OP_T
USE CUBLAS_V2, ONLY : FXTRAN_ACDC_DGEMM_STRIDED_BATCHED => CUBLASDGEMMSTRIDEDBATCHED_V2, &
                    & FXTRAN_ACDC_DGEMM                 => CUBLASDGEMM_V2,               &
                    & FXTRAN_ACDC_OP_N                  => CUBLAS_OP_N,                  &
                    & FXTRAN_ACDC_OP_T                  => CUBLAS_OP_T
#endif

#ifdef _FXTRAN_USE_ROCBLAS
USE HIPFORT
USE HIPFORT_ROCBLAS
USE HIPFORT_ROCBLAS, ONLY : ROCBLAS_DGEMM_STRIDED_BATCHED, &
                          & ROCBLAS_DGEMM,                 &
                          & ROCBLAS_OPERATION_NONE,        &
                          & ROCBLAS_OPERATION_TRANSPOSE
USE HIPFORT_ROCBLAS, ONLY : FXTRAN_ACDC_DGEMM_STRIDED_BATCHED => ROCBLAS_DGEMM_STRIDED_BATCHED, &
                          & FXTRAN_ACDC_DGEMM                 => ROCBLAS_DGEMM,                 &
                          & FXTRAN_ACDC_OP_N                  => ROCBLAS_OPERATION_NONE,        &
                          & FXTRAN_ACDC_OP_T                  => ROCBLAS_OPERATION_TRANSPOSE
USE ISO_C_BINDING
#endif

USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE

PRIVATE

#ifdef _FXTRAN_USE_CUBLAS
PUBLIC :: CHECKCUBLAS, GETCUBLASHANDLE
PUBLIC :: CUBLASDGEMMSTRIDEDBATCHED_V2, CUBLASDGEMM_V2
PUBLIC :: CUBLAS_OP_N, CUBLAS_OP_T
INTEGER (KIND=CUDA_STREAM_KIND), SAVE :: STREAM = 0
INTEGER, PARAMETER :: JPSTRIDE = 4
#endif

#ifdef _FXTRAN_USE_ROCBLAS
PUBLIC :: CHECKROCBLAS, GETROCBLASHANDLE
PUBLIC :: ROCBLAS_DGEMM_STRIDED_BATCHED, ROCBLAS_DGEMM
PUBLIC :: ROCBLAS_OPERATION_NONE, ROCBLAS_OPERATION_TRANSPOSE
TYPE (C_PTR), SAVE :: STREAM = C_NULL_PTR
INTEGER, PARAMETER :: JPSTRIDE = C_INT64_T
PUBLIC :: FXTRAN_ACDC_DGEMM_STRIDED_BATCHED
PUBLIC :: FXTRAN_ACDC_DGEMM
PUBLIC :: FXTRAN_ACDC_OP_N
PUBLIC :: FXTRAN_ACDC_OP_T
#endif

PUBLIC :: FXTRAN_ACDC_CHECK_BLAS_STREAM

PUBLIC :: FXTRAN_ACDC_USE_SIMPLE_DGEMM

PUBLIC :: FXTRAN_ACDC_CHECKCONTIGUOUS, FXTRAN_ACDC_STRIDE

INTERFACE FXTRAN_ACDC_CHECKCONTIGUOUS
  MODULE PROCEDURE CHECKCONTIGUOUS1
  MODULE PROCEDURE CHECKCONTIGUOUS2
END INTERFACE

INTERFACE FXTRAN_ACDC_STRIDE
  MODULE PROCEDURE STRIDE0
  MODULE PROCEDURE STRIDE2
  MODULE PROCEDURE STRIDE3
END INTERFACE

PUBLIC :: FXTRAN_ACDC_BLAS_GET_HANDLE

CONTAINS

LOGICAL FUNCTION FXTRAN_ACDC_USE_SIMPLE_DGEMM ()

CHARACTER*1, SAVE :: CLENV = ''
LOGICAL, SAVE :: LLSIMPLE_DGEMM = .FALSE.

IF (CLENV == '') THEN
  CALL GETENV ('FXTRAN_ACDC_SIMPLE_DGEMM', CLENV)
  LLSIMPLE_DGEMM = CLENV (1:1) == '1' 
  CLENV = '0'
ENDIF

FXTRAN_ACDC_USE_SIMPLE_DGEMM = LLSIMPLE_DGEMM

END FUNCTION

#ifdef _FXTRAN_USE_CUBLAS
TYPE (CUBLASHANDLE) FUNCTION GETCUBLASHANDLE ()

TYPE (CUBLASHANDLE), SAVE :: YLCBH
LOGICAL, SAVE :: LLCBH_INITIALIZED = .FALSE.

IF (.NOT. LLCBH_INITIALIZED) THEN
  CALL CHECKCUBLAS (CUBLASCREATE (YLCBH))
  CALL CHECKCUBLAS (CUBLASGETSTREAM (YLCBH, STREAM))
  LLCBH_INITIALIZED = .TRUE.
ENDIF

GETCUBLASHANDLE = YLCBH

END FUNCTION

SUBROUTINE CHECKCUBLAS (STATUS)

INTEGER :: STATUS

IF (STATUS /= CUBLAS_STATUS_SUCCESS) THEN
  PRINT *, 'CUBLAS ERROR: STATUS =', STATUS
  CALL FXTRAN_ACDC_ABORT ('CHECKCUBLAS')
END IF

END SUBROUTINE CHECKCUBLAS

SUBROUTINE CHECKCUDA (STATUS)

INTEGER :: STATUS

IF (STATUS /= CUDASUCCESS) THEN
  PRINT *, 'CUDA ERROR:', CUDAGETERRORSTRING (STATUS)
  CALL FXTRAN_ACDC_ABORT ('CHECKCUDA')
END IF

END SUBROUTINE CHECKCUDA

SUBROUTINE FXTRAN_ACDC_CHECK_BLAS_STREAM
CALL CHECKCUDA (CUDASTREAMSYNCHRONIZE (STREAM))
END SUBROUTINE
#endif

#ifdef _FXTRAN_USE_ROCBLAS
TYPE (C_PTR) FUNCTION FXTRAN_ACDC_BLAS_GET_HANDLE ()

TYPE (C_PTR), SAVE :: YLCBH
LOGICAL, SAVE :: LLCBH_INITIALIZED = .FALSE.

IF (.NOT. LLCBH_INITIALIZED) THEN
  CALL CHECKROCBLAS (ROCBLAS_CREATE_HANDLE (YLCBH))
  CALL CHECKROCBLAS (ROCBLAS_GET_STREAM (YLCBH, STREAM))
  LLCBH_INITIALIZED = .TRUE.
ENDIF

FXTRAN_ACDC_BLAS_GET_HANDLE = YLCBH

END FUNCTION

TYPE (C_PTR) FUNCTION GETROCBLASHANDLE ()

TYPE (C_PTR), SAVE :: YLCBH
LOGICAL, SAVE :: LLCBH_INITIALIZED = .FALSE.

IF (.NOT. LLCBH_INITIALIZED) THEN
  CALL CHECKROCBLAS (ROCBLAS_CREATE_HANDLE (YLCBH))
  CALL CHECKROCBLAS (ROCBLAS_GET_STREAM (YLCBH, STREAM))
  LLCBH_INITIALIZED = .TRUE.
ENDIF

GETROCBLASHANDLE = YLCBH

END FUNCTION

SUBROUTINE CHECKROCBLAS (STATUS)

INTEGER :: STATUS

IF (STATUS /= ROCBLAS_STATUS_SUCCESS) THEN
  PRINT *, 'ROCBLAS ERROR: STATUS =', STATUS
  CALL FXTRAN_ACDC_ABORT ('CHECKROCBLAS')
END IF

END SUBROUTINE CHECKROCBLAS

SUBROUTINE CHECKHIP (STATUS)

INTEGER :: STATUS

IF (STATUS /= HIPSUCCESS) THEN
  PRINT *, 'HIP ERROR:', STATUS
  CALL FXTRAN_ACDC_ABORT ('CHECKHIP')
END IF

END SUBROUTINE CHECKHIP

SUBROUTINE FXTRAN_ACDC_CHECK_BLAS_STREAM
CALL CHECKHIP (HIPSTREAMSYNCHRONIZE (STREAM))
END SUBROUTINE
#endif

SUBROUTINE CHECKCONTIGUOUS1 (P, CDMESS)

REAL*8 :: P (:)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (2)) - LOC (P (1)) /= 8) THEN
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

SUBROUTINE CHECKCONTIGUOUS2 (P, CDMESS)

REAL*8 :: P (:, :)
CHARACTER (LEN=*) :: CDMESS

IF (LOC (P (1, 2)) - LOC (P (1, 1)) /= SIZE (P, 1) * 8) THEN  
  CALL FXTRAN_ACDC_ABORT (CDMESS)
ENDIF

END SUBROUTINE

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE0 (K)
INTEGER :: K 
STRIDE0 = K
END FUNCTION

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE3 (P)
REAL*8 :: P (:,:,:)
STRIDE3 = (LOC (P (1, 1, 2)) - LOC (P (1, 1, 1))) / 8
END FUNCTION

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE2 (P)
REAL*8 :: P (:,:)
STRIDE2 = (LOC (P (1, 2)) - LOC (P (1, 1))) / 8
END FUNCTION

END MODULE
