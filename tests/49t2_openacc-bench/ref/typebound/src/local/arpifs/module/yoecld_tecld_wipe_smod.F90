SUBMODULE (YOECLD) YOECLD_TECLD_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TECLD (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TECLD), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LCETA
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LCETA = ALLOCATED (SELF%CETA)
IF (LCETA) THEN
  !$ACC EXIT DATA DETACH (SELF%CETA)
  !$ACC EXIT DATA DELETE (SELF%CETA)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
