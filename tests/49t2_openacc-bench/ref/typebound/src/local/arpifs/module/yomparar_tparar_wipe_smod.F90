SUBMODULE (YOMPARAR) YOMPARAR_TPARAR_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TPARAR (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TPARAR), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LXSW_BANDS
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

CALL SELF%PHYEX%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LXSW_BANDS = ALLOCATED (SELF%XSW_BANDS)
IF (LXSW_BANDS) THEN
  !$ACC EXIT DATA DETACH (SELF%XSW_BANDS)
  !$ACC EXIT DATA DELETE (SELF%XSW_BANDS)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
