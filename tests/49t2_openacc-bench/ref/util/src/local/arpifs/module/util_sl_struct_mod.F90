INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LDIST1GP, LLCOMPLAT, LLSLCOMM, LLSLCORE, LNADDR, LNLATGLO, LNLOENG, LNOFFLEV, LNSLCOMM, LNSLCORE
