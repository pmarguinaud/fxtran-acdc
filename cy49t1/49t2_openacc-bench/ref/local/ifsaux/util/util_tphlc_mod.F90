  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LVDFDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSDRDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSDRDS'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

