    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYDF => GET_HOST_DATA_RDONLY&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYDF)
