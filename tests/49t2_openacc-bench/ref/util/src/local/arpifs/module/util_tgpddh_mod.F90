SUBROUTINE ACDC_LOAD_TGPDDH (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TGPDDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL4(4), IU4(4)
LOGICAL :: LGFLTNDHD_DDH, LGFLTNDSL_DDH, LGMVTNDHD_DDH, LGMVTNDSI_DDH, LGMVTNDSL_DDH
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) LGMVTNDHD_DDH
IF (LGMVTNDHD_DDH) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%GMVTNDHD_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%GMVTNDHD_DDH,1):UBOUND(SELF%GMVTNDHD_DDH,1), LBOUND(SELF%GMVTNDHD_DDH,2):UBOUND(SELF%GMVTNDHD_DDH,2), LBOUND(SELF%GMVTNDHD_DDH,3):UBOUND(SELF%GMVTNDHD_DDH,3), LBOUND(SELF%GMVTNDHD_DDH,4):UBOUND(SELF%GMVTNDHD_DDH,4)))
  READ (KLUN) ZTMP4
  SELF%GMVTNDHD_DDH = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LGFLTNDHD_DDH
IF (LGFLTNDHD_DDH) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%GFLTNDHD_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%GFLTNDHD_DDH,1):UBOUND(SELF%GFLTNDHD_DDH,1), LBOUND(SELF%GFLTNDHD_DDH,2):UBOUND(SELF%GFLTNDHD_DDH,2), LBOUND(SELF%GFLTNDHD_DDH,3):UBOUND(SELF%GFLTNDHD_DDH,3), LBOUND(SELF%GFLTNDHD_DDH,4):UBOUND(SELF%GFLTNDHD_DDH,4)))
  READ (KLUN) ZTMP4
  SELF%GFLTNDHD_DDH = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LGMVTNDSI_DDH
IF (LGMVTNDSI_DDH) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%GMVTNDSI_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%GMVTNDSI_DDH,1):UBOUND(SELF%GMVTNDSI_DDH,1), LBOUND(SELF%GMVTNDSI_DDH,2):UBOUND(SELF%GMVTNDSI_DDH,2), LBOUND(SELF%GMVTNDSI_DDH,3):UBOUND(SELF%GMVTNDSI_DDH,3), LBOUND(SELF%GMVTNDSI_DDH,4):UBOUND(SELF%GMVTNDSI_DDH,4)))
  READ (KLUN) ZTMP4
  SELF%GMVTNDSI_DDH = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LGMVTNDSL_DDH
IF (LGMVTNDSL_DDH) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%GMVTNDSL_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%GMVTNDSL_DDH,1):UBOUND(SELF%GMVTNDSL_DDH,1), LBOUND(SELF%GMVTNDSL_DDH,2):UBOUND(SELF%GMVTNDSL_DDH,2), LBOUND(SELF%GMVTNDSL_DDH,3):UBOUND(SELF%GMVTNDSL_DDH,3), LBOUND(SELF%GMVTNDSL_DDH,4):UBOUND(SELF%GMVTNDSL_DDH,4)))
  READ (KLUN) ZTMP4
  SELF%GMVTNDSL_DDH = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LGFLTNDSL_DDH
IF (LGFLTNDSL_DDH) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%GFLTNDSL_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%GFLTNDSL_DDH,1):UBOUND(SELF%GFLTNDSL_DDH,1), LBOUND(SELF%GFLTNDSL_DDH,2):UBOUND(SELF%GFLTNDSL_DDH,2), LBOUND(SELF%GFLTNDSL_DDH,3):UBOUND(SELF%GFLTNDSL_DDH,3), LBOUND(SELF%GFLTNDSL_DDH,4):UBOUND(SELF%GFLTNDSL_DDH,4)))
  READ (KLUN) ZTMP4
  SELF%GFLTNDSL_DDH = ZTMP4
  DEALLOCATE (ZTMP4)
