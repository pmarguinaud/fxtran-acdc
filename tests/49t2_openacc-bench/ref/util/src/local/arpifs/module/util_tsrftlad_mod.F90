SUBROUTINE ACDC_SAVE_TSRFTLAD (SELF, KLUN)

IMPLICIT NONE
TYPE (TSRFTLAD), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LGPTSKIN0, LGPTSKIN9
LGPTSKIN0 = ALLOCATED (SELF%GPTSKIN0)
WRITE (KLUN) LGPTSKIN0
IF (LGPTSKIN0) THEN
  WRITE (KLUN) LBOUND (SELF%GPTSKIN0)
  WRITE (KLUN) UBOUND (SELF%GPTSKIN0)
  WRITE (KLUN) SELF%GPTSKIN0
ENDIF
LGPTSKIN9 = ALLOCATED (SELF%GPTSKIN9)
WRITE (KLUN) LGPTSKIN9
IF (LGPTSKIN9) THEN
  WRITE (KLUN) LBOUND (SELF%GPTSKIN9)
  WRITE (KLUN) UBOUND (SELF%GPTSKIN9)
  WRITE (KLUN) SELF%GPTSKIN9
ENDIF
WRITE (KLUN) SELF%NGSKIN
WRITE (KLUN) SELF%LREGSF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TSRFTLAD (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TSRFTLAD), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL3(3), IU3(3)
LOGICAL :: LGPTSKIN0, LGPTSKIN9
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LGPTSKIN0
IF (LGPTSKIN0) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%GPTSKIN0 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%GPTSKIN0,1):UBOUND(SELF%GPTSKIN0,1), LBOUND(SELF%GPTSKIN0,2):UBOUND(SELF%GPTSKIN0,2), LBOUND(SELF%GPTSKIN0,3):UBOUND(SELF%GPTSKIN0,3)))
  READ (KLUN) ZTMP3
  SELF%GPTSKIN0 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LGPTSKIN9
IF (LGPTSKIN9) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%GPTSKIN9 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%GPTSKIN9,1):UBOUND(SELF%GPTSKIN9,1), LBOUND(SELF%GPTSKIN9,2):UBOUND(SELF%GPTSKIN9,2), LBOUND(SELF%GPTSKIN9,3):UBOUND(SELF%GPTSKIN9,3)))
  READ (KLUN) ZTMP3
  SELF%GPTSKIN9 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) SELF%NGSKIN
READ (KLUN) SELF%LREGSF
END SUBROUTINE

