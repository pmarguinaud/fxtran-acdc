SUBMODULE (MODD_NSV) MODD_NSV_NSV_T_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_NSV_T (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (NSV_T), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1, J2
LOGICAL :: LCSV, LCSV_A, LCSV_CHEM_LIST, LCSV_CHEM_LIST_A, LTSVLIST, LTSVLIST_A
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LCSV_CHEM_LIST_A = ALLOCATED (SELF%CSV_CHEM_LIST_A)
IF (LCSV_CHEM_LIST_A) THEN
  !$ACC EXIT DATA DETACH (SELF%CSV_CHEM_LIST_A)
  !$ACC EXIT DATA DELETE (SELF%CSV_CHEM_LIST_A)
ENDIF

LCSV_A = ALLOCATED (SELF%CSV_A)
IF (LCSV_A) THEN
  !$ACC EXIT DATA DETACH (SELF%CSV_A)
  !$ACC EXIT DATA DELETE (SELF%CSV_A)
ENDIF

LTSVLIST_A = ALLOCATED (SELF%TSVLIST_A)
IF (LTSVLIST_A) THEN
  !$ACC EXIT DATA DETACH (SELF%TSVLIST_A)
  DO J2 = LBOUND (SELF%TSVLIST_A, 2), UBOUND (SELF%TSVLIST_A, 2)
    DO J1 = LBOUND (SELF%TSVLIST_A, 1), UBOUND (SELF%TSVLIST_A, 1)
      CALL SELF%TSVLIST_A (J1, J2)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
    ENDDO
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%TSVLIST_A)
ENDIF

LCSV_CHEM_LIST = ASSOCIATED (SELF%CSV_CHEM_LIST)
IF (LCSV_CHEM_LIST) THEN
  !$ACC EXIT DATA DETACH (SELF%CSV_CHEM_LIST)
  !$ACC EXIT DATA DELETE (SELF%CSV_CHEM_LIST)
ENDIF

LCSV = ASSOCIATED (SELF%CSV)
IF (LCSV) THEN
  !$ACC EXIT DATA DETACH (SELF%CSV)
  !$ACC EXIT DATA DELETE (SELF%CSV)
ENDIF

LTSVLIST = ASSOCIATED (SELF%TSVLIST)
IF (LTSVLIST) THEN
  !$ACC EXIT DATA DETACH (SELF%TSVLIST)
  DO J1 = LBOUND (SELF%TSVLIST, 1), UBOUND (SELF%TSVLIST, 1)
    CALL SELF%TSVLIST (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%TSVLIST)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
