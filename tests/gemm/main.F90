PROGRAM GEMM

USE FXTRAN_ACDC_GEMM_MOD           , ONLY : FXTRAN_ACDC_GEMM
USE FXTRAN_ACDC_GEMM_MOD_OPENACC   , ONLY : FXTRAN_ACDC_GEMM_OPENACC
USE FXTRAN_ACDC_STACK_MOD          , ONLY : FXTRAN_ACDC_STACK
USE FXTRAN_ACDC_GEMM_MOD_MANYBLOCKS, ONLY : FXTRAN_ACDC_GEMM_MANYBLOCKS

IMPLICIT NONE

INTEGER, PARAMETER :: JPIM = 4, JPRB = 8

INTEGER (KIND=JPIM) :: NPROMA, ILEVIN, ILEVOUT, NGPBLKS, IST, IEND

REAL (KIND=JPRB), ALLOCATABLE :: ZIN (:,:,:), ZOUT (:,:,:), PINTE (:,:), ZREF (:,:,:), ZRMS (:), ZAVG (:)
INTEGER (KIND=JPIM), ALLOCATABLE :: ICNT (:)

LOGICAL :: LLDONE, LLACC
INTEGER (KIND=JPIM) :: JLON, JLEV, JBLK, IFDIA
TYPE (FXTRAN_ACDC_STACK) :: YLSTACK


OPEN (77, FILE='VERINT.T30L15.DAT', FORM='UNFORMATTED', STATUS='OLD')

READ (77) NPROMA, ILEVIN, ILEVOUT, NGPBLKS, IST, IEND

ALLOCATE (ZOUT (NPROMA, ILEVOUT-1, NGPBLKS),  &
        & ZREF (NPROMA, ILEVOUT-1, NGPBLKS), &
        & ZIN (NPROMA, ILEVIN, NGPBLKS), &
        & PINTE (ILEVOUT,ILEVIN), &
        & ZRMS (ILEVOUT-1), ZAVG (ILEVOUT-1), ICNT (ILEVOUT-1))
READ (77) ZIN
READ (77) ZREF
READ (77) PINTE
CLOSE (77)


#ifdef UNDEF
!$OMP PARALLEL DO PRIVATE (IFDIA)
DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
  CALL FXTRAN_ACDC_GEMM (1, IFDIA, 'N', 'T', NPROMA, ILEVOUT-1, ILEVIN, 1.0_JPRB, ZIN (:,:,JBLK), &
                       & ILEVOUT, PINTE, ILEVOUT, 0.0_JPRB, ZOUT (:,:,JBLK), NPROMA, LLDONE)
ENDDO
!$OMP END PARALLEL DO
#endif


#ifdef UNDEF
!$ACC DATA COPYIN (ZIN, PINTE) COPYOUT (ZOUT)
!$ACC PARALLEL LOOP GANG PRIVATE (JBLK, IFDIA)
DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
!$ACC LOOP VECTOR PRIVATE (JLON)
  DO JLON = 1, IFDIA
    CALL FXTRAN_ACDC_GEMM_OPENACC (JLON, JLON, 'N', 'T', NPROMA, ILEVOUT-1, ILEVIN, 1.0_JPRB, ZIN (:,:,JBLK), &
                                 & ILEVOUT, PINTE, ILEVOUT, 0.0_JPRB, ZOUT (:,:,JBLK), NPROMA, LLDONE, YLSTACK)
  ENDDO
!$ACC END LOOP 
ENDDO
!$ACC END PARALLEL 
!$ACC END DATA
#endif

LLACC = .TRUE.

!$ACC DATA COPYIN (ZIN, PINTE) COPYOUT (ZOUT) IF (LLACC)
CALL FXTRAN_ACDC_GEMM_MANYBLOCKS (1, IEND, 'N', 'T', NPROMA, ILEVOUT-1, ILEVIN, 1.0_JPRB, ZIN, &
                                & ILEVOUT, PINTE, ILEVOUT, 0.0_JPRB, ZOUT, NPROMA, LLDONE, LLACC, NGPBLKS, YLSTACK)
!$ACC END DATA





ZRMS = 0
ZAVG = 0
ICNT = 0

DO JLEV = 1, ILEVOUT-1
  DO JBLK = 1, NGPBLKS
    IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
    DO JLON = 1, IFDIA
      ZRMS (JLEV) = ZRMS (JLEV) + (ZOUT (JLON, JLEV, JBLK) - ZREF (JLON, JLEV, JBLK)) ** 2
      ZAVG (JLEV) = ZAVG (JLEV) + ABS (ZREF (JLON, JLEV, JBLK))
      ICNT (JLEV) = ICNT (JLEV) + 1
    ENDDO
  ENDDO
ENDDO

ZRMS = SQRT (ZRMS / ICNT)
ZAVG = ZAVG / ICNT

DO JLEV = 1, ILEVOUT-1
  WRITE (0, '(I6,E30.20,E30.20)') JLEV, ZRMS (JLEV), ZAVG (JLEV)
ENDDO

END PROGRAM
