ISIZE = KIND (YD%LVDFLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LVDFLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSDRLC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSDRLC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LZMCON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LZMCON'
ENDIF
KSIZE = KSIZE + ISIZE
