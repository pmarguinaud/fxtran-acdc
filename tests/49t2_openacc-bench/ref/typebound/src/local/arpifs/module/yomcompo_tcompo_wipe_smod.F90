SUBMODULE (YOMCOMPO) YOMCOMPO_TCOMPO_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TCOMPO (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCOMPO), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYEMIS2DAUX_DESC, LYEMIS2D_DESC, LYEMIS3D_DESC
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LYEMIS2D_DESC = ALLOCATED (SELF%YEMIS2D_DESC)
IF (LYEMIS2D_DESC) THEN
  !$ACC EXIT DATA DETACH (SELF%YEMIS2D_DESC)
  DO J1 = LBOUND (SELF%YEMIS2D_DESC, 1), UBOUND (SELF%YEMIS2D_DESC, 1)
    CALL SELF%YEMIS2D_DESC (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%YEMIS2D_DESC)
ENDIF

LYEMIS3D_DESC = ALLOCATED (SELF%YEMIS3D_DESC)
IF (LYEMIS3D_DESC) THEN
  !$ACC EXIT DATA DETACH (SELF%YEMIS3D_DESC)
  DO J1 = LBOUND (SELF%YEMIS3D_DESC, 1), UBOUND (SELF%YEMIS3D_DESC, 1)
    CALL SELF%YEMIS3D_DESC (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%YEMIS3D_DESC)
ENDIF

LYEMIS2DAUX_DESC = ALLOCATED (SELF%YEMIS2DAUX_DESC)
IF (LYEMIS2DAUX_DESC) THEN
  !$ACC EXIT DATA DETACH (SELF%YEMIS2DAUX_DESC)
  DO J1 = LBOUND (SELF%YEMIS2DAUX_DESC, 1), UBOUND (SELF%YEMIS2DAUX_DESC, 1)
    CALL SELF%YEMIS2DAUX_DESC (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DELETE (SELF%YEMIS2DAUX_DESC)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
