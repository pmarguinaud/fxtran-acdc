FUNCTION ACDC_SIZE_TCDDH (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCDDH),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LCADHFSS, LCADHIIS, LCADHSSS, LCADHTIS, LCADHTLS, LCADHTSS, LCADHTTS, LCADHWLS
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LCADHTLS = ALLOCATED (SELF%CADHTLS)
IF (LCADHTLS) THEN
  ISIZE = KIND (SELF%CADHTLS) * SIZE (SELF%CADHTLS) * LEN (SELF%CADHTLS)
