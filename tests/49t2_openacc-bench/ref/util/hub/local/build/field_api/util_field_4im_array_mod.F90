MODULE UTIL_FIELD_4IM_ARRAY_MOD

USE FIELD_4IM_ARRAY_MODULE, ONLY : FIELD_4IM_ARRAY

INTERFACE ACDC_SAVE
MODULE PROCEDURE ACDC_SAVE_FIELD_4IM_ARRAY
END INTERFACE

INTERFACE ACDC_LOAD
MODULE PROCEDURE ACDC_LOAD_FIELD_4IM_ARRAY
END INTERFACE

INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_FIELD_4IM_ARRAY
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_FIELD_4IM_ARRAY
END INTERFACE


INTERFACE ACDC_CRC64
MODULE PROCEDURE ACDC_CRC64_FIELD_4IM_ARRAY
END INTERFACE

INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_FIELD_4IM_ARRAY
END INTERFACE

INTERFACE ACDC_SIZE
MODULE PROCEDURE ACDC_SIZE_FIELD_4IM_ARRAY
END INTERFACE

CONTAINS

SUBROUTINE ACDC_SAVE_FIELD_4IM_ARRAY (SELF, KLUN)
USE FIELD_4IM_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
WRITE (KLUN) LF_P
IF (LF_P) THEN
  CALL SAVE_FIELD_4IM (KLUN, SELF%F_P)
ENDIF
LP_FIELD = ASSOCIATED (SELF%P_FIELD)
WRITE (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  WRITE (KLUN) LBOUND (SELF%P_FIELD)
  WRITE (KLUN) UBOUND (SELF%P_FIELD)
  WRITE (KLUN) SELF%P_FIELD
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_FIELD_4IM_ARRAY (SELF, KLUN)
USE FIELD_4IM_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL4(4), IU4(4)
LOGICAL :: LF_P, LP_FIELD
READ (KLUN) LF_P
IF (LF_P) THEN
  CALL LOAD_FIELD_4IM (KLUN, SELF%F_P)
ELSE
  NULLIFY (SELF%F_P)
ENDIF
READ (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%P_FIELD (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  READ (KLUN) SELF%P_FIELD
ELSE
  NULLIFY (SELF%P_FIELD)
ENDIF
NULLIFY (SELF%P)
END SUBROUTINE

SUBROUTINE ACDC_COPY_FIELD_4IM_ARRAY (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_4IM_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LP_FIELD
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4IM (SELF%F_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P)
ENDIF

LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  !$ACC ENTER DATA CREATE (SELF%P_FIELD)
  !$ACC UPDATE DEVICE (SELF%P_FIELD)
  !$ACC ENTER DATA ATTACH (SELF%P_FIELD)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_FIELD_4IM_ARRAY (SELF)
USE FIELD_4IM_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY), TARGET :: SELF
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_4IM (SELF%F_P)
ENDIF

END SUBROUTINE



SUBROUTINE ACDC_CRC64_FIELD_4IM_ARRAY (SELF, KLUN, CDPATH)
USE FIELD_4IM_UTIL_MODULE
USE FXTRAN_ACDC_CRC64_INTRINSIC_MOD, ONLY : FCRC64 => FXTRAN_ACDC_CRC64
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CLIND = ''
  WRITE (KLUN, '(Z16.16," ",A)') SELF%F_P%CRC64 (), CDPATH//'%F_P'//TRIM(CLIND)
ENDIF

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%P_FIELD), CDPATH//'%P_FIELD'

END SUBROUTINE

SUBROUTINE ACDC_WIPE_FIELD_4IM_ARRAY (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_4IM_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LP_FIELD
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4IM (SELF%F_P)
ENDIF

LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  !$ACC EXIT DATA DETACH (SELF%P_FIELD)
  !$ACC EXIT DATA DELETE (SELF%P_FIELD)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION ACDC_SIZE_FIELD_4IM_ARRAY (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE FIELD_4IM_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_4IM_ARRAY),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LF_P, LP_FIELD
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  ISIZE = KIND (SELF%P_FIELD) * SIZE (SELF%P_FIELD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%P_FIELD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END MODULE
