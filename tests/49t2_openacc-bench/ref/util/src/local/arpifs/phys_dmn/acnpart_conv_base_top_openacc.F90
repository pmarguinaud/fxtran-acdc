SUBROUTINE ACNPART_CONV_BASE_TOP_OPENACC (YDCST, YDML_PHY_MF, KIDIA&
&, KFDIA, KLON, KLEV, PNEBC, PAPRSF, PAPHIF, PTOPC, YDSTACK)
!$ACC ROUTINE (ACNPART_CONV_BASE_TOP_OPENACC) SEQ
USE MODEL_PHYSICS_MF_MOD,ONLY:MODEL_PHYSICS_MF_TYPE
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


TYPE (TCST), INTENT (IN)::YDCST
TYPE (MODEL_PHYSICS_MF_TYPE), INTENT (IN)::YDML_PHY_MF
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
REAL (KIND=JPRB), INTENT (IN)::PNEBC (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPRSF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPHIF (KLON, KLEV)
REAL (KIND=JPRB), INTENT (OUT)::PTOPC (KLON)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
REAL (KIND=JPRB)::ZABINV 
REAL (KIND=JPRB)::ZBETA 
REAL (KIND=JPRB)::ZALPHA 
REAL (KIND=JPRB)::ZNCCRIT
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::JCBASE 
INTEGER (KIND=JPIM)::JCTOP 


JLON = KIDIA



ZNCCRIT=0.1_JPRB
JCTOP =KLEV
JCBASE =KLEV

DO JLEV=KLEV-1, 1,-1
  
  JCTOP =KLEV-MAX ((KLEV-JLEV)*INT (SIGN (1._JPRB, PNEBC (JLON, JLEV)-ZNCCRIT)),(KLEV-JCTOP ))
  JCBASE =KLEV-MAX ((KLEV-JLEV)*SIGN (1_JPIM, JCTOP -KLEV), KLEV-JCBASE )
  
ENDDO


JCTOP =KLEV-MAX ((KLEV-JCTOP )*INT (SIGN (1._JPRB,((PAPHIF (JLON, JCTOP )-PAPHIF&
& (JLON, JCBASE ))/YDCST%RG)-YDML_PHY_MF%YRPHY0%GTOPDEPTH)), 0_JPIM)


ZALPHA =ABS (PNEBC (JLON, JCTOP )-ZNCCRIT)
ZBETA =ABS (ZNCCRIT-PNEBC (JLON, JCTOP -1_JPIM))
ZABINV =1._JPRB/(ZALPHA +ZBETA )
PTOPC (JLON)=PAPRSF (JLON, JCTOP )*ZBETA *ZABINV +PAPRSF (JLON, JCTOP -1_JPIM)*ZALPHA *ZABINV 
PTOPC (JLON)=PAPRSF (JLON, KLEV)*FLOAT (MAX (0_JPIM, SIGN (1_JPIM, JCTOP -KLEV&
&)))+PTOPC (JLON)*FLOAT (MAX (0_JPIM, SIGN (1_JPIM, KLEV-JCTOP -1_JPIM)))



END SUBROUTINE ACNPART_CONV_BASE_TOP_OPENACC
