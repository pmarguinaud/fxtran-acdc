FUNCTION ACDC_SIZE_TCOM (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCOM),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGTTLIN, LOMLDTH, LSSTMSK, LSSTPRE, LTRAFLX
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%NVCOM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVCOM'
ENDIF
KSIZE = KSIZE + ISIZE
LOMLDTH = ALLOCATED (SELF%OMLDTH)
IF (LOMLDTH) THEN
  ISIZE = KIND (SELF%OMLDTH) * SIZE (SELF%OMLDTH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OMLDTH'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LGTTLIN = ALLOCATED (SELF%GTTLIN)
IF (LGTTLIN) THEN
  ISIZE = KIND (SELF%GTTLIN) * SIZE (SELF%GTTLIN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GTTLIN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSSTPRE = ALLOCATED (SELF%SSTPRE)
IF (LSSTPRE) THEN
  ISIZE = KIND (SELF%SSTPRE) * SIZE (SELF%SSTPRE)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SSTPRE'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSSTMSK = ALLOCATED (SELF%SSTMSK)
IF (LSSTMSK) THEN
  ISIZE = KIND (SELF%SSTMSK) * SIZE (SELF%SSTMSK)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SSTMSK'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%LOMLDTH)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LOMLDTH'
ENDIF
KSIZE = KSIZE + ISIZE
LTRAFLX = ALLOCATED (SELF%TRAFLX)
IF (LTRAFLX) THEN
  ISIZE = KIND (SELF%TRAFLX) * SIZE (SELF%TRAFLX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TRAFLX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

