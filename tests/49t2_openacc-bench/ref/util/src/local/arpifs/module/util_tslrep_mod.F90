SUBROUTINE ACDC_SAVE_TSLREP (SELF, KLUN)

IMPLICIT NONE
TYPE (TSLREP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN
WRITE (KLUN) SELF%NGPTOTAD
LNADMAP = ALLOCATED (SELF%NADMAP)
WRITE (KLUN) LNADMAP
IF (LNADMAP) THEN
  WRITE (KLUN) LBOUND (SELF%NADMAP)
  WRITE (KLUN) UBOUND (SELF%NADMAP)
  WRITE (KLUN) SELF%NADMAP
ENDIF
LNADCORE = ALLOCATED (SELF%NADCORE)
WRITE (KLUN) LNADCORE
IF (LNADCORE) THEN
  WRITE (KLUN) LBOUND (SELF%NADCORE)
  WRITE (KLUN) UBOUND (SELF%NADCORE)
  WRITE (KLUN) SELF%NADCORE
ENDIF
LLADCORE = ALLOCATED (SELF%LADCORE)
