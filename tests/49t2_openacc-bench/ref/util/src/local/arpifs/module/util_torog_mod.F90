    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGLM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

