    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RATATX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

