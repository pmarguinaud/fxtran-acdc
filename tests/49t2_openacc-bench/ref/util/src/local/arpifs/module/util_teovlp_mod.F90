SUBROUTINE ACDC_SAVE_TEOVLP (SELF, KLUN)

IMPLICIT NONE
TYPE (TEOVLP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRA1OVLP
LRA1OVLP = ALLOCATED (SELF%RA1OVLP)
WRITE (KLUN) LRA1OVLP
IF (LRA1OVLP) THEN
  WRITE (KLUN) LBOUND (SELF%RA1OVLP)
  WRITE (KLUN) UBOUND (SELF%RA1OVLP)
  WRITE (KLUN) SELF%RA1OVLP
ENDIF
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TEOVLP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TEOVLP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LRA1OVLP
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LRA1OVLP
IF (LRA1OVLP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RA1OVLP (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RA1OVLP,1):UBOUND(SELF%RA1OVLP,1)))
  READ (KLUN) ZTMP1
  SELF%RA1OVLP = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
END SUBROUTINE

