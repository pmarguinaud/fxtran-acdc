  INTEGER (KIND=JPIM) :: JLON, JBLK
  REAL (KIND=JPRB), POINTER :: PLHS (:,:)
  REAL (KIND=JPRB), POINTER :: PRHS (:,:)
  TYPE (CPG_OPTS_TYPE), INTENT (IN), OPTIONAL :: YDCPG_OPTS
  TYPE (CPG_BNDS_TYPE), INTENT (IN), OPTIONAL :: YDCPG_BNDS
  INTEGER (KIND=JPIM) :: JBLKMIN, JBLKMAX
  
