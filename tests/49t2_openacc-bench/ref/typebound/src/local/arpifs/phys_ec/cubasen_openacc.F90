#include "cuadjtq.func.h"

YLSTACK = YDSTACK



IF (KIND (ILAB) == 8) THEN
    fxtran_acdc_alloc8 (ILAB)
ELSEIF (KIND (ILAB) == 4) THEN
    fxtran_acdc_alloc4 (ILAB)
