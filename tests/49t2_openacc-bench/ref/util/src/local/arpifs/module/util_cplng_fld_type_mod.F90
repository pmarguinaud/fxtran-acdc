FUNCTION ACDC_SIZE_CPLNG_FLD_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (CPLNG_FLD_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LD, LID
LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LID = ALLOCATED (SELF%ID)
IF (LID) THEN
  ISIZE = KIND (SELF%ID) * SIZE (SELF%ID)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ID'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (SELF%NAME) * LEN (SELF%NAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%TYPE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TYPE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%INOUT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%INOUT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%STAGE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%STAGE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NUM_LVL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NUM_LVL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NUM_CAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NUM_CAT'
ENDIF
KSIZE = KSIZE + ISIZE
LD = ALLOCATED (SELF%D)
IF (LD) THEN
  ISIZE = KIND (SELF%D) * SIZE (SELF%D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

