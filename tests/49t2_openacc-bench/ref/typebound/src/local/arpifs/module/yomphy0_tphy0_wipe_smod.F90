MODULE SUBROUTINE ACDC_WIPE_TPHY0 (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TPHY0), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LRHUC, LRN1D, LRQSMOD, LRRC1D, LRSRC1D, LRSURF
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LRHUC = ALLOCATED (SELF%RHUC)
IF (LRHUC) THEN
  !$ACC EXIT DATA DETACH (SELF%RHUC)
  !$ACC EXIT DATA DELETE (SELF%RHUC)
ENDIF

LRQSMOD = ALLOCATED (SELF%RQSMOD)
IF (LRQSMOD) THEN
  !$ACC EXIT DATA DETACH (SELF%RQSMOD)
  !$ACC EXIT DATA DELETE (SELF%RQSMOD)
ENDIF

LRSURF = ALLOCATED (SELF%RSURF)
IF (LRSURF) THEN
  !$ACC EXIT DATA DETACH (SELF%RSURF)
  !$ACC EXIT DATA DELETE (SELF%RSURF)
ENDIF

LRN1D = ALLOCATED (SELF%RN1D)
IF (LRN1D) THEN
  !$ACC EXIT DATA DETACH (SELF%RN1D)
  !$ACC EXIT DATA DELETE (SELF%RN1D)
ENDIF

LRRC1D = ALLOCATED (SELF%RRC1D)
IF (LRRC1D) THEN
  !$ACC EXIT DATA DETACH (SELF%RRC1D)
  !$ACC EXIT DATA DELETE (SELF%RRC1D)
ENDIF

LRSRC1D = ALLOCATED (SELF%RSRC1D)
IF (LRSRC1D) THEN
  !$ACC EXIT DATA DETACH (SELF%RSRC1D)
  !$ACC EXIT DATA DELETE (SELF%RSRC1D)
ENDIF

CALL SELF%YRADVPRCS%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

