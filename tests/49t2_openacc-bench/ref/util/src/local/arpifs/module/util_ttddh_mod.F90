READ (KLUN) LCFLDTYPES3D
IF (LCFLDTYPES3D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CFLDTYPES3D (IL1(1):IU1(1)))
  READ (KLUN) SELF%CFLDTYPES3D
ENDIF
READ (KLUN) LCFLDNAMES2D
