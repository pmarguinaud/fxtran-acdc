IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LKEXP'
ENDIF
KSIZE = KSIZE + ISIZE
