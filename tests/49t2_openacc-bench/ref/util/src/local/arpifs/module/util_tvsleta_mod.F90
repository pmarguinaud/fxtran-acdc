  ISIZE = KIND (SELF%VSLDH) * SIZE (SELF%VSLDH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%VSLDH'
  ENDIF
