SUBROUTINE ACDC_SAVE_TOZO (SELF, KLUN)

IMPLICIT NONE
TYPE (TOZO), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LTOZ2DG, LTOZ2DL, LTOZ3DBL
LTOZ2DG = ALLOCATED (SELF%TOZ2DG)
WRITE (KLUN) LTOZ2DG
IF (LTOZ2DG) THEN
  WRITE (KLUN) LBOUND (SELF%TOZ2DG)
  WRITE (KLUN) UBOUND (SELF%TOZ2DG)
  WRITE (KLUN) SELF%TOZ2DG
ENDIF
LTOZ2DL = ALLOCATED (SELF%TOZ2DL)
WRITE (KLUN) LTOZ2DL
IF (LTOZ2DL) THEN
  WRITE (KLUN) LBOUND (SELF%TOZ2DL)
  WRITE (KLUN) UBOUND (SELF%TOZ2DL)
  WRITE (KLUN) SELF%TOZ2DL
ENDIF
LTOZ3DBL = ALLOCATED (SELF%TOZ3DBL)
WRITE (KLUN) LTOZ3DBL
IF (LTOZ3DBL) THEN
  WRITE (KLUN) LBOUND (SELF%TOZ3DBL)
  WRITE (KLUN) UBOUND (SELF%TOZ3DBL)
  WRITE (KLUN) SELF%TOZ3DBL
ENDIF
WRITE (KLUN) SELF%TEECL2
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TOZO (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TOZO), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LTOZ2DG, LTOZ2DL, LTOZ3DBL
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LTOZ2DG
IF (LTOZ2DG) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%TOZ2DG (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%TOZ2DG,1):UBOUND(SELF%TOZ2DG,1), LBOUND(SELF%TOZ2DG,2):UBOUND(SELF%TOZ2DG,2)))
  READ (KLUN) ZTMP2
  SELF%TOZ2DG = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LTOZ2DL
IF (LTOZ2DL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%TOZ2DL (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%TOZ2DL,1):UBOUND(SELF%TOZ2DL,1), LBOUND(SELF%TOZ2DL,2):UBOUND(SELF%TOZ2DL,2)))
  READ (KLUN) ZTMP2
  SELF%TOZ2DL = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LTOZ3DBL
IF (LTOZ3DBL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%TOZ3DBL (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%TOZ3DBL,1):UBOUND(SELF%TOZ3DBL,1), LBOUND(SELF%TOZ3DBL,2):UBOUND(SELF%TOZ3DBL,2)))
  READ (KLUN) ZTMP2
  SELF%TOZ3DBL = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) ZTMP0
SELF%TEECL2 = ZTMP0
END SUBROUTINE

