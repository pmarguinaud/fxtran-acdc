SUBROUTINE CUENTR_OPENACC (YDCST, YDECUMF, YDSPP_CONFIG, KIDIA, KFDIA, KLON, KLEV, KK, KCBOT&
&, KTYPE, LDCUM, LDWORK, PQSEN, PAPH, PGEOH, PMFU, PGP2DSPP, PDMFEN, PDMFDE, YDSTACK)
!$ACC ROUTINE (CUENTR_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
USE YOECUMF,ONLY:TECUMF
USE SPP_MOD,ONLY:TSPP_CONFIG
USE SPP_GEN_MOD,ONLY:SPP_PERT
#include "fxtran_acdc_stack.h"
USE FXTRAN_ACDC_STACK_MOD
USE FXTRAN_ACDC_ABORT_MOD

IMPLICIT NONE


TYPE (TCST), INTENT (IN)::YDCST
TYPE (TECUMF), INTENT (IN)::YDECUMF
TYPE (TSPP_CONFIG), INTENT (IN)::YDSPP_CONFIG
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KK
INTEGER (KIND=JPIM), INTENT (IN)::KCBOT (KLON)
INTEGER (KIND=JPIM), INTENT (IN)::KTYPE (KLON)
LOGICAL, INTENT (IN)::LDCUM (KLON)
LOGICAL, INTENT (IN)::LDWORK
REAL (KIND=JPRB), INTENT (IN)::PQSEN (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PAPH (KLON, KLEV+1)
REAL (KIND=JPRB), INTENT (IN)::PGEOH (KLON, KLEV+1)
REAL (KIND=JPRB), INTENT (IN)::PMFU (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PGP2DSPP (KLON, YDSPP_CONFIG%SM%NRFTOTAL)
REAL (KIND=JPRB), INTENT (OUT)::PDMFEN (KLON)
REAL (KIND=JPRB), INTENT (OUT)::PDMFDE (KLON)
TYPE(FXTRAN_ACDC_STACK), INTENT (IN) :: YDSTACK
LOGICAL::LLO1
INTEGER (KIND=JPIM)::JL
LOGICAL::LLPERT_DETRPEN
INTEGER (KIND=JPIM)::IPDETRPEN
INTEGER (KIND=JPIM)::IPN
TYPE (SPP_PERT)::PN1
REAL (KIND=JPRB)::ZXDETRPEN
REAL (KIND=JPRB)::ZRG
REAL (KIND=JPRB)::ZMF
REAL (KIND=JPRB)::ZENTR 
REAL (KIND=JPRB)::ZDZ


JL = KIDIA





IF (LDWORK) THEN
  ZRG=1.0_JPRB/YDCST%RG
  
  LLPERT_DETRPEN=.FALSE.
  
  
  PDMFEN (JL)=0.0_JPRB
  PDMFDE (JL)=0.0_JPRB
  ZENTR =0.0

  

  

  IF (LDCUM (JL)) THEN
    ZDZ=(PGEOH (JL, KK)-PGEOH (JL, KK+1))*ZRG
    ZMF=PMFU (JL, KK+1)*ZDZ
    LLO1=KK<KCBOT (JL)

    IF (LLO1) THEN
      
      ZXDETRPEN=YDECUMF%DETRPEN
      
      PDMFEN (JL)=ZENTR *ZMF
      PDMFDE (JL)=ZXDETRPEN*ZMF
    ENDIF

  ENDIF

  
ENDIF



ENDSUBROUTINE CUENTR_OPENACC
