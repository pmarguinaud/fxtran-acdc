  ISIZE = KIND (SELF%VSLDW) * SIZE (SELF%VSLDW)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%VSLDW'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LVSLDWH = ALLOCATED (SELF%VSLDWH)
IF (LVSLDWH) THEN
  ISIZE = KIND (SELF%VSLDWH) * SIZE (SELF%VSLDWH)
