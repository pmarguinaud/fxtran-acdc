SUBMODULE (YOMTNH) YOMTNH_TTNH_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_TTNH (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TTNH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LTRAJNH
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LTRAJNH = ALLOCATED (SELF%TRAJNH)
IF (LTRAJNH) THEN
  !$ACC EXIT DATA DETACH (SELF%TRAJNH)
  !$ACC EXIT DATA DELETE (SELF%TRAJNH)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
