SUBROUTINE ACDC_SAVE_TSLPHY (SELF, KLUN)

IMPLICIT NONE
TYPE (TSLPHY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LRSLWX, LSAVTEND, LSAVTEND5
LRSLWX = ALLOCATED (SELF%RSLWX)
WRITE (KLUN) LRSLWX
IF (LRSLWX) THEN
  WRITE (KLUN) LBOUND (SELF%RSLWX)
  WRITE (KLUN) UBOUND (SELF%RSLWX)
  WRITE (KLUN) SELF%RSLWX
ENDIF
WRITE (KLUN) SELF%NVTEND
LSAVTEND = ALLOCATED (SELF%SAVTEND)
WRITE (KLUN) LSAVTEND
IF (LSAVTEND) THEN
  WRITE (KLUN) LBOUND (SELF%SAVTEND)
  WRITE (KLUN) UBOUND (SELF%SAVTEND)
  WRITE (KLUN) SELF%SAVTEND
ENDIF
LSAVTEND5 = ALLOCATED (SELF%SAVTEND5)
WRITE (KLUN) LSAVTEND5
IF (LSAVTEND5) THEN
  WRITE (KLUN) LBOUND (SELF%SAVTEND5)
  WRITE (KLUN) UBOUND (SELF%SAVTEND5)
  WRITE (KLUN) SELF%SAVTEND5
ENDIF
WRITE (KLUN) SELF%MU_SAVTEND
WRITE (KLUN) SELF%MV_SAVTEND
WRITE (KLUN) SELF%MT_SAVTEND
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TSLPHY (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
IMPLICIT NONE
TYPE (TSLPHY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1), IL4(4), IU4(4)
LOGICAL :: LRSLWX, LSAVTEND, LSAVTEND5
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) LRSLWX
IF (LRSLWX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%RSLWX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%RSLWX,1):UBOUND(SELF%RSLWX,1)))
  READ (KLUN) ZTMP1
  SELF%RSLWX = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) SELF%NVTEND
READ (KLUN) LSAVTEND
IF (LSAVTEND) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%SAVTEND (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%SAVTEND,1):UBOUND(SELF%SAVTEND,1), LBOUND(SELF%SAVTEND,2):UBOUND(SELF%SAVTEND,2), LBOUND(SELF%SAVTEND,3):UBOUND(SELF%SAVTEND,3), LBOUND(SELF%SAVTEND,4):UBOUND(SELF%SAVTEND,4)))
  READ (KLUN) ZTMP4
  SELF%SAVTEND = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LSAVTEND5
IF (LSAVTEND5) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%SAVTEND5 (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%SAVTEND5,1):UBOUND(SELF%SAVTEND5,1), LBOUND(SELF%SAVTEND5,2):UBOUND(SELF%SAVTEND5,2), LBOUND(SELF%SAVTEND5,3):UBOUND(SELF%SAVTEND5,3), LBOUND(SELF%SAVTEND5,4):UBOUND(SELF%SAVTEND5,4)))
  READ (KLUN) ZTMP4
  SELF%SAVTEND5 = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) SELF%MU_SAVTEND
READ (KLUN) SELF%MV_SAVTEND
READ (KLUN) SELF%MT_SAVTEND
END SUBROUTINE

