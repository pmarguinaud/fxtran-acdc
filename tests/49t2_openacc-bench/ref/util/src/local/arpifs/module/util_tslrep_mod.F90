IF (LLADCORE) THEN
  WRITE (KLUN) LBOUND (SELF%LADCORE)
  WRITE (KLUN) UBOUND (SELF%LADCORE)
  WRITE (KLUN) SELF%LADCORE
ENDIF
LRSASIGN = ALLOCATED (SELF%RSASIGN)
