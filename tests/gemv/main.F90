PROGRAM GEMV

#include "fxtran_acdc_config.h"

USE FXTRAN_ACDC_GEMV_MOD            , ONLY : FXTRAN_ACDC_GEMV
USE FXTRAN_ACDC_GEMV_MOD_OPENACC    , ONLY : FXTRAN_ACDC_GEMV_OPENACC
USE FXTRAN_ACDC_GEMV_MOD_MANYBLOCKS , ONLY : FXTRAN_ACDC_GEMV_MANYBLOCKS
USE FXTRAN_ACDC_GEMV_MOD_SINGLEBLOCK, ONLY : FXTRAN_ACDC_GEMV_SINGLEBLOCK
USE FXTRAN_ACDC_STACK_MOD           , ONLY : FXTRAN_ACDC_STACK

USE, INTRINSIC :: ISO_FORTRAN_ENV
USE, INTRINSIC :: IEEE_ARITHMETIC

IMPLICIT NONE

INTERFACE
SUBROUTINE SETENV (NAME, VALUE, OVERWRITE) BIND (C, NAME="setenv")
CHARACTER (LEN=*), INTENT (IN) :: NAME
CHARACTER (LEN=*), INTENT (IN) :: VALUE
INTEGER, INTENT (IN), VALUE :: OVERWRITE
END SUBROUTINE
END INTERFACE

INTEGER, PARAMETER :: JPIM = 4, JPRB = 8

REAL (KIND=JPRB) :: ZNAN 

INTEGER (KIND=JPIM) :: NPROMA, ILEVIN, ILEVOUT, NGPBLKS, IST, IEND

REAL (KIND=JPRB), PARAMETER :: ZEPS = 1E-15_JPRB
LOGICAL, PARAMETER :: LLVERBOSE = .FALSE.

REAL (KIND=JPRB), ALLOCATABLE :: ZIN (:,:,:), ZOUTS (:,:), ZINTE (:,:), ZREFS (:,:)

LOGICAL :: LLDONE, LLACC
INTEGER (KIND=JPIM) :: JLON, JLEV, JBLK, IFDIA
TYPE (FXTRAN_ACDC_STACK) :: YLSTACK
INTEGER :: JSIMPLE, JACC
CHARACTER :: CLSIMPLE
CHARACTER (LEN=128) :: CLFILE

ZNAN = IEEE_VALUE (1._JPRB, IEEE_QUIET_NAN)

CALL GETARG (1, CLFILE)

OPEN (77, FILE=TRIM (CLFILE), FORM='UNFORMATTED')
READ (77) NPROMA, ILEVIN, ILEVOUT, IST, IEND, NGPBLKS
ALLOCATE (ZINTE (ILEVOUT, ILEVIN), ZIN (NPROMA, ILEVIN, NGPBLKS), ZOUTS (NPROMA, NGPBLKS), ZREFS (NPROMA, NGPBLKS))
READ (77) ZINTE, ZIN, ZREFS
CLOSE (77)

DO JACC = 0, 1
DO JSIMPLE = 0, 1

LLACC = JACC == 1
WRITE (CLSIMPLE, '(I1.1)') JSIMPLE
CALL SETENV ('FXTRAN_ACDC_SIMPLE_DGEMM'//CHAR (0), CLSIMPLE//CHAR (0), 1)

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMV',             47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUTS = ZNAN
CALL RUN_GEMV
CALL CHECK

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMV_OPENACC',     47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUTS = ZNAN
CALL RUN_GEMV_OPENACC
CALL CHECK

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMV_MANYBLOCKS',  47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUTS = ZNAN
CALL RUN_GEMV_MANYBLOCKS
CALL CHECK

WRITE (*, '(A47," ACC = ",I1," SIMPLE = ",I1)') LEFT ('FXTRAN_ACDC_GEMV_SINGLEBLOCK', 47, '.'), JACC, JSIMPLE
WRITE (*, *)

ZOUTS = ZNAN
CALL RUN_GEMV_SINGLEBLOCK
CALL CHECK

ENDDO
ENDDO

CONTAINS

SUBROUTINE RUN_GEMV 

!$OMP PARALLEL DO PRIVATE (IFDIA)
DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
  CALL FXTRAN_ACDC_GEMV (1, IFDIA, 'N', 'T', NPROMA, 1, ILEVIN, 1.0_JPRB, ZIN (:,:,JBLK), &
                       & NPROMA, ZINTE, ILEVOUT, 0.0_JPRB, ZOUTS (:,JBLK), NPROMA, LLDONE)
ENDDO
!$OMP END PARALLEL DO

END SUBROUTINE

SUBROUTINE RUN_GEMV_OPENACC

#ifdef _FXTRAN_USE_OPENACC
!$ACC DATA COPYIN (ZIN, ZINTE) COPYOUT (ZOUTS) IF (LLACC)
!$ACC PARALLEL LOOP GANG VECTOR PRIVATE (JBLK, IFDIA, JLON) IF (LLACC)
#endif
#ifdef _FXTRAN_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TO: ZIN, ZINTE) MAP (FROM: ZOUTS) IF (LLACC)
!$OMP TARGET TEAMS DISTRIBUTE PARALLEL DO SIMD PRIVATE (JBLK, IFDIA, JLON) IF (LLACC)
#endif
DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
  DO JLON = 1, IFDIA
    CALL FXTRAN_ACDC_GEMV_OPENACC (JLON, JLON, 'N', 'T', NPROMA, 1, ILEVIN, 1.0_JPRB, ZIN (:,:,JBLK), &
                                 & NPROMA, ZINTE, ILEVOUT, 0.0_JPRB, ZOUTS (:,JBLK), NPROMA, LLDONE, YLSTACK)
  ENDDO
ENDDO
#ifdef _FXTRAN_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

END SUBROUTINE

SUBROUTINE RUN_GEMV_MANYBLOCKS 

#ifdef _FXTRAN_USE_OPENACC
!$ACC DATA COPYIN (ZIN, ZINTE) COPYOUT (ZOUTS) IF (LLACC)
#endif
#ifdef _FXTRAN_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TO: ZIN, ZINTE) MAP (FROM: ZOUTS) IF (LLACC)
#endif
CALL FXTRAN_ACDC_GEMV_MANYBLOCKS (1, IEND, 'N', 'T', NPROMA, 1, ILEVIN, 1.0_JPRB, ZIN, &
                                & NPROMA, ZINTE, ILEVOUT, 0.0_JPRB, ZOUTS, NPROMA, LLDONE, LLACC, NGPBLKS, YLSTACK)
#ifdef _FXTRAN_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

END SUBROUTINE

SUBROUTINE RUN_GEMV_SINGLEBLOCK

REAL (KIND=JPRB), ALLOCATABLE :: ZIN1 (:,:), ZOUTS1 (:)

INTEGER (KIND=JPIM) :: NPROMA1

NPROMA1 = NPROMA * NGPBLKS

ALLOCATE (ZIN1 (NPROMA1, ILEVIN), ZOUTS1 (NPROMA1))

DO JBLK = 1, NGPBLKS
  DO JLON = 1, NPROMA
    ZIN1 (NPROMA * (JBLK-1) + JLON, :) = ZIN (JLON, :, JBLK)
  ENDDO
ENDDO

#ifdef _FXTRAN_USE_OPENACC
!$ACC DATA COPYIN (ZIN1, ZINTE) COPYOUT (ZOUTS1) IF (LLACC)
#endif
#ifdef _FXTRAN_USE_OPENMPTARGET
!$OMP TARGET DATA MAP (TO: ZIN1, ZINTE) MAP (FROM: ZOUTS1) IF (LLACC)
#endif
CALL FXTRAN_ACDC_GEMV_SINGLEBLOCK (1, NPROMA1, 'N', 'T', NPROMA1, 1, ILEVIN, 1.0_JPRB, ZIN1, &
                                 & NPROMA1, ZINTE, ILEVOUT, 0.0_JPRB, ZOUTS1, NPROMA1, LLDONE, LLACC)
#ifdef _FXTRAN_USE_OPENACC
!$ACC END DATA
#endif
#ifdef _FXTRAN_USE_OPENMPTARGET
!$OMP END TARGET DATA
#endif

DO JBLK = 1, NGPBLKS
  DO JLON = 1, NPROMA
    ZOUTS (JLON, JBLK) = ZOUTS1 (NPROMA * (JBLK-1) + JLON)
  ENDDO
ENDDO

DEALLOCATE (ZIN1, ZOUTS1)

END SUBROUTINE

SUBROUTINE CHECK

REAL (KIND=JPRB) :: ZRMS, ZAVG
INTEGER (KIND=JPIM) :: ICNT 

ZRMS = 0
ZAVG = 0
ICNT = 0

DO JBLK = 1, NGPBLKS
  IFDIA = MERGE (NPROMA, IEND, JBLK < NGPBLKS)
  DO JLON = 1, IFDIA
    ZRMS = ZRMS + (ZOUTS (JLON, JBLK) - ZREFS (JLON, JBLK)) ** 2
    ZAVG = ZAVG + ABS (ZREFS (JLON, JBLK))
    ICNT = ICNT + 1
  ENDDO
ENDDO

ZRMS = SQRT (ZRMS / ICNT)
ZAVG = ZAVG / ICNT

IF (LLVERBOSE) THEN
WRITE (*, *)
WRITE (*, '(6X,E30.20,E30.20)') ZRMS, ZAVG
WRITE (*, *)
ENDIF

IF (ZRMS < ZEPS) THEN
WRITE (*, *) "  OK  "
ELSE
WRITE (*, *) " FAIL "
ENDIF
WRITE (*, *)

END SUBROUTINE

FUNCTION LEFT (CDSTRING, KLENGTH, CDPAD) RESULT (CLLEFT)

CHARACTER (LEN=*) :: CDSTRING
INTEGER :: KLENGTH
CHARACTER*1, OPTIONAL :: CDPAD

CHARACTER (LEN=KLENGTH) :: CLLEFT
INTEGER :: ILEN, I
CHARACTER*1 :: CLPAD

CLPAD = ' '

IF (PRESENT (CDPAD)) CLPAD = CDPAD

DO I = 1, KLENGTH
  CLLEFT (I:I) = CLPAD
ENDDO

ILEN = LEN_TRIM (CDSTRING)

CLLEFT (1:ILEN) = CDSTRING

END FUNCTION

END PROGRAM
