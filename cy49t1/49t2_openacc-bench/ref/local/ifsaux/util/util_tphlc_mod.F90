SUBROUTINE CRC64_TPHLC (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TPHLC), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ALPHA), CDPATH//'%ALPHA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%AH0), CDPATH//'%AH0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%USTARL), CDPATH//'%USTARL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%USTARS), CDPATH//'%USTARS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ALANDZ0), CDPATH//'%ALANDZ0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ASEAZ0), CDPATH//'%ASEAZ0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSPHLC), CDPATH//'%LSPHLC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LVDFLC), CDPATH//'%LVDFLC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSDRLC), CDPATH//'%LSDRLC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LZMCON), CDPATH//'%LZMCON'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LKEXP), CDPATH//'%LKEXP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LVDFDS), CDPATH//'%LVDFDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSDRDS), CDPATH//'%LSDRDS'

END SUBROUTINE

