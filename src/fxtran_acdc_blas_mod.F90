END FUNCTION

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE3 (P)
REAL*8 :: P (:,:,:)
STRIDE3 = (LOC (P (1, 1, 2)) - LOC (P (1, 1, 1))) / 8
END FUNCTION

INTEGER (KIND=JPSTRIDE) FUNCTION STRIDE2 (P)
REAL*8 :: P (:,:)
STRIDE2 = (LOC (P (1, 2)) - LOC (P (1, 1))) / 8
END FUNCTION

