    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:COMPUTE',1,ZHOOK_HANDLE_COMPUTE)

    IF (FXTRAN_ACDC_LSYNCHOST ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL:RADAER')) THEN
      IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
      &:RADAER:SYNCHOST',0,ZHOOK_HANDLE_FIELD_API)
      PAER => GET_HOST_DATA_RDWR (YD_PAER)
      PAERINDS => GET_HOST_DATA_RDWR (YD_PAERINDS)
      ZAEDES => GET_HOST_DATA_RDWR (YL_ZAEDES)
      ZAELAN => GET_HOST_DATA_RDWR (YL_ZAELAN)
      ZAESEA => GET_HOST_DATA_RDWR (YL_ZAESEA)
      ZAESOO => GET_HOST_DATA_RDWR (YL_ZAESOO)
      ZAESUL => GET_HOST_DATA_RDWR (YL_ZAESUL)
      ZAEVOL => GET_HOST_DATA_RDWR (YL_ZAEVOL)
      Z_YDMF_PHYS_BASE_STATE_T => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_T)
      Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDWR&
      & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
      Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYDF => GET_HOST_DATA_RDWR&
      & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYDF)
      Z_YDMF_PHYS_BASE_STATE_YGSP_RR_T => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%YGSP_RR%F_T)
      Z_YDMF_PHYS_SURF_GSD_VA_PDES => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_DES)
      Z_YDMF_PHYS_SURF_GSD_VA_PLAN => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_LAN)
      Z_YDMF_PHYS_SURF_GSD_VA_PSEA => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_SEA)
      Z_YDMF_PHYS_SURF_GSD_VA_PSOO => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_SOO)
      IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
      &:RADAER:SYNCHOST',1,ZHOOK_HANDLE_FIELD_API)
      
      
    ENDIF

    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:NULLIFY',0,ZHOOK_HANDLE_FIELD_API)
    PAER => NULL ()
    PAERINDS => NULL ()
    ZAEDES => NULL ()
    ZAELAN => NULL ()
    ZAESEA => NULL ()
    ZAESOO => NULL ()
    ZAESUL => NULL ()
    ZAEVOL => NULL ()
    Z_YDMF_PHYS_BASE_STATE_T => NULL ()
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => NULL ()
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYDF => NULL ()
    Z_YDMF_PHYS_BASE_STATE_YGSP_RR_T => NULL ()
    Z_YDMF_PHYS_SURF_GSD_VA_PDES => NULL ()
    Z_YDMF_PHYS_SURF_GSD_VA_PLAN => NULL ()
    Z_YDMF_PHYS_SURF_GSD_VA_PSEA => NULL ()
    Z_YDMF_PHYS_SURF_GSD_VA_PSOO => NULL ()
    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:NULLIFY',1,ZHOOK_HANDLE_FIELD_API)
    
    
  ELSEIF (FXTRAN_ACDC_LPARALLELMETHOD ('OPENMPSINGLECOLUMN','APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER')) THEN
    
    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:GET_DATA',0,ZHOOK_HANDLE_FIELD_API)
    PAER => GET_HOST_DATA_RDWR (YD_PAER)
    PAERINDS => GET_HOST_DATA_RDWR (YD_PAERINDS)
    ZAEDES => GET_HOST_DATA_RDWR (YL_ZAEDES)
    ZAELAN => GET_HOST_DATA_RDWR (YL_ZAELAN)
    ZAESEA => GET_HOST_DATA_RDWR (YL_ZAESEA)
    ZAESOO => GET_HOST_DATA_RDWR (YL_ZAESOO)
    ZAESUL => GET_HOST_DATA_RDWR (YL_ZAESUL)
    ZAEVOL => GET_HOST_DATA_RDWR (YL_ZAEVOL)
    Z_YDMF_PHYS_BASE_STATE_T => GET_HOST_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_T)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDONLY&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYDF => GET_HOST_DATA_RDONLY&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYDF)
    Z_YDMF_PHYS_BASE_STATE_YGSP_RR_T => GET_HOST_DATA_RDONLY (YDMF_PHYS_BASE_STATE%YGSP_RR%F_T)
    Z_YDMF_PHYS_SURF_GSD_VA_PDES => GET_HOST_DATA_RDONLY (YDMF_PHYS_SURF%GSD_VA%F_DES)
    Z_YDMF_PHYS_SURF_GSD_VA_PLAN => GET_HOST_DATA_RDONLY (YDMF_PHYS_SURF%GSD_VA%F_LAN)
    Z_YDMF_PHYS_SURF_GSD_VA_PSEA => GET_HOST_DATA_RDONLY (YDMF_PHYS_SURF%GSD_VA%F_SEA)
    Z_YDMF_PHYS_SURF_GSD_VA_PSOO => GET_HOST_DATA_RDONLY (YDMF_PHYS_SURF%GSD_VA%F_SOO)
    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:GET_DATA',1,ZHOOK_HANDLE_FIELD_API)
    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:COMPUTE',0,ZHOOK_HANDLE_COMPUTE)
    
    !$OMP PARALLEL DO PRIVATE (JBLK, JLON, YLCPG_BNDS, YLSTACK)

    DO JBLK = YDCPG_OPTS%JBLKMIN, YDCPG_OPTS%JBLKMAX

      

      

      DO JLON = 1, MIN (YDCPG_OPTS%KLON, YDCPG_OPTS%KGPCOMP - (JBLK - 1) * YDCPG_OPTS%KLON)
        YLCPG_BNDS%KIDIA = JLON
        YLCPG_BNDS%KFDIA = JLON
        YLSTACK%L8 = fxtran_acdc_stack_l8 (YFXTRAN_ACDC_STACK, (JBLK-YDCPG_OPTS%JBLKMIN)+1, YDCPG_OPTS%KGPBLKS)
        YLSTACK%U8 = fxtran_acdc_stack_u8 (YFXTRAN_ACDC_STACK, (JBLK-YDCPG_OPTS%JBLKMIN)+1, YDCPG_OPTS%KGPBLKS)
        YLSTACK%L4 = fxtran_acdc_stack_l4 (YFXTRAN_ACDC_STACK, (JBLK-YDCPG_OPTS%JBLKMIN)+1, YDCPG_OPTS%KGPBLKS)
        YLSTACK%U4 = fxtran_acdc_stack_u4 (YFXTRAN_ACDC_STACK, (JBLK-YDCPG_OPTS%JBLKMIN)+1, YDCPG_OPTS%KGPBLKS)

        

        IF (LLAERO) THEN
          
          ZAESEA (JLON, JBLK)=Z_YDMF_PHYS_SURF_GSD_VA_PSEA (JLON, JBLK)
          ZAELAN (JLON, JBLK)=Z_YDMF_PHYS_SURF_GSD_VA_PLAN (JLON, JBLK)
          ZAESOO (JLON, JBLK)=Z_YDMF_PHYS_SURF_GSD_VA_PSOO (JLON, JBLK)
          ZAEDES (JLON, JBLK)=Z_YDMF_PHYS_SURF_GSD_VA_PDES (JLON, JBLK)
        
        ELSE
          
          ZAESEA (JLON, JBLK)=0.0_JPRB
          ZAELAN (JLON, JBLK)=0.0_JPRB
          ZAESOO (JLON, JBLK)=0.0_JPRB
          ZAEDES (JLON, JBLK)=0.0_JPRB
        
        ENDIF

        
        ZAESUL (JLON, JBLK)=0.0_JPRB
        
        
        ZAEVOL (JLON, JBLK)=0.0_JPRB

        

        IF ((YDMODEL%YRML_PHY_RAD%YRERAD%NAER/=0).AND.LLAERO) THEN
          CALL RADAER_OPENACC (YDMODEL%YRML_PHY_RAD%YREAERD, YDMODEL%YRML_PHY_RAD%YRERAD, YDMODEL%YRML_PHY_MF&
          &%YRPHY, YLCPG_BNDS%KIDIA, YLCPG_BNDS%KFDIA, YDCPG_OPTS%KLON, YDCPG_OPTS%KFLEVG, Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD&
          &(:,:, JBLK), Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYDF(:,:, JBLK), Z_YDMF_PHYS_BASE_STATE_T&
          &(:,:, JBLK), Z_YDMF_PHYS_BASE_STATE_YGSP_RR_T(:, JBLK), ZAESEA(:, JBLK), ZAELAN(:, JBLK&
          &), ZAESOO(:, JBLK), ZAEDES(:, JBLK), ZAESUL(:, JBLK), ZAEVOL(:, JBLK), PAER(:,:,:, JBLK&
          &), PAERINDS(:,:, JBLK),YDSTACK=YLSTACK)
        ENDIF

      ENDDO

    ENDDO

    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:COMPUTE',1,ZHOOK_HANDLE_COMPUTE)

    IF (FXTRAN_ACDC_LSYNCHOST ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL:RADAER')) THEN
      IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
      &:RADAER:SYNCHOST',0,ZHOOK_HANDLE_FIELD_API)
      PAER => GET_HOST_DATA_RDWR (YD_PAER)
      PAERINDS => GET_HOST_DATA_RDWR (YD_PAERINDS)
      ZAEDES => GET_HOST_DATA_RDWR (YL_ZAEDES)
      ZAELAN => GET_HOST_DATA_RDWR (YL_ZAELAN)
      ZAESEA => GET_HOST_DATA_RDWR (YL_ZAESEA)
      ZAESOO => GET_HOST_DATA_RDWR (YL_ZAESOO)
      ZAESUL => GET_HOST_DATA_RDWR (YL_ZAESUL)
      ZAEVOL => GET_HOST_DATA_RDWR (YL_ZAEVOL)
      Z_YDMF_PHYS_BASE_STATE_T => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_T)
      Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDWR&
      & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
      Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYDF => GET_HOST_DATA_RDWR&
      & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYDF)
      Z_YDMF_PHYS_BASE_STATE_YGSP_RR_T => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%YGSP_RR%F_T)
      Z_YDMF_PHYS_SURF_GSD_VA_PDES => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_DES)
      Z_YDMF_PHYS_SURF_GSD_VA_PLAN => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_LAN)
      Z_YDMF_PHYS_SURF_GSD_VA_PSEA => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_SEA)
      Z_YDMF_PHYS_SURF_GSD_VA_PSOO => GET_HOST_DATA_RDWR (YDMF_PHYS_SURF%GSD_VA%F_SOO)
      IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
      &:RADAER:SYNCHOST',1,ZHOOK_HANDLE_FIELD_API)
      
      
    ENDIF

    IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_AEROSOLS_FOR_RADIATION_PARALLEL&
    &:RADAER:NULLIFY',0,ZHOOK_HANDLE_FIELD_API)
    PAER => NULL ()
    PAERINDS => NULL ()
    ZAEDES => NULL ()
    ZAELAN => NULL ()
    ZAESEA => NULL ()
    ZAESOO => NULL ()
    ZAESUL => NULL ()
