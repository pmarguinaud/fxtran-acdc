MODULE UTIL_SURFACE_VIEW_GROUP_VEXTRDI_MOD

USE SURFACE_VIEWS_DIAGNOSTIC_MODULE, ONLY : SURFACE_VIEW_GROUP_VEXTRDI



INTERFACE ACDC_COPY
MODULE PROCEDURE ACDC_COPY_SURFACE_VIEW_GROUP_VEXTRDI
END INTERFACE

INTERFACE ACDC_HOST
MODULE PROCEDURE ACDC_HOST_SURFACE_VIEW_GROUP_VEXTRDI
END INTERFACE



INTERFACE ACDC_WIPE
MODULE PROCEDURE ACDC_WIPE_SURFACE_VIEW_GROUP_VEXTRDI
END INTERFACE


CONTAINS





SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_VEXTRDI (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VEXTRDI), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_XEDR
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_XEDR = ASSOCIATED (SELF%F_XEDR)
IF (LF_XEDR .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_XEDR)
  !$ACC ENTER DATA ATTACH (SELF%F_XEDR)
ENDIF

END SUBROUTINE

SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_VEXTRDI (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VEXTRDI), TARGET :: SELF
LOGICAL :: LF_XEDR
LF_XEDR = ASSOCIATED (SELF%F_XEDR)
IF (LF_XEDR) THEN
  CALL HOST_FIELD_3RB (SELF%F_XEDR)
ENDIF

END SUBROUTINE





SUBROUTINE ACDC_WIPE_SURFACE_VIEW_GROUP_VEXTRDI (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VEXTRDI), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_XEDR
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_XEDR = ASSOCIATED (SELF%F_XEDR)
IF (LF_XEDR .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_XEDR)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
