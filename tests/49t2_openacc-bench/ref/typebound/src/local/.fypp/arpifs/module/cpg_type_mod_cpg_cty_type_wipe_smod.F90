SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_CTY_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_CTY_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_CTY_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_DIVDP, LF_EVEL, LF_PSDIV, LF_PSDVBC, LF_VVEL
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LF_EVEL = ASSOCIATED (SELF%F_EVEL)
IF (LF_EVEL .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_EVEL)
ENDIF

LF_VVEL = ASSOCIATED (SELF%F_VVEL)
IF (LF_VVEL .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_VVEL)
ENDIF

LF_PSDIV = ASSOCIATED (SELF%F_PSDIV)
IF (LF_PSDIV .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PSDIV)
ENDIF

LF_PSDVBC = ASSOCIATED (SELF%F_PSDVBC)
IF (LF_PSDVBC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_PSDVBC)
ENDIF

LF_DIVDP = ASSOCIATED (SELF%F_DIVDP)
IF (LF_DIVDP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_DIVDP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
