SUBROUTINE MF_PHYS_MOCON_PARALLEL (YDCPG_BNDS, YDCPG_OPTS, YD_PRDG_LCVQ&
&, YD_PMOC_CLPH, YDMF_PHYS, YDMF_PHYS_BASE_STATE)
USE YOMHOOK,ONLY:LHOOK, DR_HOOK, JPHOOK

USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE MF_PHYS_TYPE_MOD,ONLY:MF_PHYS_TYPE
USE MF_PHYS_BASE_STATE_TYPE_MOD,ONLY:MF_PHYS_BASE_STATE_TYPE
USE FIELD_MODULE
USE FIELD_FACTORY_MODULE
USE FIELD_ACCESS_MODULE
USE FXTRAN_ACDC_PARALLELMETHOD_MOD
USE FXTRAN_ACDC_STACK_MOD
USE PARKIND1
USE FXTRAN_ACDC_ARRAY_COPY_MOD
USE FXTRAN_ACDC_GPUMEM_MOD
#include "fxtran_acdc_stack.h"

IMPLICIT NONE


TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
CLASS (FIELD_3RB), POINTER :: YD_PRDG_LCVQ
REAL (KIND=JPRB), POINTER::PRDG_LCVQ (:,:,:)
CLASS (FIELD_2IM), POINTER :: YD_PMOC_CLPH
INTEGER (KIND=JPIM), POINTER::PMOC_CLPH (:,:)
TYPE (MF_PHYS_TYPE), INTENT (INOUT)::YDMF_PHYS
TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT (IN)::YDMF_PHYS_BASE_STATE
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
INTEGER(KIND=JPIM) :: JBLK
TYPE(CPG_BNDS_TYPE) :: YLCPG_BNDS
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE_FIELD_API
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE_PARALLEL
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE_COMPUTE
TYPE(FXTRAN_ACDC_STACK) :: YLSTACK
TYPE(FXTRAN_ACDC_STACK) :: YLOFFSET
REAL (KIND=JPRB), POINTER :: Z_YDMF_PHYS_BASE_STATE_DIV(:,:,:)
REAL (KIND=JPRB), POINTER :: Z_YDMF_PHYS_BASE_STATE_Q(:,:,:)
REAL (KIND=JPRB), POINTER :: Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD(:,:,:)
REAL (KIND=JPRB), POINTER :: Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP(:,:,:)
REAL (KIND=JPRB), POINTER :: Z_YDMF_PHYS_OUT_MOCON(:,:)
#include "abor1.intfb.h"
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL', 0, ZHOOK_HANDLE)

CALL FXTRAN_ACDC_GPUMEMSTAT (__FILE__, __LINE__, "BEGIN")


IF (LHOOK) CALL DR_HOOK ('CREATE_TEMPORARIES',0,ZHOOK_HANDLE_FIELD_API)
IF (LHOOK) CALL DR_HOOK ('CREATE_TEMPORARIES',1,ZHOOK_HANDLE_FIELD_API)
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT',0,ZHOOK_HANDLE_PARALLEL)

IF (FXTRAN_ACDC_LPARALLELMETHOD ('OPENMP','MF_PHYS_MOCON_PARALLEL:PPWETPOINT')) THEN
  
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:GET_DATA',0,ZHOOK_HANDLE_FIELD_API)
  PMOC_CLPH => GET_HOST_DATA_RDONLY (YD_PMOC_CLPH)
  PRDG_LCVQ => GET_HOST_DATA_RDONLY (YD_PRDG_LCVQ)
  Z_YDMF_PHYS_BASE_STATE_DIV => GET_HOST_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_DIV)
  Z_YDMF_PHYS_BASE_STATE_Q => GET_HOST_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_Q)
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDONLY&
  & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => GET_HOST_DATA_RDONLY&
  & (YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%F_DELP)
  Z_YDMF_PHYS_OUT_MOCON => GET_HOST_DATA_RDWR (YDMF_PHYS%OUT%F_MOCON)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:GET_DATA',1,ZHOOK_HANDLE_FIELD_API)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:COMPUTE',0,ZHOOK_HANDLE_COMPUTE)
  
  CALL YLCPG_BNDS%INIT (YDCPG_OPTS)
  !$OMP PARALLEL DO PRIVATE (JBLK, JLEV, JLON) FIRSTPRIVATE (YLCPG_BNDS)

  DO JBLK = YDCPG_OPTS%JBLKMIN, YDCPG_OPTS%JBLKMAX
    CALL YLCPG_BNDS%UPDATE (JBLK)

    

    DO JLEV=1, YDCPG_OPTS%KFLEVG

      DO JLON=YLCPG_BNDS%KIDIA, YLCPG_BNDS%KFDIA
        Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)=Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)+(PRDG_LCVQ (JLON, JLEV, JBLK)-Z_YDMF_PHYS_BASE_STATE_Q&
        & (JLON, JLEV, JBLK)*Z_YDMF_PHYS_BASE_STATE_DIV (JLON, JLEV, JBLK))*Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP&
        & (JLON, JLEV, JBLK)*MAX (0, SIGN (1, JLEV-PMOC_CLPH (JLON, JBLK)))
      ENDDO

    ENDDO


    DO JLON=YLCPG_BNDS%KIDIA, YLCPG_BNDS%KFDIA
      Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)=Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)/(Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD&
      & (JLON, YDCPG_OPTS%KFLEVG, JBLK)-Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD (JLON&
      &, PMOC_CLPH (JLON, JBLK)-1, JBLK))
    ENDDO

  ENDDO

  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:COMPUTE',1,ZHOOK_HANDLE_COMPUTE)

  IF (FXTRAN_ACDC_LSYNCHOST ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT')) THEN
    IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:SYNCHOST',0,ZHOOK_HANDLE_FIELD_API)
    PMOC_CLPH => GET_HOST_DATA_RDWR (YD_PMOC_CLPH)
    PRDG_LCVQ => GET_HOST_DATA_RDWR (YD_PRDG_LCVQ)
    Z_YDMF_PHYS_BASE_STATE_DIV => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_DIV)
    Z_YDMF_PHYS_BASE_STATE_Q => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_Q)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDWR&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => GET_HOST_DATA_RDWR&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%F_DELP)
    Z_YDMF_PHYS_OUT_MOCON => GET_HOST_DATA_RDWR (YDMF_PHYS%OUT%F_MOCON)
    IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:SYNCHOST',1,ZHOOK_HANDLE_FIELD_API)
    
    
  ENDIF

  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:NULLIFY',0,ZHOOK_HANDLE_FIELD_API)
  PMOC_CLPH => NULL ()
  PRDG_LCVQ => NULL ()
  Z_YDMF_PHYS_BASE_STATE_DIV => NULL ()
  Z_YDMF_PHYS_BASE_STATE_Q => NULL ()
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => NULL ()
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => NULL ()
  Z_YDMF_PHYS_OUT_MOCON => NULL ()
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:NULLIFY',1,ZHOOK_HANDLE_FIELD_API)
  
  
ELSEIF (FXTRAN_ACDC_LPARALLELMETHOD ('OPENMPSINGLECOLUMN','MF_PHYS_MOCON_PARALLEL:PPWETPOINT')) THEN
  
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:GET_DATA',0,ZHOOK_HANDLE_FIELD_API)
  PMOC_CLPH => GET_HOST_DATA_RDONLY (YD_PMOC_CLPH)
  PRDG_LCVQ => GET_HOST_DATA_RDONLY (YD_PRDG_LCVQ)
  Z_YDMF_PHYS_BASE_STATE_DIV => GET_HOST_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_DIV)
  Z_YDMF_PHYS_BASE_STATE_Q => GET_HOST_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_Q)
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDONLY&
  & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => GET_HOST_DATA_RDONLY&
  & (YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%F_DELP)
  Z_YDMF_PHYS_OUT_MOCON => GET_HOST_DATA_RDWR (YDMF_PHYS%OUT%F_MOCON)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:GET_DATA',1,ZHOOK_HANDLE_FIELD_API)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:COMPUTE',0,ZHOOK_HANDLE_COMPUTE)
  
  !$OMP PARALLEL DO PRIVATE (JBLK, JLEV, JLON, YLCPG_BNDS)

  DO JBLK = YDCPG_OPTS%JBLKMIN, YDCPG_OPTS%JBLKMAX

    

    

    DO JLON = 1, MIN (YDCPG_OPTS%KLON, YDCPG_OPTS%KGPCOMP - (JBLK - 1) * YDCPG_OPTS%KLON)
      YLCPG_BNDS%KIDIA = JLON
      YLCPG_BNDS%KFDIA = JLON

      

      DO JLEV=1, YDCPG_OPTS%KFLEVG
        
        Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)=Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)+(PRDG_LCVQ (JLON, JLEV, JBLK)-Z_YDMF_PHYS_BASE_STATE_Q&
        & (JLON, JLEV, JBLK)*Z_YDMF_PHYS_BASE_STATE_DIV (JLON, JLEV, JBLK))*Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP&
        & (JLON, JLEV, JBLK)*MAX (0, SIGN (1, JLEV-PMOC_CLPH (JLON, JBLK)))
      
      ENDDO

      
      Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)=Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)/(Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD&
      & (JLON, YDCPG_OPTS%KFLEVG, JBLK)-Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD (JLON&
      &, PMOC_CLPH (JLON, JBLK)-1, JBLK))
    
    ENDDO

  ENDDO

  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:COMPUTE',1,ZHOOK_HANDLE_COMPUTE)

  IF (FXTRAN_ACDC_LSYNCHOST ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT')) THEN
    IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:SYNCHOST',0,ZHOOK_HANDLE_FIELD_API)
    PMOC_CLPH => GET_HOST_DATA_RDWR (YD_PMOC_CLPH)
    PRDG_LCVQ => GET_HOST_DATA_RDWR (YD_PRDG_LCVQ)
    Z_YDMF_PHYS_BASE_STATE_DIV => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_DIV)
    Z_YDMF_PHYS_BASE_STATE_Q => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_Q)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDWR&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => GET_HOST_DATA_RDWR&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%F_DELP)
    Z_YDMF_PHYS_OUT_MOCON => GET_HOST_DATA_RDWR (YDMF_PHYS%OUT%F_MOCON)
    IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:SYNCHOST',1,ZHOOK_HANDLE_FIELD_API)
    
    
  ENDIF

  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:NULLIFY',0,ZHOOK_HANDLE_FIELD_API)
  PMOC_CLPH => NULL ()
  PRDG_LCVQ => NULL ()
  Z_YDMF_PHYS_BASE_STATE_DIV => NULL ()
  Z_YDMF_PHYS_BASE_STATE_Q => NULL ()
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => NULL ()
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => NULL ()
  Z_YDMF_PHYS_OUT_MOCON => NULL ()
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:NULLIFY',1,ZHOOK_HANDLE_FIELD_API)
  
  
ELSEIF (FXTRAN_ACDC_LPARALLELMETHOD ('OPENACCSINGLECOLUMN&
  &','MF_PHYS_MOCON_PARALLEL:PPWETPOINT')) THEN
  
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:GET_DATA',0,ZHOOK_HANDLE_FIELD_API)
  PMOC_CLPH => GET_DEVICE_DATA_RDONLY (YD_PMOC_CLPH)
  PRDG_LCVQ => GET_DEVICE_DATA_RDONLY (YD_PRDG_LCVQ)
  Z_YDMF_PHYS_BASE_STATE_DIV => GET_DEVICE_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_DIV)
  Z_YDMF_PHYS_BASE_STATE_Q => GET_DEVICE_DATA_RDONLY (YDMF_PHYS_BASE_STATE%F_Q)
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_DEVICE_DATA_RDONLY&
  & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => GET_DEVICE_DATA_RDONLY&
  & (YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%F_DELP)
  Z_YDMF_PHYS_OUT_MOCON => GET_DEVICE_DATA_RDWR (YDMF_PHYS%OUT%F_MOCON)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:GET_DATA',1,ZHOOK_HANDLE_FIELD_API)
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:COMPUTE',0,ZHOOK_HANDLE_COMPUTE)
  
  
    !$ACC PARALLEL LOOP GANG &
    !$ACC&PRESENT (PMOC_CLPH, PRDG_LCVQ, YDCPG_OPTS, YFXTRAN_ACDC_STACK, Z_YDMF_PHYS_BASE_STATE_DIV, &
    !$ACC&         Z_YDMF_PHYS_BASE_STATE_Q, Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD, &
    !$ACC&         Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP, Z_YDMF_PHYS_OUT_MOCON) &
    !$ACC&PRIVATE (JBLK) &
    !$ACC&VECTOR_LENGTH (YDCPG_OPTS%KLON) 
    
    DO JBLK = YDCPG_OPTS%JBLKMIN, YDCPG_OPTS%JBLKMAX
    
    
    
    !$ACC LOOP VECTOR &
    !$ACC&PRIVATE (JLEV, JLON, YLCPG_BNDS) 

    

    DO JLON = 1, MIN (YDCPG_OPTS%KLON, YDCPG_OPTS%KGPCOMP - (JBLK - 1) * YDCPG_OPTS%KLON)
      YLCPG_BNDS%KIDIA = JLON
      YLCPG_BNDS%KFDIA = JLON

      

      DO JLEV=1, YDCPG_OPTS%KFLEVG
        
        Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)=Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)+(PRDG_LCVQ (JLON, JLEV, JBLK)-Z_YDMF_PHYS_BASE_STATE_Q&
        & (JLON, JLEV, JBLK)*Z_YDMF_PHYS_BASE_STATE_DIV (JLON, JLEV, JBLK))*Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP&
        & (JLON, JLEV, JBLK)*MAX (0, SIGN (1, JLEV-PMOC_CLPH (JLON, JBLK)))
      
      ENDDO

      
      Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)=Z_YDMF_PHYS_OUT_MOCON (JLON, JBLK)/(Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD&
      & (JLON, YDCPG_OPTS%KFLEVG, JBLK)-Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD (JLON&
      &, PMOC_CLPH (JLON, JBLK)-1, JBLK))
    
    ENDDO

  ENDDO

  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:COMPUTE',1,ZHOOK_HANDLE_COMPUTE)

  IF (FXTRAN_ACDC_LSYNCHOST ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT')) THEN
    IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:SYNCHOST',0,ZHOOK_HANDLE_FIELD_API)
    PMOC_CLPH => GET_HOST_DATA_RDWR (YD_PMOC_CLPH)
    PRDG_LCVQ => GET_HOST_DATA_RDWR (YD_PRDG_LCVQ)
    Z_YDMF_PHYS_BASE_STATE_DIV => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_DIV)
    Z_YDMF_PHYS_BASE_STATE_Q => GET_HOST_DATA_RDWR (YDMF_PHYS_BASE_STATE%F_Q)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => GET_HOST_DATA_RDWR&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%F_PREHYD)
    Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => GET_HOST_DATA_RDWR&
    & (YDMF_PHYS_BASE_STATE%YCPG_PHY%XYB%F_DELP)
    Z_YDMF_PHYS_OUT_MOCON => GET_HOST_DATA_RDWR (YDMF_PHYS%OUT%F_MOCON)
    IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:SYNCHOST',1,ZHOOK_HANDLE_FIELD_API)
    
    
  ENDIF

  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:NULLIFY',0,ZHOOK_HANDLE_FIELD_API)
  PMOC_CLPH => NULL ()
  PRDG_LCVQ => NULL ()
  Z_YDMF_PHYS_BASE_STATE_DIV => NULL ()
  Z_YDMF_PHYS_BASE_STATE_Q => NULL ()
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_PREHYD => NULL ()
  Z_YDMF_PHYS_BASE_STATE_YCPG_PHY_XYB_DELP => NULL ()
  Z_YDMF_PHYS_OUT_MOCON => NULL ()
  IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT:NULLIFY',1,ZHOOK_HANDLE_FIELD_API)
  
  
ELSE
    CALL ABOR1 ('MF_PHYS_MOCON_PARALLEL: NOT METHOD WAS FOUND')
ENDIF

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL:PPWETPOINT',1,ZHOOK_HANDLE_PARALLEL)

CALL FXTRAN_ACDC_GPUMEMSTAT (__FILE__, __LINE__, "END")

IF (LHOOK) CALL DR_HOOK ('DELETE_TEMPORARIES',0,ZHOOK_HANDLE_FIELD_API)
IF (LHOOK) CALL DR_HOOK ('DELETE_TEMPORARIES',1,ZHOOK_HANDLE_FIELD_API)
IF (LHOOK) CALL DR_HOOK ('MF_PHYS_MOCON_PARALLEL', 1, ZHOOK_HANDLE)
END SUBROUTINE MF_PHYS_MOCON_PARALLEL
